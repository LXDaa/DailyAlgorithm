package Hot100.二叉树;

/**
 * @author lxd
 **/
public class Hot040 {
    /**
     * 记录最大直径长度
     */
    private int ans;

    /**
     * <a href="https://leetcode.cn/problems/diameter-of-binary-tree/description/">543. 二叉树的直径</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给定一棵二叉树，你需要计算它的直径长度。一棵二叉树的直径长度是任意两个结点路径长度中的最大值。这条路径可能穿过也可能不穿过根结点。</p>
     * <p>路径长度定义为两个节点之间边的数目，不是节点数目。</p>
     *
     * <h3>💡 核心思路：DFS 后序遍历</h3>
     * <ul>
     *   <li><b>基本思想</b>：对于每个节点，计算以该节点为根的子树的最大直径
     *     <ul>
     *       <li>直径 = 左子树深度 + 右子树深度</li>
     *       <li>在计算深度的过程中更新全局最大直径</li>
     *       <li>最终返回全局最大值</li>
     *     </ul>
     *   </li>
     *   <li><b>为什么用后序遍历</b>：
     *     <ul>
     *       <li>需要先知道左右子树的深度才能计算直径</li>
     *       <li>后序遍历：先处理子节点，再处理父节点</li>
     *       <li>天然适合这种需要子结果的问题</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，每个节点访问一次</li>
     *   <li><b>空间复杂度</b>：O(n)，递归栈空间</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例二叉树：
     *       1
     *      / \
     *     2   3
     *    / \
     *   4   5
     *
     * 【DFS 遍历过程】
     *
     * dfs(1)
     *   ├─ dfs(2)
     *   │   ├─ dfs(4)
     *   │   │   ├─ dfs(null) → 0
     *   │   │   ├─ dfs(null) → 0
     *   │   │   ├─ ans = max(0, 0+0) = 0
     *   │   │   └─ return max(0, 0) + 1 = 1
     *   │   │
     *   │   ├─ dfs(5)
     *   │   │   ├─ dfs(null) → 0
     *   │   │   ├─ dfs(null) → 0
     *   │   │   ├─ ans = max(0, 0+0) = 0
     *   │   │   └─ return max(0, 0) + 1 = 1
     *   │   │
     *   │   ├─ ans = max(0, 1+1) = 2  ← 最大直径在这里！
     *   │   └─ return max(1, 1) + 1 = 2
     *   │
     *   ├─ dfs(3)
     *   │   ├─ dfs(null) → 0
     *   │   ├─ dfs(null) → 0
     *   │   ├─ ans = max(2, 0+0) = 2
     *   │   └─ return max(0, 0) + 1 = 1
     *   │
     *   ├─ ans = max(2, 2+1) = 2
     *   └─ return max(2, 1) + 1 = 3
     *
     * 最终结果：ans = 2 ✅
     *
     * ---
     *
     * 【直径计算图解】
     *
     *       1
     *      / \
     *     2   3
     *    / \
     *   4   5
     *
     * 各节点的直径：
     * - 节点4：左0 + 右0 = 0
     * - 节点5：左0 + 右0 = 0
     * - 节点2：左1 + 右1 = 2 ← 最大直径
     * - 节点3：左0 + 右0 = 0
     * - 节点1：左2 + 右1 = 3（边数，不是节点数）
     *
     * 但路径 4-2-5 长度为 2（两条边），是最长路径
     *
     * 【路径长度说明】
     * 题目中的路径长度是边数，不是节点数：
     * - 4→2→5：两条边，长度为 2
     * - 4→2→1→3：三条边，长度为 3
     *
     * 但在这个例子中，最长路径是经过节点2的路径
     *
     * ---
     *
     * 【递归树结构】
     *
     *              dfs(1)
     *              /    \
     *         dfs(2)   dfs(3)
     *         /   \       |
     *    dfs(4) dfs(5)  return 1
     *       |      |
     *    return 1 return 1
     *         \    /
     *          ans=2
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么用全局变量 ans？
     *    - 需要在递归过程中记录最大值
     *    - 递归函数返回的是深度，不是直径
     *    - 全局变量可以跨递归调用保存状态
     *
     * 2️⃣ dfs 函数返回的是什么？
     *    - 返回以当前节点为根的子树的深度
     *    - 深度 = max(左子树深度, 右子树深度) + 1
     *    - 空节点深度为 0
     *
     * 3️⃣ 直径是如何计算的？
     *    - 对于每个节点，直径 = 左子树深度 + 右子树深度
     *    - 这是穿过当前节点的最长路径
     *    - 不穿过当前节点的路径在子树递归中处理
     *
     * 4️⃣ 为什么 ans 初始化为 0？
     *    - 空树的直径为 0
     *    - 单节点树的直径为 0（没有边）
     *    - 至少需要两个节点才有边
     *
     * 5️⃣ 直径是否一定经过根节点？
     *    - 不一定！直径可能在某个子树内部
     *    - 例如本示例中，最长路径经过节点2而不是根节点1
     *    - 因此必须在每个节点都计算直径
     *
     * 6️⃣ 为什么要在计算深度时更新 ans？
     *    - 后序遍历顺序：先计算左右子树深度
     *    - 然后计算当前节点的直径
     *    - 更新全局最大值
     *    - 最后返回深度给父节点
     *
     * 7️⃣ 与最大深度的关系？
     *    - 最大深度是直径的一部分
     *    - 直径可能需要左右子树的深度之和
     *    - 但深度只取最大值
     *
     * 8️⃣ 完整流程总结：
     *
     *    初始化：
     *    └─ ans = 0
     *
     *    入口函数：
     *    ├─ 调用 dfs(root)
     *    └─ 返回 ans
     *
     *    递归函数 dfs(node)：
     *    ├─ 基本情况：node == null → return 0
     *    ├─ 计算左子树深度：lLen = dfs(node.left)
     *    ├─ 计算右子树深度：rLen = dfs(node.right)
     *    ├─ 更新最大直径：ans = max(ans, lLen + rLen)
     *    └─ 返回当前子树深度：return max(lLen, rLen) + 1
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>直径定义</b>：路径长度是边数，不是节点数</li>
     *   <li><b>全局变量</b>：需要用成员变量保存最大直径</li>
     *   <li><b>递归顺序</b>：必须先计算左右深度，再更新 ans</li>
     *   <li><b>空树处理</b>：root == null 时返回 0</li>
     *   <li><b>单节点树</b>：直径为 0（没有边）</li>
     * </ul>
     *
     * @param root 二叉树的根节点
     * @return 二叉树的直径
     */
    public int diameterOfBinaryTree(TreeNode root) {
        // 🔄 进行深度优先搜索，同时更新最大直径
        dfs(root);
        // ✅ 返回最大直径
        return ans;
    }

    /**
     * DFS 后序遍历，计算子树深度并更新最大直径
     *
     * @param root 当前节点
     * @return 以当前节点为根的子树深度
     */
    public int dfs(TreeNode root) {
        // 🎯 基本情况：空节点，深度为 0
        if (root == null) {
            return 0;
        }

        // 🔄 递归计算左子树深度
        int lLen = dfs(root.left);
        // 🔄 递归计算右子树深度
        int rLen = dfs(root.right);

        // 📈 更新最大直径：当前直径 = 左深度 + 右深度
        ans = Math.max(ans, lLen + rLen);

        // ✅ 返回当前子树深度：max(左深度, 右深度) + 1（加上当前节点）
        return Math.max(lLen, rLen) + 1;
    }
}

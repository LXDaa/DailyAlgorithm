package Hot100.二叉树;

/**
 * @author lxd
 **/
public class Hot046 {
    /**
     * <a href="https://leetcode.cn/problems/flatten-binary-tree-to-linked-list/description/">114. 二叉树展开为链表</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给你二叉树的根结点 root ，请你将它展开为一个单链表：</p>
     * <ul>
     *   <li>展开后的单链表应该同样使用 TreeNode ，其中 right 子指针指向链表中下一个节点，而左子指针始终为 null</li>
     *   <li>展开后的单链表应该与二叉树先序遍历顺序相同</li>
     * </ul>
     *
     * <h3>💡 核心思路：后序遍历 + 右子树拼接</h3>
     * <ul>
     *   <li><b>基本思想</b>：使用后序遍历，自底向上地展开
     *     <ul>
     *       <li>先递归处理左子树</li>
     *       <li>再递归处理右子树</li>
     *       <li>最后处理当前节点：将左子树接到右边，原右子树接到左子树的最右节点</li>
     *     </ul>
     *   </li>
     *   <li><b>为什么用后序遍历</b>：
     *     <ul>
     *       <li>需要先处理子树，才能正确拼接</li>
     *       <li>自底向上，保证子树已经展开</li>
     *       <li>避免指针丢失</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，每个节点访问一次</li>
     *   <li><b>空间复杂度</b>：O(n)，递归栈空间，最坏情况为链表</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例二叉树：
     *       1
     *      / \
     *     2   5
     *    / \   \
     *   3   4   6
     *
     * 【后序遍历展开过程】
     *
     * flatten(1)
     *   ├─ flatten(2)
     *   │   ├─ flatten(3)
     *   │   │   ├─ flatten(null) → 返回
     *   │   │   ├─ flatten(null) → 返回
     *   │   │   └─ 左子树为空，无需操作
     *   │   │   返回
     *   │   │
     *   │   ├─ flatten(4)
     *   │   │   ├─ flatten(null) → 返回
     *   │   │   ├─ flatten(null) → 返回
     *   │   │   └─ 左子树为空，无需操作
     *   │   │   返回
     *   │   │
     *   │   └─ 处理节点 2：
     *   │       左子树：3
     *   │       右子树：4
     *   │       找到左子树最右节点：3
     *   │       3.right = 4（原右子树）
     *   │       2.right = 3（原左子树）
     *   │       2.left = null
     *   │       结果：2 → 3 → 4
     *   │
     *   ├─ flatten(5)
     *   │   ├─ flatten(null) → 返回
     *   │   ├─ flatten(6)
     *   │   │   └─ 左子树为空，无需操作
     *   │   └─ 处理节点 5：
     *   │       左子树为空，无需操作
     *   │       结果：5 → 6
     *   │
     *   └─ 处理节点 1：
     *       左子树：2 → 3 → 4
     *       右子树：5 → 6
     *       找到左子树最右节点：4
     *       4.right = 5 → 6（原右子树）
     *       1.right = 2 → 3 → 4 → 5 → 6（原左子树）
     *       1.left = null
     *       结果：1 → 2 → 3 → 4 → 5 → 6
     *
     * 最终结果：1 → 2 → 3 → 4 → 5 → 6 ✅
     *
     * ---
     *
     * 【展开步骤详解】
     *
     * 处理节点 2：
     *   原结构：
     *     2
     *    / \
     *   3   4
     *
     *   步骤1：找到左子树最右节点
     *     temp = 2.left = 3
     *     temp.right == null，所以最右节点是 3
     *
     *   步骤2：将原右子树接到左子树最右节点
     *     3.right = 4
     *
     *   步骤3：将左子树移到右边
     *     2.right = 3
     *
     *   步骤4：左指针置空
     *     2.left = null
     *
     *   结果：
     *     2
     *      \
     *       3
     *        \
     *         4
     *
     * 处理节点 1：
     *   原结构：
     *       1
     *      / \
     *     2   5
     *    / \   \
     *   3   4   6
     *
     *   步骤1：找到左子树最右节点
     *     temp = 1.left = 2
     *     temp.right = 3, temp = 3
     *     temp.right = 4, temp = 4
     *     temp.right = null，所以最右节点是 4
     *
     *   步骤2：将原右子树接到左子树最右节点
     *     4.right = 5 → 6
     *
     *   步骤3：将左子树移到右边
     *     1.right = 2 → 3 → 4 → 5 → 6
     *
     *   步骤4：左指针置空
     *     1.left = null
     *
     *   结果：
     *     1
     *      \
     *       2
     *        \
     *         3
     *          \
     *           4
     *            \
     *             5
     *              \
     *               6
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么要找左子树的最右节点？
     *    - 需要将原右子树接到左子树的末尾
     *    - 左子树的最右节点就是左子树的最后一个节点
     *    - 这样才能保证先序遍历的顺序
     *
     * 2️⃣ 为什么用后序遍历？
     *    - 必须先处理子树，才能正确拼接
     *    - 如果用前序遍历，会丢失右子树的信息
     *    - 后序遍历自底向上，保证子树已经展开
     *
     * 3️⃣ 拼接顺序为什么重要？
     *    - 必须先将原右子树接到左子树末尾
     *    - 然后才能将左子树移到右边
     *    - 顺序错误会导致指针丢失
     *
     * 4️⃣ 为什么要将 left 置为 null？
     *    - 题目要求展开后的链表左指针为 null
     *    - 避免循环引用
     *    - 符合单链表的定义
     *
     * 5️⃣ 如果左子树为空怎么办？
     *    - 不需要任何操作
     *    - 直接保持原样（右子树已经在右边）
     *    - 这是递归的优化
     *
     * 6️⃣ 如何保证先序遍历顺序？
     *    - 后序遍历：左 → 右 → 根
     *    - 处理根节点时，将左子树接到右边
     *    - 左子树已经在前面，右子树在后面
     *    - 最终结果就是先序遍历顺序
     *
     * 7️⃣ 能否用其他方法？
     *    - 可以，先序遍历 + 栈
     *    - 可以，先序遍历 + 前驱节点（类似 Morris）
     *    - 后序遍历最简洁，面试首选
     *
     * 8️⃣ 完整流程总结：
     *
     *    递归函数 flatten(node)：
     *    ├─ base case: node == null → return
     *    ├─ 递归处理左子树：flatten(node.left)
     *    ├─ 递归处理右子树：flatten(node.right)
     *    └─ 处理当前节点：
     *       ├─ if (node.left == null) → return（无需操作）
     *       ├─ 找到左子树最右节点：temp = node.left
     *       │   while (temp.right != null) temp = temp.right
     *       ├─ 将原右子树接到左子树末尾：temp.right = node.right
     *       ├─ 将左子树移到右边：node.right = node.left
     *       └─ 左指针置空：node.left = null
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>遍历顺序</b>：必须是后序遍历（左-右-根），不能是前序</li>
     *   <li><b>拼接顺序</b>：先接右子树到左子树末尾，再移动左子树</li>
     *   <li><b>找最右节点</b>：必须找到左子树的最右节点，不是最左</li>
     *   <li><b>左指针置空</b>：展开后必须将 left 置为 null</li>
     *   <li><b>空左子树</b>：如果左子树为空，无需操作</li>
     * </ul>
     *
     * @param root 二叉树的根节点
     */
    public void flatten(TreeNode root) {
        // 🎯 基本情况：空节点，无需处理
        if (root == null) {
            return;
        }

        // 🔄 后序遍历：先处理左右子树
        flatten(root.left);
        flatten(root.right);

        // 📍 处理当前节点：如果左子树不为空，需要拼接
        if (root.left != null) {
            // 🔍 找到左子树的最右节点（左子树的末尾）
            TreeNode temp = root.left;
            while (temp.right != null) {
                temp = temp.right;
            }

            // 🔗 将原右子树接到左子树的末尾
            // 这样左子树展开后，原右子树在后面
            temp.right = root.right;

            // 🔗 将左子树移到右边
            root.right = root.left;

            // 🧹 左指针置空
            root.left = null;
        }
    }
}

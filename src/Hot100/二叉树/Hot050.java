package Hot100.二叉树;

/**
 * @author lxd
 **/
public class Hot050 {
    /**
     * 记录最大路径和
     */
    private int ans = Integer.MIN_VALUE;

    /**
     * <a href="https://leetcode.cn/problems/binary-tree-maximum-path-sum/description/">124. 二叉树中的最大路径和</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>二叉树中的路径被定义为一条节点序列，序列中每对相邻节点之间都存在一条边。同一个节点在一条路径序列中至多出现一次。注意，路径不一定是经过根节点的。</p>
     * <p>路径和是路径中各节点值的总和。给你一个二叉树的根节点 root，返回其最大路径和。</p>
     *
     * <h3>💡 核心思路：DFS 后序遍历 + 负数剪枝</h3>
     * <ul>
     *   <li><b>基本思想</b>：使用后序遍历，计算每个节点作为路径拐点时的最大路径和
     *     <ul>
     *       <li>对于每个节点，计算左子树和右子树的最大贡献值</li>
     *       <li>如果贡献值为负，则不选择该子树（剪枝）</li>
     *       <li>当前节点的最大路径和 = 左贡献 + 右贡献 + 当前节点值</li>
     *       <li>更新全局最大值</li>
     *       <li>返回当前节点对父节点的最大贡献值</li>
     *     </ul>
     *   </li>
     *   <li><b>为什么用后序遍历</b>：
     *     <ul>
     *       <li>需要先知道子树的最大贡献值</li>
     *       <li>后序遍历：先处理子节点，再处理父节点</li>
     *       <li>保证计算当前节点时，子树信息已知</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，每个节点访问一次</li>
     *   <li><b>空间复杂度</b>：O(n)，递归栈空间</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例二叉树：
     *      -10
     *      /  \
     *     9   20
     *        /  \
     *       15   7
     *
     * 【DFS 遍历过程】
     *
     * dfs(-10)
     *   ├─ dfs(9)
     *   │   ├─ dfs(null) → 0
     *   │   ├─ dfs(null) → 0
     *   │   ├─ lVal = max(0, 0) = 0
     *   │   ├─ rVal = max(0, 0) = 0
     *   │   ├─ ans = max(-∞, 0 + 0 + 9) = 9
     *   │   └─ return max(0, 0) + 9 = 9
     *   │
     *   ├─ dfs(20)
     *   │   ├─ dfs(15)
     *   │   │   ├─ dfs(null) → 0
     *   │   │   ├─ dfs(null) → 0
     *   │   │   ├─ lVal = max(0, 0) = 0
     *   │   │   ├─ rVal = max(0, 0) = 0
     *   │   │   ├─ ans = max(9, 0 + 0 + 15) = 15
     *   │   │   └─ return max(0, 0) + 15 = 15
     *   │   │
     *   │   ├─ dfs(7)
     *   │   │   ├─ dfs(null) → 0
     *   │   │   ├─ dfs(null) → 0
     *   │   │   ├─ lVal = max(0, 0) = 0
     *   │   │   ├─ rVal = max(0, 0) = 0
     *   │   │   ├─ ans = max(15, 0 + 0 + 7) = 15
     *   │   │   └─ return max(0, 0) + 7 = 7
     *   │   │
     *   │   ├─ lVal = max(0, 15) = 15
     *   │   ├─ rVal = max(0, 7) = 7
     *   │   ├─ ans = max(15, 15 + 7 + 20) = 42 ✅
     *   │   └─ return max(15, 7) + 20 = 35
     *   │
     *   ├─ lVal = max(0, 9) = 9
     *   ├─ rVal = max(0, 35) = 35
     *   ├─ ans = max(42, 9 + 35 + (-10)) = 42
     *   └─ return max(9, 35) + (-10) = 25
     *
     * 最终结果：ans = 42 ✅
     *
     * ---
     *
     * 【最大路径】
     *
     * 最大路径：15 → 20 → 7
     * 路径和：15 + 20 + 7 = 42
     *
     * 路径图示：
     *      -10
     *      /  \
     *     9   20 ← 拐点
     *        /  \
     *      15   7
     *      ↑    ↑
     *      └────┘
     *       最大路径
     *
     * ---
     *
     * 【负数剪枝示例】
     *
     * 示例二叉树：
     *       1
     *      / \
     *    -2  -3
     *
     * dfs(1)
     *   ├─ dfs(-2)
     *   │   ├─ lVal = max(0, 0) = 0
     *   │   ├─ rVal = max(0, 0) = 0
     *   │   ├─ ans = max(-∞, 0 + 0 + (-2)) = -2
     *   │   └─ return max(0, 0) + (-2) = 0（剪枝！）
     *   │
     *   ├─ dfs(-3)
     *   │   └─ return 0（剪枝！）
     *   │
     *   ├─ lVal = max(0, 0) = 0
     *   ├─ rVal = max(0, 0) = 0
     *   ├─ ans = max(-2, 0 + 0 + 1) = 1
     *   └─ return max(0, 0) + 1 = 1
     *
     * 最终结果：1 ✅
     *
     * 负数剪枝的作用：
     * -2 和 -3 的贡献值为负，不选择
     * 最大路径只包含节点 1
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么要用 max(0, dfs(...))？
     *    - 如果子树的贡献值为负，不选择该子树
     *    - 这就是负数剪枝
     *    - 避免负数降低路径和
     *
     * 2️⃣ 路径和如何计算？
     *    - 当前节点作为拐点时的路径和
     *    - = 左子树最大贡献 + 右子树最大贡献 + 当前节点值
     *    - 这就是以当前节点为拐点的最大路径
     *
     * 3️⃣ 返回值是什么？
     *    - 返回当前节点对父节点的最大贡献值
     *    - = max(左贡献, 右贡献) + 当前节点值
     *    - 父节点可以选择左或右，但不能同时选择
     *
     * 4️⃣ 为什么需要全局变量 ans？
     *    - 需要在递归过程中记录最大值
     *    - 每个节点都可能产生最大路径
     *    - 全局变量可以跨递归调用保存状态
     *
     * 5️⃣ 为什么用后序遍历？
     *    - 需要先知道子树的最大贡献值
     *    - 后序遍历：先子后父
     *    - 保证计算当前节点时，子树信息已知
     *
     * 6️⃣ 路径的定义是什么？
     *    - 路径是节点序列，相邻节点间有边
     *    - 同一节点至多出现一次
     *    - 路径可以不经过根节点
     *    - 路径可以是单个节点
     *
     * 7️⃣ 初始值为什么是 Integer.MIN_VALUE？
     *    - 节点值可能为负
     *    - 需要处理所有可能的值
     *    - 初始值必须小于任何可能的路径和
     *
     * 8️⃣ 完整流程总结：
     *
     *    入口函数：
     *    ├─ ans = Integer.MIN_VALUE
     *    ├─ 调用 dfs(root)
     *    └─ 返回 ans
     *
     *    递归函数 dfs(node)：
     *    ├─ base case: node == null → return 0
     *    ├─ lVal = max(0, dfs(node.left))（负数剪枝）
     *    ├─ rVal = max(0, dfs(node.right))（负数剪枝）
     *    ├─ ans = max(ans, lVal + rVal + node.val)
     *    └─ return max(lVal, rVal) + node.val
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>负数剪枝</b>：必须使用 max(0, dfs(...))，不能直接使用 dfs(...)</li>
     *   <li><b>全局变量</b>：每次调用前会重置，保证正确性</li>
     *   <li><b>返回值</b>：返回的是对父节点的贡献值，不是当前节点的最大路径和</li>
     *   <li><b>路径定义</b>：路径可以不经过根节点，也可以是单个节点</li>
     *   <li><b>初始值</b>：ans 初始值必须足够小，处理所有可能的负值情况</li>
     * </ul>
     *
     * @param root 二叉树的根节点
     * @return 最大路径和
     */
    public int maxPathSum(TreeNode root) {
        // 🔄 进行深度优先搜索，计算最大路径和
        dfs(root);
        // ✅ 返回最大路径和
        return ans;
    }

    /**
     * DFS 后序遍历，计算最大路径和
     *
     * @param node 当前节点
     * @return 当前节点对父节点的最大贡献值
     */
    private int dfs(TreeNode node) {
        // 🎯 基本情况：空节点，贡献值为 0
        if (node == null) {
            return 0;
        }

        // 🔄 递归计算左子树的最大贡献值（负数剪枝）
        int lVal = Math.max(0, dfs(node.left));
        // 🔄 递归计算右子树的最大贡献值（负数剪枝）
        int rVal = Math.max(0, dfs(node.right));

        // 📊 计算以当前节点为拐点的最大路径和
        // 路径和 = 左贡献 + 右贡献 + 当前节点值
        int pathSum = lVal + rVal + node.val;
        // 📈 更新全局最大路径和
        ans = Math.max(ans, pathSum);

        // ✅ 返回当前节点对父节点的最大贡献值
        // 父节点可以选择左或右，但不能同时选择
        return Math.max(lVal, rVal) + node.val;
    }
}

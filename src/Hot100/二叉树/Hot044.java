package Hot100.二叉树;

/**
 * @author lxd
 **/
public class Hot044 {
    /**
     * 记录第 k 小的元素值
     */
    private int ans;
    /**
     * 剩余需要遍历的次数
     */
    private int k;

    /**
     * <a href="https://leetcode.cn/problems/kth-smallest-element-in-a-bst/description/">230. 二叉搜索树中第K小的元素</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给定一个二叉搜索树的根节点 root ，和一个整数 k ，请你设计一个算法查找其中第 k 个最小元素（从 1 开始计数）。</p>
     *
     * <h3>💡 核心思路：中序遍历 + 计数器</h3>
     * <ul>
     *   <li><b>基本思想</b>：利用 BST 中序遍历有序的特性
     *     <ul>
     *       <li>中序遍历顺序：左 → 根 → 右</li>
     *       <li>遍历过程中递减计数器 k</li>
     *       <li>当 k == 0 时，当前节点就是第 k 小的元素</li>
     *       <li>找到后可以提前终止遍历</li>
     *     </ul>
     *   </li>
     *   <li><b>为什么用中序遍历</b>：
     *     <ul>
     *       <li>BST 的中序遍历结果是升序序列</li>
     *       <li>第 k 个访问的节点就是第 k 小的元素</li>
     *       <li>不需要额外排序，时间复杂度 O(k)</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(k)，最多访问 k 个节点</li>
     *   <li><b>空间复杂度</b>：O(h)，递归栈空间，h 是树高</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：BST
     *       5
     *      / \
     *     3   6
     *    / \
     *   2   4
     *  /
     * 1
     *
     * 查找第 3 小的元素（k = 3）
     *
     * 【中序遍历过程】
     *
     * 初始：ans = 0, k = 3
     *
     * dfs(5)
     *   ├─ dfs(3)
     *   │   ├─ dfs(2)
     *   │   │   ├─ dfs(1)
     *   │   │   │   ├─ dfs(null) → 返回
     *   │   │   │   ├─ k-- → k = 2
     *   │   │   │   ├─ k != 0，不记录
     *   │   │   │   └─ dfs(null) → 返回
     *   │   │   │
     *   │   │   ├─ k-- → k = 1
     *   │   │   ├─ k != 0，不记录
     *   │   │   └─ dfs(null) → 返回
     *   │   │
     *   │   ├─ k-- → k = 0
     *   │   ├─ k == 0，记录 ans = 3
     *   │   └─ dfs(4)
     *   │       └─ k <= 0，提前返回
     *   │
     *   └─ k <= 0，提前返回
     *
     * 最终结果：ans = 3 ✅
     *
     * ---
     *
     * 【遍历顺序与 k 值变化】
     *
     * 节点顺序：1 → 2 → 3 → 4 → 5 → 6
     * k 值变化：3 → 2 → 1 → 0（找到）
     *
     * 第1小：1（k=3→2）
     * 第2小：2（k=2→1）
     * 第3小：3（k=1→0）← 找到！
     * 后续节点不再访问（提前终止）
     *
     * ---
     *
     * 【提前终止优化】
     *
     * 如果没有提前终止：
     *   需要遍历所有节点，时间 O(n)
     *
     * 使用提前终止：
     *   只遍历前 k 个节点，时间 O(k)
     *   当 k <= 0 时直接返回，不继续遍历
     *
     * 对比：
     *   k=1（最小元素）：只访问左子树最左节点
     *   k=n（最大元素）：需要访问所有节点
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么用中序遍历？
     *    - BST 的中序遍历结果是升序的
     *    - 第 k 个访问的节点就是第 k 小的元素
     *    - 这是 BST 的核心性质
     *
     * 2️⃣ 为什么用 --k 而不是 k--？
     *    - 两者效果相同，但 --k 更直观
     *    - 在判断条件中使用时，先递减再判断
     *    - 第一次访问节点时 k 就减 1
     *
     * 3️⃣ 为什么要提前终止？
     *    - 找到答案后继续遍历是浪费
     *    - 提前终止可以优化时间复杂度
     *    - 特别是当 k 较小时，性能提升明显
     *
     * 4️⃣ 全局变量的作用是什么？
     *    - ans：存储找到的第 k 小元素
     *    - k：剩余需要遍历的次数
     *    - 跨递归调用保存状态
     *
     * 5️⃣ k 的初始值是什么？
     *    - 题目要求从 1 开始计数
     *    - 所以初始 k 就是传入的参数
     *    - 每访问一个节点，k 减 1
     *
     * 6️⃣ 如何处理 k > 节点总数？
     *    - 题目保证 1 <= k <= 节点总数
     *    - 如果 k 超出范围，最后 ans 会是最后一个节点值
     *    - 实际应用中应该先检查边界
     *
     * 7️⃣ 迭代法如何实现？
     *    - 使用栈模拟中序遍历
     *    - 每次弹出节点时 k--
     *    - 当 k == 0 时返回当前节点值
     *    - 递归更简洁，面试首选
     *
     * 8️⃣ 完整流程总结：
     *
     *    入口函数：
     *    ├─ this.k = k（保存到成员变量）
     *    ├─ 调用 dfs(root)
     *    └─ 返回 ans
     *
     *    递归函数 dfs(node)：
     *    ├─ base case: node == null || k <= 0 → return
     *    ├─ 递归左子树：dfs(node.left)
     *    ├─ k--（访问当前节点）
     *    ├─ if (k == 0) → ans = node.val
     *    └─ 递归右子树：dfs(node.right)
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>遍历顺序</b>：必须是中序遍历（左-根-右）</li>
     *   <li><b>提前终止</b>：k <= 0 时立即返回，避免无效遍历</li>
     *   <li><b>全局变量</b>：每次调用前会重置，保证正确性</li>
     *   <li><b>计数方式</b>：题目从 1 开始计数，不是从 0</li>
     *   <li><b>时间复杂度</b>：O(k)，不是 O(n)，因为提前终止</li>
     * </ul>
     *
     * @param root 二叉搜索树的根节点
     * @param k    第 k 小的元素（从 1 开始计数）
     * @return 第 k 小的元素值
     */
    public int kthSmallest(TreeNode root, int k) {
        // 🔄 将 k 保存到成员变量，供递归使用
        this.k = k;
        // 🔄 进行中序遍历，查找第 k 小的元素
        dfs(root);
        // ✅ 返回找到的第 k 小元素
        return ans;
    }

    /**
     * 中序遍历辅助函数
     *
     * @param node 当前节点
     */
    private void dfs(TreeNode node) {
        // 🎯 基本情况：空节点或已找到答案，直接返回
        if (node == null || k <= 0) {
            return;
        }

        // ⬅️ 递归遍历左子树
        dfs(node.left);

        // 📍 访问当前节点：计数器减 1
        if (--k == 0) {
            // ✅ 找到第 k 小的元素，记录结果
            ans = node.val;
        }

        // ➡️ 递归遍历右子树
        dfs(node.right);
    }
}

package Hot100.双指针;

/**
 * @author lxd
 **/
public class Hot004 {
    /**
     * <a href="https://leetcode.cn/problems/move-zeroes/description/">283. 移动零</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给定一个数组 nums，编写一个函数将所有 0 移动到数组的末尾，同时保持非零元素的相对顺序。</p>
     * <p><b>注意</b>：必须在不复制数组的情况下原地对数组进行操作。</p>
     *
     * <h3>💡 核心思路：双指针法</h3>
     * <ul>
     *   <li><b>基本思想</b>：使用左右两个指针，左指针指向已处理部分的末尾，右指针遍历数组
     *     <ul>
     *       <li>右指针遇到非零元素时，与左指针位置交换</li>
     *       <li>左指针只在交换后前进一步，保证非零元素的相对顺序</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，只需一次遍历</li>
     *   <li><b>空间复杂度</b>：O(1)，原地操作</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：nums = [0, 1, 0, 3, 12]
     *
     * 初始化：l = 0, r = 0
     * 数组状态：[0, 1, 0, 3, 12]
     *
     * 【r=0】nums[0] = 0
     *   - 是 0，不交换
     *   - l = 0, r++ → r = 1
     *   - 数组：[0, 1, 0, 3, 12]
     *
     * 【r=1】nums[1] = 1
     *   - 非 0，交换 nums[1] 和 nums[0]
     *   - l++ → l = 1, r++ → r = 2
     *   - 数组：[1, 0, 0, 3, 12]
     *          ↑l
     *
     * 【r=2】nums[2] = 0
     *   - 是 0，不交换
     *   - l = 1, r++ → r = 3
     *   - 数组：[1, 0, 0, 3, 12]
     *          ↑l
     *
     * 【r=3】nums[3] = 3
     *   - 非 0，交换 nums[3] 和 nums[1]
     *   - l++ → l = 2, r++ → r = 4
     *   - 数组：[1, 3, 0, 0, 12]
     *             ↑l
     *
     * 【r=4】nums[4] = 12
     *   - 非 0，交换 nums[4] 和 nums[2]
     *   - l++ → l = 3, r++ → r = 5
     *   - 数组：[1, 3, 12, 0, 0]
     *                ↑l
     *
     * 最终结果：[1, 3, 12, 0, 0] ✅
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 双指针的含义？
     *    - 左指针 l：指向下一个非零元素应该放置的位置
     *    - 右指针 r：遍历数组，寻找非零元素
     *
     * 2️⃣ 为什么能保持相对顺序？
     *    - 左指针只在遇到非零元素时才前进
     *    - 非零元素按遇到的顺序依次放到前面
     *    - 例如：先遇到 1，再遇到 3，再遇到 12
     *      → 结果就是 [1, 3, 12, ...]
     *
     * 3️⃣ 交换的时机？
     *    - 只有当右指针遇到非零元素时才交换
     *    - 交换后左指针前进一步
     *
     * 4️⃣ 时间复杂度：O(n)
     *    - 右指针遍历整个数组一次
     *    - 每次操作都是 O(1)
     *
     * 5️⃣ 空间复杂度：O(1)
     *    - 只使用了两个指针变量
     *    - 原地操作，不需要额外空间
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>原地操作</b>：不能创建新数组，必须在原数组上修改</li>
     *   <li><b>相对顺序</b>：非零元素的相对顺序不能改变</li>
     *   <li><b>最小操作</b>：尽量减少操作次数（交换次数等于非零元素个数）</li>
     *   <li><b>边界情况</b>：全零数组、无零数组、单元素数组等</li>
     * </ul>
     *
     * @param nums 整数数组
     */
    public void moveZeroes(int[] nums) {
        // 📍 初始化双指针：l 指向下一个非零元素应放位置，r 用于遍历
        int l = 0, r = 0;

        // 🔄 右指针遍历整个数组
        for (; r < nums.length; r++) {
            // ✅ 当右指针遇到非零元素时
            if (nums[r] != 0) {
                // 🔀 将非零元素交换到左指针位置
                int temp = nums[r];
                nums[r] = nums[l];
                nums[l] = temp;
                // ➕ 左指针前进一步
                l++;
            }
        }
    }
}

package Hot100.双指针;

/**
 * @author lxd
 **/
public class Hot007 {
    /**
     * <a href="https://leetcode.cn/problems/trapping-rain-water/description/">42. 接雨水</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给定 n 个非负整数表示每个宽度为 1 的柱子的高度图，计算按此排列的柱子，下雨之后能接多少雨水。</p>
     *
     * <h3>💡 核心思路：双指针法</h3>
     * <ul>
     *   <li><b>基本思想</b>：使用左右两个指针从两端向中间移动，维护左右两侧的最大高度
     *     <ul>
     *       <li>对于当前位置，能接的雨水量 = min(左侧最大高度, 右侧最大高度) - 当前高度</li>
     *       <li>哪侧的最大高度小，就处理哪侧（因为雨水量由较短的一侧决定）</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，只需一次遍历</li>
     *   <li><b>空间复杂度</b>：O(1)，只使用常数额外空间</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：height = [0,1,0,2,1,0,1,3,2,1,2,1]
     *
     * 初始化：l=0, r=11, maxL=0, maxR=1, res=0
     *
     * 图示：
     *         █
     *     ████░█████░█
     *   ██████████████████
     *   0123456789ABC (索引)
     *
     * 【第1轮】maxL=0 <= maxR=1，处理左侧
     *   - l=0: res += 0 - 0 = 0, l++ → l=1
     *   - 更新 maxL = max(0, 1) = 1
     *
     * 【第2轮】maxL=1 <= maxR=1，处理左侧
     *   - l=1: res += 1 - 1 = 0, l++ → l=2
     *   - 更新 maxL = max(1, 0) = 1
     *
     * 【第3轮】maxL=1 <= maxR=1，处理左侧
     *   - l=2: res += 1 - 0 = 1, l++ → l=3
     *   - 更新 maxL = max(1, 2) = 2
     *   - res = 1
     *
     * 【第4轮】maxL=2 > maxR=1，处理右侧
     *   - r=11: res += 1 - 1 = 0, r-- → r=10
     *   - 更新 maxR = max(1, 2) = 2
     *
     * 【第5轮】maxL=2 <= maxR=2，处理左侧
     *   - l=3: res += 2 - 2 = 0, l++ → l=4
     *   - 更新 maxL = max(2, 1) = 2
     *
     * ... 继续直到 l > r
     *
     * 最终结果：res = 6 ✅
     *
     * 接雨水示意：
     *         █
     *     ████░█████░█
     *   ██████████████████
     *   ░ = 雨水位置，共 6 个单位
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么可以用双指针？
     *    - 每个位置能接的雨水量取决于：min(左边最高, 右边最高) - 当前高度
     *    - 如果左边的最大值 <= 右边的最大值，那么当前位置的雨水量由左边决定
     *    - 反之，由右边决定
     *
     * 2️⃣ 如何处理左右两侧？
     *    - 比较 maxL 和 maxR
     *    - 如果 maxL <= maxR：处理左侧，因为左侧是瓶颈
     *      - 雨水量 = maxL - height[l]
     *      - 左指针右移
     *    - 如果 maxL > maxR：处理右侧，因为右侧是瓶颈
     *      - 雨水量 = maxR - height[r]
     *      - 右指针左移
     *
     * 3️⃣ 为什么要实时更新 maxL 和 maxR？
     *    - maxL 记录从左边到当前位置的最大高度
     *    - maxR 记录从右边到当前位置的最大高度
     *    - 每次移动指针时都要更新对应的最大值
     *
     * 4️⃣ 时间复杂度：O(n)
     *    - 左右指针总共移动 n 次
     *    - 每次操作都是 O(1)
     *
     * 5️⃣ 与其他方法的对比：
     *    - 暴力法：对每个位置找左右最大值，O(n²)
     *    - 动态规划：预处理左右最大值数组，O(n) 时间 + O(n) 空间
     *    - 双指针：最优解，O(n) 时间 + O(1) 空间
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>循环条件</b>：while (l <= r)，需要处理到指针相遇</li>
     *   <li><b>更新顺序</b>：先更新 maxL/maxR，再计算雨水量</li>
     *   <li><b>边界情况</b>：空数组或长度小于 3 的数组无法接雨水</li>
     *   <li><b>雨水量计算</b>：确保不会出现负数（maxL/maxR 一定 >= 当前高度）</li>
     * </ul>
     *
     * @param height 表示柱子高度的非负整数数组
     * @return 能接住的雨水总量
     */
    public int trap(int[] height) {
        // 📍 初始化双指针：左指针在起点，右指针在终点
        int l = 0, r = height.length - 1;

        // 📊 初始化左右两侧的最大高度
        int maxL = height[0], maxR = height[r];
        int res = 0;

        // 🔄 当左右指针未相遇时继续
        while (l <= r) {
            // 📈 更新左右两侧的最大高度
            maxL = Math.max(maxL, height[l]);
            maxR = Math.max(maxR, height[r]);

            // 🔀 根据较小的最大高度决定处理哪一侧
            if (maxL <= maxR) {
                // 左侧是瓶颈，计算左侧当前位置的雨水量
                res += maxL - height[l];
                l++;
            } else {
                // 右侧是瓶颈，计算右侧当前位置的雨水量
                res += maxR - height[r];
                r--;
            }
        }

        return res;
    }
}


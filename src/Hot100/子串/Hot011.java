package Hot100.子串;

import java.util.Deque;
import java.util.LinkedList;

/**
 * @author lxd
 **/
public class Hot011 {
    /**
     * <a href="https://leetcode.cn/problems/sliding-window-maximum/description/">239. 滑动窗口最大值</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给你一个整数数组 nums，有一个大小为 k 的滑动窗口从数组的最左侧移动到数组的最右侧。你只可以看到在滑动窗口内的 k 个数字。滑动窗口每次只向右移动一位。</p>
     * <p>返回滑动窗口中的最大值。</p>
     *
     * <h3>💡 核心思路：单调队列（双端队列）</h3>
     * <ul>
     *   <li><b>基本思想</b>：使用双端队列维护一个单调递减的下标序列，队首始终是当前窗口的最大值的下标
     *     <ul>
     *       <li>队列中存储的是元素的下标，而非元素值</li>
     *       <li>保持队列单调递减：新元素加入前，移除所有比它小的队尾元素</li>
     *       <li>移除过期元素：如果队首下标超出窗口范围，将其移除</li>
     *       <li>队首元素即为当前窗口的最大值</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，每个元素最多入队和出队一次</li>
     *   <li><b>空间复杂度</b>：O(k)，队列最多存储 k 个元素</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：nums = [1,3,-1,-3,5,3,6,7], k = 3
     *
     * 初始化：deque = [], res = []
     *
     * 【j=0】nums[0] = 1
     *   - 队列为空，直接加入
     *   - deque = [0]
     *   - j < k-1，不记录结果
     *
     * 【j=1】nums[1] = 3
     *   - nums[0]=1 <= nums[1]=3，移除队尾 0
     *   - deque = []
     *   - 加入 1
     *   - deque = [1]
     *   - j < k-1，不记录结果
     *
     * 【j=2】nums[2] = -1
     *   - nums[1]=3 > nums[2]=-1，不移除
     *   - 加入 2
     *   - deque = [1, 2]
     *   - j >= k-1，记录结果：res[0] = nums[1] = 3
     *   - res = [3]
     *
     * 【j=3】nums[3] = -3
     *   - 检查过期：队首 1 >= j-k+1=1，不过期
     *   - nums[2]=-1 > nums[3]=-3，不移除
     *   - 加入 3
     *   - deque = [1, 2, 3]
     *   - 记录结果：res[1] = nums[1] = 3
     *   - res = [3, 3]
     *
     * 【j=4】nums[4] = 5
     *   - 检查过期：队首 1 < j-k+1=2，过期，移除
     *   - deque = [2, 3]
     *   - nums[3]=-3 <= 5，移除 3
     *   - nums[2]=-1 <= 5，移除 2
     *   - deque = []
     *   - 加入 4
     *   - deque = [4]
     *   - 记录结果：res[2] = nums[4] = 5
     *   - res = [3, 3, 5]
     *
     * 【j=5】nums[5] = 3
     *   - 检查过期：队首 4 >= j-k+1=3，不过期
     *   - nums[4]=5 > nums[5]=3，不移除
     *   - 加入 5
     *   - deque = [4, 5]
     *   - 记录结果：res[3] = nums[4] = 5
     *   - res = [3, 3, 5, 5]
     *
     * 【j=6】nums[6] = 6
     *   - 检查过期：队首 4 < j-k+1=4，过期，移除
     *   - deque = [5]
     *   - nums[5]=3 <= 6，移除 5
     *   - deque = []
     *   - 加入 6
     *   - deque = [6]
     *   - 记录结果：res[4] = nums[6] = 6
     *   - res = [3, 3, 5, 5, 6]
     *
     * 【j=7】nums[7] = 7
     *   - 检查过期：队首 6 >= j-k+1=5，不过期
     *   - nums[6]=6 <= 7，移除 6
     *   - deque = []
     *   - 加入 7
     *   - deque = [7]
     *   - 记录结果：res[5] = nums[7] = 7
     *   - res = [3, 3, 5, 5, 6, 7]
     *
     * 最终结果：[3, 3, 5, 5, 6, 7] ✅
     *
     * 图示：
     *   窗口位置                  最大值
     *   ---------------           -----
     *   [1  3  -1] -3  5  3  6  7   3
     *    1 [3  -1  -3] 5  3  6  7   3
     *    1  3 [-1  -3  5] 3  6  7   5
     *    1  3  -1 [-3  5  3] 6  7   5
     *    1  3  -1  -3 [5  3  6] 7   6
     *    1  3  -1  -3  5 [3  6  7]  7
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么用双端队列？
     *    - 需要从队首获取最大值
     *    - 需要从队尾移除较小元素
     *    - 需要从队首移除过期元素
     *    - 双端队列支持两端操作，O(1) 时间复杂度
     *
     * 2️⃣ 为什么存储下标而不是值？
     *    - 需要判断元素是否过期（超出窗口范围）
     *    - 通过下标可以计算元素的位置
     *    - 通过下标可以访问对应的值
     *
     * 3️⃣ 为什么要保持单调递减？
     *    - 队首始终是最大值
     *    - 如果新元素比队尾大，队尾元素永远不会成为最大值
     *    - 可以安全地移除队尾元素
     *    - 保证队列中都是有潜力成为最大值的元素
     *
     * 4️⃣ 如何判断元素过期？
     *    - 当前窗口范围：[j - k + 1, j]
     *    - 如果队首下标 < j - k + 1，说明已过期
     *    - 需要从队首移除
     *
     * 5️⃣ 时间复杂度分析：
     *    - 每个元素最多入队一次、出队一次
     *    - 总体操作次数是 O(n)
     *    - 比暴力法 O(n*k) 更优
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>边界条件</b>：数组为空或 k=0 时返回空数组</li>
     *   <li><b>存储下标</b>：队列中存储的是下标，不是元素值</li>
     *   <li><b>单调性</b>：保持队列单调递减，允许相等元素存在</li>
     *   <li><b>过期判断</b>：先判断过期，再维护单调性</li>
     * </ul>
     *
     * @param nums 整数数组
     * @param k    滑动窗口大小
     * @return 每个滑动窗口的最大值数组
     */
    public int[] maxSlidingWindow(int[] nums, int k) {
        // ✂️ 边界条件：数组为空或窗口大小为 0
        if (nums.length == 0 || k == 0) {
            return new int[]{};
        }

        // 📋 创建双端队列：存储元素的下标，保持单调递减
        Deque<Integer> deque = new LinkedList<>();
        int[] res = new int[nums.length - k + 1];
        int resIndex = 0;

        // 🔄 遍历数组
        for (int j = 0; j < nums.length; j++) {
            // 🗑️ 移除窗口外的过期元素（队首下标超出窗口范围）
            // 当前窗口范围是 [j - k + 1, j]
            if (!deque.isEmpty() && deque.peekFirst() < j - k + 1) {
                deque.removeFirst();
            }

            // 🔀 保持队列单调递减：移除所有比当前元素小的队尾元素
            while (!deque.isEmpty() && nums[deque.peekLast()] <= nums[j]) {
                deque.removeLast();
            }

            // ➕ 将当前元素的下标加入队尾
            deque.addLast(j);

            // 📊 当形成完整窗口时，记录最大值（队首元素）
            if (j >= k - 1) {
                res[resIndex++] = nums[deque.peekFirst()];
            }
        }

        return res;
    }
}


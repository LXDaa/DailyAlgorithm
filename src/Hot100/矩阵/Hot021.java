package Hot100.矩阵;

/**
 * @author lxd
 **/
public class Hot021 {
    /**
     * <a href="https://leetcode.cn/problems/search-a-2d-matrix-ii/description/">240. 搜索二维矩阵 II</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>编写一个高效的算法来搜索 m x n 矩阵 matrix 中的一个目标值 target 。该矩阵具有以下特性：每行的元素从左到右升序排列。每列的元素从上到下升序排列。</p>
     *
     * <h3>💡 核心思路：从右上角开始的搜索（步进法）</h3>
     * <ul>
     *   <li><b>基本思想</b>：利用矩阵的有序性，从右上角开始搜索，每次排除一行或一列
     *     <ul>
     *       <li>从右上角元素开始（第一行最后一列）</li>
     *       <li>如果当前元素等于 target，找到目标</li>
     *       <li>如果当前元素大于 target，说明 target 在左边，向左移动一列</li>
     *       <li>如果当前元素小于 target，说明 target 在下边，向下移动一行</li>
     *       <li>重复直到找到目标或越界</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(m + n)，最多遍历 m 行 + n 列</li>
     *   <li><b>空间复杂度</b>：O(1)，只使用了常数个变量</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：matrix = [
     *   [1,  4,  7, 11, 15],
     *   [2,  5,  8, 12, 19],
     *   [3,  6,  9, 16, 22],
     *   [10, 13, 14, 17, 24],
     *   [18, 21, 23, 26, 30]
     * ], target = 5
     *
     * 初始化：startRow=0, startCol=4（右上角）
     *
     * 【步骤1】matrix[0][4] = 15
     *   - 15 > 5，目标在左边
     *   - startCol-- → startCol=3
     *   - 排除第4列（所有元素都 > 5）
     *
     * 【步骤2】matrix[0][3] = 11
     *   - 11 > 5，目标在左边
     *   - startCol-- → startCol=2
     *   - 排除第3列（所有元素都 > 5）
     *
     * 【步骤3】matrix[0][2] = 7
     *   - 7 > 5，目标在左边
     *   - startCol-- → startCol=1
     *   - 排除第2列（所有元素都 > 5）
     *
     * 【步骤4】matrix[0][1] = 4
     *   - 4 < 5，目标在下边
     *   - startRow++ → startRow=1
     *   - 排除第0行（该行剩余元素都 < 5）
     *
     * 【步骤5】matrix[1][1] = 5
     *   - 5 == 5，找到目标！✅
     *   - 返回 true
     *
     * 搜索路径图示：
     *   [1,  4,  7, 11, 15]
     *        ↓
     *   [2,  5,  8, 12, 19]
     *        ↑
     *   [3,  6,  9, 16, 22]
     *   [10, 13, 14, 17, 24]
     *   [18, 21, 23, 26, 30]
     *
     *   路径：15 → 11 → 7 → 4 → 5 ✅
     *         ←   ←   ←   ↓
     *
     * 为什么这个方法有效？
     *   - 从右上角开始，这个位置有两个特性：
     *     1. 是该行的最大值
     *     2. 是该列的最小值
     *   - 如果 target < 当前值：target 不可能在当前列（该列都是更大的值），向左移
     *   - 如果 target > 当前值：target 不可能在当前行（该行都是更小的值），向下移
     *   - 每次都能排除一行或一列，效率很高
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么从右上角开始？
     *    - 右上角是特殊的"分水岭"位置
     *    - 左边的元素都比它小，下边的元素都比它大
     *    - 可以根据比较结果确定性地排除一行或一列
     *    - 也可以从左下角开始，原理相同
     *
     * 2️⃣ 为什么不能从左上角或右下角开始？
     *    - 左上角：右边的元素和下边的元素都比它大，无法确定往哪走
     *    - 右下角：左边的元素和上边的元素都比它小，无法确定往哪走
     *    - 只有右上角和左下角有明确的"一边大一边小"的特性
     *
     * 3️⃣ 与二分查找法的对比？
     *    - 逐行二分：对每一行做二分查找 → O(m log n)
     *    - 步进法：从右上角逐步搜索 → O(m + n)
     *    - 当 m 和 n 接近时，O(m + n) 更优
     *    - 步进法代码更简洁，不需要处理二分边界
     *
     * 4️⃣ 为什么这个方法高效？
     *    - 每次比较都能排除一整行或一整列
     *    - 最坏情况：从右上角走到左下角
     *    - 最多走 m + n 步，每步 O(1)
     *    - 比暴力法 O(m × n) 快得多
     *
     * 5️⃣ 循环终止条件？
     *    - startRow >= matrix.length：超出下边界
     *    - startCol < 0：超出左边界
     *    - 任一条件满足说明未找到目标
     *
     * 6️⃣ 矩阵的特性利用？
     *    - 每行从左到右升序：同一行，右边 > 左边
     *    - 每列从上到下升序：同一列，下边 > 上边
     *    - 这两个特性保证了步进法的正确性
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>起始位置</b>：必须从右上角或左下角开始</li>
     *   <li><b>边界检查</b>：循环条件要同时检查行和列的边界</li>
     *   <li><b>移动方向</b>：大于 target 向左移，小于 target 向下移</li>
     *   <li><b>矩阵特性</b>：依赖行列的有序性，无序矩阵不适用</li>
     *   <li><b>返回值</b>：找到返回 true，遍历完未找到返回 false</li>
     * </ul>
     *
     * @param matrix m x n 的有序矩阵
     * @param target 目标值
     * @return 是否找到目标值
     */
    public boolean searchMatrix(int[][] matrix, int target) {
        // 🔧 从右上角开始搜索
        int startRow = 0, startCol = matrix[0].length - 1;

        // 🔄 在矩阵范围内搜索
        while (startRow < matrix.length && startCol >= 0) {
            // ✅ 找到目标
            if (matrix[startRow][startCol] == target) {
                return true;
            } else if (matrix[startRow][startCol] > target) {
                // ⬅️ 当前值太大，向左移动（排除当前列）
                startCol--;
            } else {
                // ⬇️ 当前值太小，向下移动（排除当前行）
                startRow++;
            }

        }

        // ❌ 未找到目标
        return false;
    }
}

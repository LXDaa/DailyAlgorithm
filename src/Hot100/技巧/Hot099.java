public class Hot099 {
    /**
     * <a href="https://leetcode.cn/problems/next-permutation/description/">31. 下一个排列</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>整数数组的一个 排列 就是将其所有成员以序列或线性顺序排列。</p>
     * <p>例如，arr = [1,2,3] ，以下这些都可以视作 arr 的排列：[1,2,3]、[1,3,2]、[3,1,2]、[2,3,1]。</p>
     * <p>整数数组的 下一个排列 是指其整数的下一个字典序更大的排列。如果不存在下一个更大的排列，则将数字重新排列成最小的排列（即升序排列）。</p>
     * <p>必须 原地 修改，只允许使用额外常数空间。</p>
     *
     * <h3>💡 核心思路：三步法</h3>
     * <ul>
     *   <li><b>基本思想</b>：找到下一个字典序更大的排列
     *     <ul>
     *       <li>步骤1：从右向左找第一个升序对 nums[i] < nums[i+1]</li>
     *       <li>步骤2：从右向左找第一个大于 nums[i] 的元素 nums[j]，交换 nums[i] 和 nums[j]</li>
     *       <li>步骤3：反转 nums[i+1..n-1]，使其变为升序</li>
     *       <li>如果整个数组都是降序，则反转整个数组</li>
     *     </ul>
     *   </li>
     *   <li><b>为什么这样做</b>：
     *     <ul>
     *       <li>步骤1找到需要调整的位置，保证后面的元素是降序排列</li>
     *       <li>步骤2找到比 nums[i] 大的最小元素，保证下一个排列是最小的更大排列</li>
     *       <li>步骤3反转后面的元素，使其变为升序，保证是最小的排列</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，最多遍历两次数组 + 一次反转</li>
     *   <li><b>空间复杂度</b>：O(1)，只使用常量空间</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：nums = [1, 3, 2]
     *
     * 步骤1：从右向左找第一个升序对 nums[i] < nums[i+1]
     *   i=0: nums[0]=1 < nums[1]=3 ✅ 找到！
     *   → i = 0
     *
     * 步骤2：从右向左找第一个大于 nums[i] 的元素
     *   j=2: nums[2]=2 > nums[0]=1 ✅ 找到！
     *   swap(nums, 0, 2) → nums = [2, 3, 1]
     *
     * 步骤3：反转 nums[i+1..n-1] = nums[1..2]
     *   swap(nums, 1, 2) → nums = [2, 1, 3]
     *
     * 结果：nums = [2, 1, 3] ✅
     *
     * 示例2：nums = [3, 2, 1]
     *   步骤1：找不到升序对（整个数组降序），i = -1
     *   步骤3：反转整个数组 → nums = [1, 2, 3] ✅
     *
     * 示例3：nums = [1, 2, 3]
     *   步骤1：i=1, nums[1]=2 < nums[2]=3
     *   步骤2：j=2, nums[2]=3 > nums[1]=2
     *   swap(nums, 1, 2) → nums = [1, 3, 2]
     *   步骤3：反转 nums[2..2]（只有一个元素）→ nums = [1, 3, 2] ✅
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 步骤1：如何找到第一个升序对？
     *    - 从右向左遍历：for (i = n-2; i >= 0; i--)
     *    - 找到 nums[i] < nums[i+1] 的位置
     *    - 如果整个数组降序，i = -1
     *    - 这个位置 i 是需要调整的位置
     *
     * 2️⃣ 步骤2：如何找到交换的元素？
     *    - 从右向左遍历：for (j = n-1; j > i; j--)
     *    - 找到第一个 nums[j] > nums[i]
     *    - 交换 nums[i] 和 nums[j]
     *    - 保证交换后 nums[i] 是后面元素中比原来 nums[i] 大的最小元素
     *
     * 3️⃣ 步骤3：为什么要反转？
     *    - 交换后，nums[i+1..n-1] 仍然是降序排列
     *    - 反转后变为升序排列
     *    - 保证是最小的更大排列
     *    - 例如：[1,3,2] → [2,3,1] → [2,1,3]
     *
     * 4️⃣ 如果找不到升序对怎么办？
     *    - 说明整个数组是降序排列
     *    - 直接反转整个数组，得到最小排列（升序）
     *    - 例如：[3,2,1] → [1,2,3]
     *
     * 5️⃣ 为什么从右向左找？
     *    - 下一个排列要求尽可能保持高位不变
     *    - 从右向左找第一个可以调整的位置
     *    - 保证调整后的排列是最小的更大排列
     *
     * 6️⃣ 为什么原地修改？
     *    - 题目要求原地修改，只允许使用额外常数空间
     *    - 反转操作可以原地完成
     *    - 不需要额外的数据结构
     *
     * 7️⃣ 时间复杂度分析：
     *    - 步骤1：最多 O(n)
     *    - 步骤2：最多 O(n)
     *    - 步骤3：最多 O(n)
     *    - 总时间：O(n)
     *
     * 8️⃣ 完整流程总结：
     *
     *    n = nums.length
     *
     *    // 步骤1：从右向左找第一个升序对
     *    i = n - 2
     *    while i >= 0 && nums[i] >= nums[i+1]：
     *      i--
     *
     *    // 步骤2：找到交换的元素并交换
     *    if i >= 0：
     *      j = n - 1
     *      while nums[j] <= nums[i]：
     *        j--
     *      swap(nums, i, j)
     *
     *    // 步骤3：反转后面的元素
     *    l = i + 1, r = n - 1
     *    while l < r：
     *      swap(nums, l, r)
     *      l++, r--
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>步骤1</b>：从右向左找第一个升序对 nums[i] < nums[i+1]</li>
     *   <li><b>步骤2</b>：从右向左找第一个大于 nums[i] 的元素</li>
     *   <li><b>步骤3</b>：反转 nums[i+1..n-1]</li>
     *   <li><b>边界情况</b>：整个数组降序时，反转整个数组</li>
     *   <li><b>原地修改</b>：只允许使用额外常数空间</li>
     * </ul>
     *
     * @param nums 整数数组
     */
    public void nextPermutation(int[] nums) {
        // 📏 获取数组长度
        int n = nums.length;

        // 📊 步骤1：从右向左找第一个升序对 nums[i] < nums[i+1]
        int i;
        for (i = n - 2; i >= 0; i--) {
            if (nums[i] < nums[i + 1]) {
                // 📊 步骤2：从右向左找第一个大于 nums[i] 的元素
                for (int j = n - 1; j > i; j--) {
                    if (nums[j] > nums[i]) {
                        swap(nums, i, j);
                        break;
                    }
                }
                break;
            }
        }

        // 📊 步骤3：反转 nums[i+1..n-1]
        int l = i + 1, r = n - 1;
        while (l < r) {
            swap(nums, l, r);
            l++;
            r--;
        }
    }

    /**
     * 交换数组中的两个元素
     *
     * @param nums 数组
     * @param i    第一个元素的索引
     * @param j    第二个元素的索引
     */
    private void swap(int[] nums, int i, int j) {
        int temp = nums[i];
        nums[i] = nums[j];
        nums[j] = temp;
    }
}

package Hot100.普通数组;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.List;

/**
 * @author lxd
 **/
public class Hot014 {
    /**
     * <a href="https://leetcode.cn/problems/merge-intervals/description/">56. 合并区间</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>以数组 intervals 表示若干个区间的集合，其中单个区间为 intervals[i] = [starti, endi] 。请你合并所有重叠的区间，并返回一个不重叠的区间数组，该数组需恰好覆盖输入中的所有区间。</p>
     *
     * <h3>💡 核心思路：排序 + 贪心</h3>
     * <ul>
     *   <li><b>基本思想</b>：先按区间起点排序，然后依次合并重叠的区间
     *     <ul>
     *       <li>首先将所有区间按照左端点升序排序</li>
     *       <li>遍历排序后的区间，判断当前区间是否与结果列表中的最后一个区间重叠</li>
     *       <li>如果重叠（当前区间的起点 <= 最后一个区间的终点），则合并：更新终点为两者的最大值</li>
     *       <li>如果不重叠，则将当前区间直接加入结果列表</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n log n)，排序的时间复杂度</li>
     *   <li><b>空间复杂度</b>：O(n)，存储结果列表</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：intervals = [[1,3],[2,6],[8,10],[15,18]]
     *
     * 【步骤1】按左端点排序（已经排好序）
     *   [[1,3], [2,6], [8,10], [15,18]]
     *
     * 【步骤2】初始状态
     *   ans = []
     *
     * 【处理 [1,3]】
     *   - ans 为空，直接添加
     *   - ans = [[1,3]]
     *
     * 【处理 [2,6]】
     *   - 检查重叠：ans 最后一个区间是 [1,3]，终点=3
     *   - 当前区间起点=2，2 <= 3，有重叠 ✅
     *   - 合并：更新终点 = max(3, 6) = 6
     *   - ans = [[1,6]]
     *
     * 【处理 [8,10]】
     *   - 检查重叠：ans 最后一个区间是 [1,6]，终点=6
     *   - 当前区间起点=8，8 > 6，无重叠 ❌
     *   - 直接添加
     *   - ans = [[1,6], [8,10]]
     *
     * 【处理 [15,18]】
     *   - 检查重叠：ans 最后一个区间是 [8,10]，终点=10
     *   - 当前区间起点=15，15 > 10，无重叠 ❌
     *   - 直接添加
     *   - ans = [[1,6], [8,10], [15,18]]
     *
     * 最终结果：[[1,6], [8,10], [15,18]] ✅
     *
     * 图示：
     *   原始区间：
     *   [1,3]:   |---|
     *   [2,6]:     |-------|
     *   [8,10]:            |-----|
     *   [15,18]:                   |------|
     *
     *   合并后：
     *   [1,6]:   |-----------|
     *   [8,10]:            |-----|
     *   [15,18]:                   |------|
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么要先排序？
     *    - 排序后，可能重叠的区间会相邻
     *    - 只需要比较当前区间和结果列表的最后一个区间
     *    - 如果不排序，需要两两比较，复杂度更高
     *
     * 2️⃣ 如何判断两个区间是否重叠？
     *    - 设已有区间 [a, b]，当前区间 [c, d]
     *    - 如果 c <= b，说明有重叠
     *    - 因为已经按左端点排序，所以 c >= a 一定成立
     *    - 只需判断 c 是否在 [a, b] 范围内
     *
     * 3️⃣ 如何合并两个重叠区间？
     *    - 新的左端点 = min(a, c)，但因为已排序，所以就是 a
     *    - 新的右端点 = max(b, d)
     *    - 例如：[1,3] 和 [2,6] 合并为 [1,6]
     *
     * 4️⃣ 为什么只和最后一个区间比较？
     *    - 因为已经排序，当前区间的左端点 >= 之前所有区间的左端点
     *    - 如果当前区间和前面的区间重叠，那它一定和最后一个区间重叠
     *    - 这是一个贪心策略：尽可能合并相邻的重叠区间
     *
     * 5️⃣ 边界情况处理？
     *    - 刚好接触：[1,3] 和 [3,5]，3 <= 3，会合并为 [1,5]
     *    - 完全包含：[1,10] 和 [2,5]，合并后还是 [1,10]
     *    - 单个区间：直接返回原数组
     *
     * 6️⃣ 时间复杂度分析：
     *    - 排序：O(n log n)
     *    - 遍历合并：O(n)
     *    - 总体：O(n log n)，由排序决定
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>排序规则</b>：必须按左端点升序排序，这是算法正确性的基础</li>
     *   <li><b>重叠判断</b>：使用 >= 而不是 >，因为刚好接触的区间也要合并</li>
     *   <li><b>合并操作</b>：更新终点时用 Math.max，保留更大的右端点</li>
     *   <li><b>空数组</b>：如果输入为空，直接返回空数组</li>
     *   <li><b>返回值</b>：需要将 List 转换为二维数组</li>
     * </ul>
     *
     * @param intervals 区间数组，每个元素为 [start, end]
     * @return 合并后的不重叠区间数组
     */
    public int[][] merge(int[][] intervals) {
        // 📊 按区间左端点升序排序
        Arrays.sort(intervals, (a, b) -> Integer.compare(a[0], b[0]));

        // 📋 创建结果列表
        List<int[]> ans = new ArrayList<>();

        // 🔄 遍历所有区间
        for (int[] array : intervals) {
            // 🔍 检查是否与结果列表中的最后一个区间重叠
            // 条件：ans 非空 且 最后一个区间的终点 >= 当前区间的起点
            if (!ans.isEmpty() && ans.get(ans.size() - 1)[1] >= array[0]) {
                // ✅ 有重叠，合并区间：更新最后一个区间的终点为两者终点的最大值
                ans.get(ans.size() - 1)[1] = Math.max(array[1], ans.get(ans.size() - 1)[1]);
            } else {
                // ❌ 无重叠，将当前区间添加到结果列表
                ans.add(array);
            }
        }

        // 📤 将 List 转换为二维数组并返回
        return ans.toArray(new int[ans.size()][]);
    }
}

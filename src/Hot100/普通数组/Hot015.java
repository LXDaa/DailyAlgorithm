package Hot100.普通数组;

/**
 * @author lxd
 **/
public class Hot015 {
    /**
     * <a href="https://leetcode.cn/problems/rotate-array/description/">189. 轮转数组</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给定一个整数数组 nums，将数组中的元素向右轮转 k 个位置，其中 k 是非负数。</p>
     *
     * <h3>💡 核心思路：三次反转法</h3>
     * <ul>
     *   <li><b>基本思想</b>：通过三次反转操作实现数组的轮转
     *     <ul>
     *       <li>第一步：反转整个数组</li>
     *       <li>第二步：反转前 k 个元素</li>
     *       <li>第三步：反转剩余的元素</li>
     *       <li>这样可以实现向右轮转 k 个位置的效果</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，每个元素最多被访问两次</li>
     *   <li><b>空间复杂度</b>：O(1)，原地修改，不需要额外空间</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：nums = [1,2,3,4,5,6,7], k = 3
     *
     * 【步骤0】预处理
     *   - k = k % nums.length = 3 % 7 = 3
     *   - 如果 k = 0，说明不需要轮转
     *
     * 【步骤1】反转整个数组 [0, 6]
     *   原始：[1, 2, 3, 4, 5, 6, 7]
     *   反转后：[7, 6, 5, 4, 3, 2, 1]
     *
     *   图示：
     *   1 2 3 4 5 6 7
     *   ↓           ↓
     *   7 6 5 4 3 2 1
     *
     * 【步骤2】反转前 k 个元素 [0, 2]
     *   当前：[7, 6, 5, 4, 3, 2, 1]
     *   反转前3个：[5, 6, 7, 4, 3, 2, 1]
     *
     *   图示：
     *   7 6 5 | 4 3 2 1
     *   ↓ ↓ ↓
     *   5 6 7 | 4 3 2 1
     *
     * 【步骤3】反转剩余元素 [3, 6]
     *   当前：[5, 6, 7, 4, 3, 2, 1]
     *   反转后4个：[5, 6, 7, 1, 2, 3, 4]
     *
     *   图示：
     *   5 6 7 | 4 3 2 1
     *           ↓ ↓ ↓ ↓
     *   5 6 7 | 1 2 3 4
     *
     * 最终结果：[5, 6, 7, 1, 2, 3, 4] ✅
     *
     * 对比验证：
     *   原始数组：[1, 2, 3, 4, 5, 6, 7]
     *   向右轮转3位：
     *   - 最后3个元素 [5, 6, 7] 移到前面
     *   - 前面的元素 [1, 2, 3, 4] 移到后面
     *   结果：[5, 6, 7, 1, 2, 3, 4] ✅
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么 k %= nums.length？
     *    - 如果 k > nums.length，轮转一圈后回到原位
     *    - 例如：长度为7，轮转7次等于没轮转
     *    - 取模可以得到有效的轮转次数
     *    - 例如：k=10, length=7 → k=3
     *
     * 2️⃣ 三次反转的原理？
     *    - 设数组分为两部分：A = [0, n-k-1], B = [n-k, n-1]
     *    - 目标是将 B 移到前面，A 移到后面：B + A
     *    - 第一次反转整个数组：reverse(A+B) = reverse(B) + reverse(A)
     *    - 第二次反转前 k 个：reverse(reverse(B)) = B
     *    - 第三次反转后 n-k 个：reverse(reverse(A)) = A
     *    - 最终结果：B + A ✅
     *
     * 3️⃣ 为什么这个方法好？
     *    - 空间复杂度 O(1)，不需要额外数组
     *    - 代码简洁，逻辑清晰
     *    - 时间复杂度 O(n)，效率高
     *    - 避免了逐个移动元素的 O(n*k) 复杂度
     *
     * 4️⃣ 其他解法对比？
     *    - 暴力法：每次轮转1位，轮转k次 → O(n*k)
     *    - 额外数组法：创建新数组，直接放置 → O(n) 时间，O(n) 空间
     *    - 三次反转法：原地操作 → O(n) 时间，O(1) 空间 ✅最优
     *
     * 5️⃣ reverse 函数的实现？
     *    - 使用双指针，从两端向中间交换
     *    - start 从左向右，end 从右向左
     *    - 当 start >= end 时停止
     *    - 每次交换两个元素，然后移动指针
     *
     * 6️⃣ 边界情况处理？
     *    - k = 0：不需要轮转，直接返回
     *    - k = nums.length：轮转一圈，等于没轮转
     *    - 数组长度为1：任何轮转都不变
     *    - k > nums.length：通过取模处理
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>取模操作</b>：必须先对 k 取模，避免无效轮转</li>
     *   <li><b>原地修改</b>：直接修改原数组，不需要返回值</li>
     *   <li><b>反转范围</b>：注意索引边界，避免越界</li>
     *   <li><b>k=0的情况</b>：取模后 k=0，三次反转都无影响</li>
     *   <li><b>偶数/奇数长度</b>：反转算法对两种情况都适用</li>
     * </ul>
     *
     * @param nums 整数数组
     * @param k    向右轮转的位置数
     */
    public void rotate(int[] nums, int k) {
        // 🔧 预处理：对 k 取模，避免无效轮转
        k %= nums.length;

        // 🔄 第一次反转：反转整个数组
        reverse(nums, 0, nums.length - 1);

        // 🔄 第二次反转：反转前 k 个元素
        reverse(nums, 0, k - 1);

        // 🔄 第三次反转：反转剩余的元素
        reverse(nums, k, nums.length - 1);
    }

    /**
     * 反转数组中指定范围的元素
     *
     * @param nums  数组
     * @param start 起始索引（包含）
     * @param end   结束索引（包含）
     */
    public void reverse(int[] nums, int start, int end) {
        // 🔄 双指针从两端向中间交换
        while (start < end) {
            // 📦 交换 nums[start] 和 nums[end]
            int temp = nums[start];
            nums[start] = nums[end];
            nums[end] = temp;

            // 👉 移动指针
            start++;
            end--;
        }
    }
}

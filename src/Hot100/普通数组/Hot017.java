public class Hot017 {
    /**
     * <a href="https://leetcode.cn/problems/first-missing-positive/description/">41. 缺失的第一个正数</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给你一个未排序的整数数组 nums ，请你找出其中没有出现的最小的正整数。请你实现时间复杂度为 O(n) 并且只使用常数级别额外空间的解决方案。</p>
     *
     * <h3>💡 核心思路：原地哈希（置换法）</h3>
     * <ul>
     *   <li><b>基本思想</b>：将每个正整数放到它应该在的位置上，即 nums[i] 应该放在索引 nums[i]-1 的位置
     *     <ul>
     *       <li>遍历数组，对于每个元素，如果它在 [1, n] 范围内且不在正确位置，就交换到正确位置</li>
     *       <li>继续处理交换过来的新元素，直到当前位置的元素不需要交换</li>
     *       <li>最后遍历数组，第一个不在正确位置的索引+1就是答案</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，每个元素最多被交换一次到正确位置</li>
     *   <li><b>空间复杂度</b>：O(1)，原地修改数组</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：nums = [3, 4, -1, 1]
     *
     * 【步骤1】原地哈希：将每个数放到正确位置
     *   n = 4，正确的数组应该是 [1, 2, 3, 4]
     *
     *   i=0: nums[0] = 3
     *        - 检查：3 在 [1,4] 范围内，且 nums[3-1]=nums[2]=-1 ≠ 3
     *        - 交换 nums[0] 和 nums[2]
     *        - 数组变为：[-1, 4, 3, 1]
     *        - 继续检查 nums[0] = -1
     *        - -1 不在 [1,4] 范围内，停止
     *
     *   i=1: nums[1] = 4
     *        - 检查：4 在 [1,4] 范围内，且 nums[4-1]=nums[3]=1 ≠ 4
     *        - 交换 nums[1] 和 nums[3]
     *        - 数组变为：[-1, 1, 3, 4]
     *        - 继续检查 nums[1] = 1
     *        - 检查：1 在 [1,4] 范围内，且 nums[1-1]=nums[0]=-1 ≠ 1
     *        - 交换 nums[1] 和 nums[0]
     *        - 数组变为：[1, -1, 3, 4]
     *        - 继续检查 nums[1] = -1
     *        - -1 不在 [1,4] 范围内，停止
     *
     *   i=2: nums[2] = 3
     *        - 检查：3 在 [1,4] 范围内，但 nums[3-1]=nums[2]=3 = 3
     *        - 已经在正确位置，不需要交换
     *
     *   i=3: nums[3] = 4
     *        - 检查：4 在 [1,4] 范围内，但 nums[4-1]=nums[3]=4 = 4
     *        - 已经在正确位置，不需要交换
     *
     *   最终数组：[1, -1, 3, 4]
     *
     * 【步骤2】查找第一个缺失的正数
     *   i=0: nums[0] = 1，期望值 = 1，匹配 ✅
     *   i=1: nums[1] = -1，期望值 = 2，不匹配 ❌
     *   - 返回 i + 1 = 2
     *
     * 最终结果：2 ✅
     *
     * 验证：
     *   原始数组：[3, 4, -1, 1]
     *   出现的正数：1, 3, 4
     *   缺失的最小正数：2 ✅
     *
     * 图示：
     *   索引：    0   1   2   3
     *   期望值：  1   2   3   4
     *   实际值：  1  -1   3   4
     *            ✅  ❌  ✅  ✅
     *   第一个不匹配的位置是索引1，所以答案是 1+1=2
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么答案是 [1, n+1] 范围内的数？
     *    - 如果数组是 [1, 2, 3, ..., n]，答案是 n+1
     *    - 否则，答案是 [1, n] 中缺失的那个数
     *    - 所以只需要关心 [1, n] 范围内的数
     *
     * 2️⃣ 为什么要用 while 而不是 if？
     *    - 交换后，当前位置可能有新的数需要处理
     *    - 例如：[3, 4, -1, 1]，i=1 时交换得到 [-1, 1, 3, 4]
     *    - 此时 nums[1]=1 还需要继续交换到位置 0
     *    - while 确保当前位置的数被放到正确位置
     *
     * 3️⃣ 判断条件 nums[nums[i] - 1] != nums[i] 的含义？
     *    - 防止死循环：如果要交换的两个位置值相同，就不交换
     *    - 例如：[1, 1]，如果不加这个条件会无限交换
     *    - 也可以写成：nums[i] != i + 1（更直观）
     *
     * 4️⃣ 为什么这个方法能达到 O(n) 时间？
     *    - 每次交换都会把一个数放到正确位置
     *    - 每个数最多被交换一次到正确位置
     *    - 总共最多 n 次交换，加上遍历，总体 O(n)
     *
     * 5️⃣ 与哈希表法的对比？
     *    - 哈希表法：将所有数放入 HashSet，然后从 1 开始查找 → O(n) 时间，O(n) 空间
     *    - 原地哈希法：利用数组本身作为哈希表 → O(n) 时间，O(1) 空间 ✅
     *    - 原地哈希法更符合题目要求的常数空间
     *
     * 6️⃣ 边界情况处理？
     *    - 空数组：返回 1
     *    - 全负数：返回 1
     *    - 全大于n的数：返回 1
     *    - 连续正数 [1,2,3]：返回 4（n+1）
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>范围判断</b>：只处理 [1, n] 范围内的数，其他数忽略</li>
     *   <li><b>避免死循环</b>：交换前检查两个位置的值是否相同</li>
     *   <li><b>while 循环</b>：确保当前位置的数被放到正确位置</li>
     *   <li><b>索引转换</b>：数值 v 应该放在索引 v-1 的位置</li>
     *   <li><b>返回值</b>：如果所有位置都正确，返回 n+1</li>
     * </ul>
     *
     * @param nums 未排序的整数数组
     * @return 缺失的第一个正数
     */
    public int firstMissingPositive(int[] nums) {
        int n = nums.length;

        // 🔄 第一次遍历：原地哈希，将每个数放到正确位置
        for (int i = 0; i < n; i++) {
            // 🔁 当 nums[i] 在 [1, n] 范围内且不在正确位置时，持续交换
            while (nums[i] >= 1 && nums[i] <= n && nums[nums[i] - 1] != nums[i]) {
                // 📍 计算 nums[i] 应该在的位置
                int index = nums[i] - 1;

                // 🔄 交换 nums[i] 和 nums[index]，将 nums[i] 放到正确位置
                if (nums[i] != nums[index]) {
                    int temp = nums[i];
                    nums[i] = nums[index];
                    nums[index] = temp;
                }
            }
        }

        // 🔍 第二次遍历：查找第一个不在正确位置的数
        for (int i = 0; i < n; i++) {
            // ❌ 如果 nums[i] != i + 1，说明 i + 1 缺失
            if (nums[i] != i + 1) {
                return i + 1;
            }
        }

        // ✅ 如果所有位置都正确，说明缺失的是 n + 1
        return n + 1;

    }
}

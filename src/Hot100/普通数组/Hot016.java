public class Hot016 {
    /**
     * <a href="https://leetcode.cn/problems/product-of-array-except-self/description/">238. 除自身以外数组的乘积</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给你一个整数数组 nums，返回数组 answer ，其中 answer[i] 等于 nums 中除 nums[i] 之外其余各元素的乘积 。题目数据保证数组 nums之中任意元素的全部前缀元素和后缀的乘积都在 32 位整数范围内。请不要使用除法，且在 O(n) 时间复杂度内完成此题。</p>
     *
     * <h3>💡 核心思路：前缀积 + 后缀积</h3>
     * <ul>
     *   <li><b>基本思想</b>：对于每个位置 i，结果 = 左边所有元素的乘积 × 右边所有元素的乘积
     *     <ul>
     *       <li>第一次遍历：计算每个位置左边所有元素的乘积（前缀积）</li>
     *       <li>第二次遍历：从右向左，维护一个后缀积变量，与前缀积相乘得到最终结果</li>
     *       <li>这样避免了使用除法，也避免了 O(n²) 的暴力解法</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，两次遍历数组</li>
     *   <li><b>空间复杂度</b>：O(1)，不计输出数组，只使用了常数额外空间</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：nums = [1, 2, 3, 4]
     *
     * 【步骤1】初始化
     *   n = 4
     *   ans = [0, 0, 0, 0]
     *   ans[0] = 1 （第一个元素左边没有元素，前缀积为1）
     *
     * 【步骤2】计算前缀积（从左到右）
     *   ans[0] = 1 （已初始化）
     *
     *   i=1: ans[1] = nums[0] * ans[0] = 1 * 1 = 1
     *        解释：位置1左边的乘积 = 1
     *
     *   i=2: ans[2] = nums[1] * ans[1] = 2 * 1 = 2
     *        解释：位置2左边的乘积 = 1 * 2 = 2
     *
     *   i=3: ans[3] = nums[2] * ans[2] = 3 * 2 = 6
     *        解释：位置3左边的乘积 = 1 * 2 * 3 = 6
     *
     *   此时 ans = [1, 1, 2, 6]
     *   这表示每个位置左边所有元素的乘积
     *
     * 【步骤3】计算后缀积并得到最终结果（从右到左）
     *   suffix = 1 （最后一个元素右边没有元素，后缀积为1）
     *
     *   i=3: ans[3] *= suffix → ans[3] = 6 * 1 = 6
     *        suffix *= nums[3] → suffix = 1 * 4 = 4
     *        解释：位置3的结果 = 左边乘积(6) × 右边乘积(1) = 6
     *
     *   i=2: ans[2] *= suffix → ans[2] = 2 * 4 = 8
     *        suffix *= nums[2] → suffix = 4 * 3 = 12
     *        解释：位置2的结果 = 左边乘积(2) × 右边乘积(4) = 8
     *
     *   i=1: ans[1] *= suffix → ans[1] = 1 * 12 = 12
     *        suffix *= nums[1] → suffix = 12 * 2 = 24
     *        解释：位置1的结果 = 左边乘积(1) × 右边乘积(3*4=12) = 12
     *
     *   i=0: ans[0] *= suffix → ans[0] = 1 * 24 = 24
     *        suffix *= nums[0] → suffix = 24 * 1 = 24
     *        解释：位置0的结果 = 左边乘积(1) × 右边乘积(2*3*4=24) = 24
     *
     *   最终 ans = [24, 12, 8, 6] ✅
     *
     * 验证：
     *   answer[0] = 2 * 3 * 4 = 24 ✅
     *   answer[1] = 1 * 3 * 4 = 12 ✅
     *   answer[2] = 1 * 2 * 4 = 8  ✅
     *   answer[3] = 1 * 2 * 3 = 6  ✅
     *
     * 图示：
     *   原始数组：[1, 2, 3, 4]
     *
     *   位置0：左边[] → 1，右边[2,3,4] → 24，结果 = 1 * 24 = 24
     *   位置1：左边[1] → 1，右边[3,4] → 12，结果 = 1 * 12 = 12
     *   位置2：左边[1,2] → 2，右边[4] → 4，结果 = 2 * 4 = 8
     *   位置3：左边[1,2,3] → 6，右边[] → 1，结果 = 6 * 1 = 6
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么不能用除法？
     *    - 题目明确要求不使用除法
     *    - 如果可以用除法：先算总乘积，再除以每个元素即可
     *    - 但如果有0元素，除法会出问题
     *    - 不用除法的方法更通用、更安全
     *
     * 2️⃣ 前缀积的计算逻辑？
     *    - ans[i] 表示 nums[0] 到 nums[i-1] 的乘积
     *    - ans[0] = 1（左边没有元素）
     *    - ans[i] = nums[i-1] * ans[i-1]（递推关系）
     *    - 例如：ans[3] = nums[2] * ans[2] = 3 * 2 = 6
     *
     * 3️⃣ 后缀积的处理方式？
     *    - 不需要额外数组存储后缀积
     *    - 用一个变量 suffix 从右向左累乘
     *    - 每次先更新 ans[i]，再更新 suffix
     *    - 这样节省空间，达到 O(1) 额外空间
     *
     * 4️⃣ 为什么这个方法巧妙？
     *    - 将问题分解为两个子问题：前缀积和后缀积
     *    - 利用输出数组存储前缀积，避免额外空间
     *    - 后缀积用单个变量动态计算
     *    - 两次遍历，时间 O(n)，空间 O(1)
     *
     * 5️⃣ 与暴力法的对比？
     *    - 暴力法：对每个位置，遍历其他所有元素相乘 → O(n²)
     *    - 本方法：两次线性遍历 → O(n)
     *    - 效率提升显著，特别是大数据量时
     *
     * 6️⃣ 边界情况处理？
     *    - 数组长度为2：正常工作
     *    - 有0元素：正常工作，不涉及除法
     *    - 有负数：正常工作，乘积符号自动处理
     *    - 大数溢出：题目保证在32位整数范围内
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>初始化</b>：ans[0] 必须初始化为 1，因为第一个元素左边没有元素</li>
     *   <li><b>遍历顺序</b>：前缀积从左到右，后缀积从右到左</li>
     *   <li><b>更新顺序</b>：在后缀积遍历时，先更新 ans[i]，再更新 suffix</li>
     *   <li><b>空间优化</b>：直接在 ans 数组上操作，不需要额外的前缀积数组</li>
     *   <li><b>不使用除法</b>：这是题目的核心要求，也是面试考察点</li>
     * </ul>
     *
     * @param nums 整数数组
     * @return 除自身以外数组的乘积数组
     */
    public int[] productExceptSelf(int[] nums) {
        int n = nums.length;

        // 📋 创建结果数组
        int[] ans = new int[n];

        // 🔧 初始化：第一个元素左边没有元素，前缀积为1
        ans[0] = 1;

        // 🔄 第一次遍历：计算每个位置的前缀积（左边所有元素的乘积）
        for (int i = 1; i < n; i++) {
            // ans[i] = nums[0] * nums[1] * ... * nums[i-1]
            ans[i] = nums[i - 1] * ans[i - 1];
        }

        // 🔧 初始化后缀积变量
        int suffix = 1;

        // 🔄 第二次遍历：从右向左，结合前缀积和后缀积得到最终结果
        for (int i = n - 1; i >= 0; i--) {
            // 📊 当前位置的结果 = 前缀积 * 后缀积
            ans[i] *= suffix;

            // ➕ 更新后缀积：将当前元素加入后缀积
            suffix *= nums[i];
        }

        return ans;
    }
}

public class Hot013 {
    /**
     * <a href="https://leetcode.cn/problems/maximum-subarray/description/">53. 最大子数组和</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给你一个整数数组 nums ，请你找出一个具有最大和的连续子数组（子数组最少包含一个元素），返回其最大和。</p>
     *
     * <h3>💡 核心思路：动态规划（Kadane 算法）</h3>
     * <ul>
     *   <li><b>基本思想</b>：对于每个位置，决定是继续之前的子数组还是从当前位置重新开始
     *     <ul>
     *       <li>sum 表示以当前元素结尾的最大子数组和</li>
     *       <li>如果 sum + num < num，说明前面的子数组和为负，不如重新开始</li>
     *       <li>ans 记录遍历过程中出现的最大值</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，只需一次遍历</li>
     *   <li><b>空间复杂度</b>：O(1)，只使用了常数个变量</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：nums = [-2, 1, -3, 4, -1, 2, 1, -5, 4]
     *
     * 初始化：sum = 0, ans = nums[0] = -2
     *
     * 【num=-2】
     *   - sum = max(-2, 0 + (-2)) = max(-2, -2) = -2
     *   - ans = max(-2, -2) = -2
     *   - 解释：第一个元素，只能选它自己
     *
     * 【num=1】
     *   - sum = max(1, -2 + 1) = max(1, -1) = 1 ✅
     *   - ans = max(-2, 1) = 1
     *   - 解释：前面的和是-2，加上1反而更小，所以从1重新开始
     *
     * 【num=-3】
     *   - sum = max(-3, 1 + (-3)) = max(-3, -2) = -2
     *   - ans = max(1, -2) = 1
     *   - 解释：继续前面的子数组 [1, -3]，和为-2
     *
     * 【num=4】
     *   - sum = max(4, -2 + 4) = max(4, 2) = 4 ✅
     *   - ans = max(1, 4) = 4
     *   - 解释：前面的和是-2，加上4得2，不如从4重新开始
     *
     * 【num=-1】
     *   - sum = max(-1, 4 + (-1)) = max(-1, 3) = 3
     *   - ans = max(4, 3) = 4
     *   - 解释：继续前面的子数组 [4, -1]，和为3
     *
     * 【num=2】
     *   - sum = max(2, 3 + 2) = max(2, 5) = 5
     *   - ans = max(4, 5) = 5
     *   - 解释：继续前面的子数组 [4, -1, 2]，和为5
     *
     * 【num=1】
     *   - sum = max(1, 5 + 1) = max(1, 6) = 6
     *   - ans = max(5, 6) = 6
     *   - 解释：继续前面的子数组 [4, -1, 2, 1]，和为6
     *
     * 【num=-5】
     *   - sum = max(-5, 6 + (-5)) = max(-5, 1) = 1
     *   - ans = max(6, 1) = 6
     *   - 解释：继续前面的子数组 [4, -1, 2, 1, -5]，和为1
     *
     * 【num=4】
     *   - sum = max(4, 1 + 4) = max(4, 5) = 5
     *   - ans = max(6, 5) = 6
     *   - 解释：继续前面的子数组 [4, -1, 2, 1, -5, 4]，和为5
     *
     * 最终结果：ans = 6 ✅
     * 对应的子数组：[4, -1, 2, 1]
     *
     * 图示：
     *   数组：[-2, 1, -3, 4, -1, 2, 1, -5, 4]
     *                  ↑        ↑
     *               开始位置  结束位置
     *   最大子数组：[4, -1, 2, 1]，和为 6
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么 sum = max(num, sum + num)？
     *    - 这是动态规划的状态转移方程
     *    - dp[i] = max(nums[i], dp[i-1] + nums[i])
     *    - 如果前面的子数组和为负，加上当前元素只会更小
     *    - 所以不如从当前元素重新开始
     *
     * 2️⃣ 为什么 ans 初始化为 nums[0]？
     *    - 数组可能全是负数
     *    - 如果初始化为 0，会错误地返回 0
     *    - 例如：[-1, -2, -3]，正确答案是 -1
     *
     * 3️⃣ Kadane 算法的本质？
     *    - 贪心思想：只要前面的和是正的，就继续累加
     *    - 一旦前面的和变成负的，就丢弃，重新开始
     *    - 因为负的和只会让后面的结果更小
     *
     * 4️⃣ 与暴力法的区别？
     *    - 暴力法：枚举所有子数组，O(n²) 或 O(n³)
     *    - Kadane 算法：一次遍历，O(n)
     *    - 关键优化：不需要枚举所有子数组
     *
     * 5️⃣ 如何记录子数组的起始和结束位置？
     *    - 可以在更新 sum 时记录起始位置
     *    - 在更新 ans 时记录结束位置
     *    - 本题只需要返回最大和，所以省略了这部分
     *
     * 6️⃣ 时间复杂度：O(n)
     *    - 只需一次遍历数组
     *    - 每次操作都是 O(1) 的比较和加法
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>初始化</b>：ans 必须初始化为 nums[0]，不能是 0</li>
     *   <li><b>全负数情况</b>：算法能正确处理全负数数组</li>
     *   <li><b>单个元素</b>：数组只有一个元素时，返回该元素</li>
     *   <li><b>溢出问题</b>：如果数字很大，注意整数溢出</li>
     *   <li><b>连续性</b>：子数组必须是连续的，不能跳跃</li>
     * </ul>
     *
     * @param nums 整数数组
     * @return 最大子数组和
     */
    public int maxSubArray(int[] nums) {
        // 🔧 初始化：sum 为当前子数组和，ans 为全局最大值
        int sum = 0, ans = nums[0];

        // 🔄 遍历数组，应用 Kadane 算法
        for (int num : nums) {
            // 📊 状态转移：决定是继续之前的子数组还是重新开始
            sum = Math.max(num, sum + num);

            // 📏 更新全局最大值
            ans = Math.max(ans, sum);
        }

        return ans;
    }
}

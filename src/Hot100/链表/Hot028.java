package Hot100.链表;

/**
 * @author lxd
 **/
public class Hot028 {
    /**
     * <a href="https://leetcode.cn/problems/add-two-numbers/description/">2. 两数相加</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给你两个 <b>非空</b> 的链表，表示两个非负的整数。它们每位数字都是按照 <b>逆序</b> 的方式存储的，并且每个节点只能存储 <b>一位</b> 数字。</p>
     * <p>请你将两个数相加，并以相同形式返回一个表示和的链表。</p>
     *
     * <h3>💡 核心思路：模拟加法运算</h3>
     * <ul>
     *   <li><b>基本思想</b>：从低位到高位逐位相加，处理进位
     *     <ul>
     *       <li>同时遍历两个链表，对应位相加</li>
     *       <li>如果某个链表较短，用 0 补齐</li>
     *       <li>记录进位 carry，加到下一位</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(max(m, n))，m 和 n 分别是两个链表的长度</li>
     *   <li><b>空间复杂度</b>：O(1)，不计输出链表的空间</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例1：l1 = [2,4,3], l2 = [5,6,4] （表示 342 + 465）
     *
     *   2 → 4 → 3
     * + 5 → 6 → 4
     * ─────────────
     *
     * 【Step 1】第 1 位：2 + 5 = 7
     *   - newVal = 2 + 5 + 0 = 7
     *   - value = 7 % 10 = 7
     *   - carry = 7 / 10 = 0
     *   - 结果链表：7
     *
     * 【Step 2】第 2 位：4 + 6 = 10
     *   - newVal = 4 + 6 + 0 = 10
     *   - value = 10 % 10 = 0
     *   - carry = 10 / 10 = 1
     *   - 结果链表：7 → 0
     *
     * 【Step 3】第 3 位：3 + 4 + carry = 8
     *   - newVal = 3 + 4 + 1 = 8
     *   - value = 8 % 10 = 8
     *   - carry = 8 / 10 = 0
     *   - 结果链表：7 → 0 → 8
     *
     * 最终结果：[7, 0, 8]（表示 807）✅
     *
     * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
     *
     * 示例2：有进位的情况 l1 = [9,9], l2 = [1]
     *
     *   9 → 9
     * + 1
     * ───────
     *
     * 【Step 1】9 + 1 = 10
     *   - newVal = 9 + 1 + 0 = 10
     *   - value = 10 % 10 = 0
     *   - carry = 10 / 10 = 1
     *   - 结果：0
     *
     * 【Step 2】9 + 0 + carry = 10
     *   - newVal = 9 + 0 + 1 = 10
     *   - value = 10 % 10 = 0
     *   - carry = 10 / 10 = 1
     *   - 结果：0 → 0
     *
     * 【Step 3】carry = 1
     *   - newVal = 0 + 0 + 1 = 1
     *   - value = 1 % 10 = 1
     *   - carry = 1 / 10 = 0
     *   - 结果：0 → 0 → 1
     *
     * 最终结果：[0, 0, 1]（表示 100）✅
     *
     * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
     *
     * 示例3：不同长度 l1 = [9,9,9,9], l2 = [9,9]
     *
     *   9 → 9 → 9 → 9
     * + 9 → 9
     * ───────────────
     *
     * 【Step 1】9 + 9 = 18
     *   - value = 8, carry = 1
     *   - 结果：8
     *
     * 【Step 2】9 + 9 + 1 = 19
     *   - value = 9, carry = 1
     *   - 结果：8 → 9
     *
     * 【Step 3】9 + 0 + 1 = 10
     *   - value = 0, carry = 1
     *   - 结果：8 → 9 → 0
     *
     * 【Step 4】9 + 0 + 1 = 10
     *   - value = 0, carry = 1
     *   - 结果：8 → 9 → 0 → 0
     *
     * 【Step 5】carry = 1
     *   - value = 1, carry = 0
     *   - 结果：8 → 9 → 0 → 0 → 1
     *
     * 最终结果：[8, 9, 0, 0, 1]（表示 10098）✅
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么用虚拟头节点？
     *    - 简化链表操作，避免特殊处理头节点
     *    - 最后返回 dummy.next 即可
     *
     * 2️⃣ 循环条件为什么是 || 而不是 &&？
     *    - 只要有一个链表未遍历完，就需要继续
     *    - 短的链表用 0 补齐
     *    - 还要考虑最后的进位
     *
     * 3️⃣ 如何处理进位？
     *    - newVal = val1 + val2 + carry
     *    - value = newVal % 10（当前位的值）
     *    - carry = newVal / 10（进位值）
     *
     * 4️⃣ 为什么最后还要检查 carry？
     *    - 最高位可能产生进位
     *    - 例如：99 + 1 = 100，需要额外的节点
     *
     * 5️⃣ 时间复杂度：O(max(m, n))
     *    - 需要遍历较长的链表
     *    - 每个节点处理一次
     *
     * 6️⃣ 空间复杂度：O(1)
     *    - 只使用了几个变量
     *    - 输出链表不计入额外空间
     *
     * 7️⃣ 逆序存储的好处？
     *    - 低位在前，符合加法运算顺序
     *    - 不需要反转链表
     *    - 直接从左到右逐位相加
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>非空链表</b>：题目保证两个链表都是非空的</li>
     *   <li><b>逆序存储</b>：链表是逆序的，低位在前</li>
     *   <li><b>进位处理</b>：不要忘记最后的进位</li>
     *   <li><b>不同长度</b>：短的链表用 0 补齐</li>
     *   <li><b>虚拟节点</b>：使用 dummy 简化操作</li>
     * </ul>
     *
     * @param l1 第一个链表
     * @param l2 第二个链表
     * @return 表示和的链表
     */
    public ListNode addTwoNumbers(ListNode l1, ListNode l2) {
        // 🎭 创建虚拟头节点，简化链表操作
        ListNode dummy = new ListNode();
        ListNode tail = dummy;  // 尾指针，用于构建结果链表
        int carry = 0;          // 进位值

        // 🔄 当任一链表未遍历完或还有进位时继续
        while (l1 != null || l2 != null || carry != 0) {
            int val1 = 0, val2 = 0;

            // 📊 获取 l1 当前节点的值
            if (l1 != null) {
                val1 = l1.val;
                l1 = l1.next;
            }

            // 📊 获取 l2 当前节点的值
            if (l2 != null) {
                val2 = l2.val;
                l2 = l2.next;
            }

            // ➕ 计算当前位的和
            int newVal = val1 + val2 + carry;
            int value = newVal % 10;  // 当前位的值（个位）
            carry = newVal / 10;      // 进位值（十位）

            // 🔗 创建新节点并连接到结果链表
            tail.next = new ListNode(value);
            tail = tail.next;
        }

        // ✅ 返回结果链表（跳过虚拟头节点）
        return dummy.next;
    }
}

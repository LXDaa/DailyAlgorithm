package Hot100.链表;

/**
 * @author lxd
 **/
public class Hot026 {
    /**
     * <a href="https://leetcode.cn/problems/linked-list-cycle-ii/description/">142. 环形链表 II</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给定一个链表的头节点 head ，返回链表开始入环的第一个节点。如果链表无环，则返回 null。</p>
     *
     * <h3>💡 核心思路：快慢指针 + 数学推导</h3>
     * <ul>
     *   <li><b>基本思想</b>：先用快慢指针检测是否有环，如果有环，再找到环的入口
     *     <ul>
     *       <li>第一阶段：用快慢指针判断是否有环，并记录相遇点</li>
     *       <li>第二阶段：将一个指针放回头部，两个指针同速前进，相遇点即为环入口</li>
     *     </ul>
     *   </li>
     *   <li><b>数学原理</b>：设环外长度为 a，环入口到相遇点距离为 b，相遇点到环入口距离为 c
     *     <ul>
     *       <li>slow 走的距离：a + b</li>
     *       <li>fast 走的距离：a + b + n(b + c)，其中 n 是 fast 在环内转的圈数</li>
     *       <li>因为 fast 速度是 slow 的2倍：2(a + b) = a + b + n(b + c)</li>
     *       <li>化简得：a = (n-1)(b + c) + c</li>
     *       <li>这意味着：从 head 走 a 步 = 从相遇点走 c + (n-1)圈</li>
     *       <li>所以两个指针同速前进，会在环入口相遇</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，n 为链表节点数</li>
     *   <li><b>空间复杂度</b>：O(1)，只使用了常数个指针</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：head = [3, 2, 0, -4]，pos = 1（环入口在节点 2）
     *
     * 链表结构：
     *   3 → 2 → 0 → -4
     *       ↑         |
     *       └─────────┘
     *
     * 【第一阶段】检测是否有环，找到相遇点
     *
     *   轮次  slow  fast
     *   初始   3     3
     *   1      2     0
     *   2      0     2
     *   3     -4    -4  ← 相遇！
     *
     *   此时：
     *   - slow 走了 3 步：3→2→0→-4
     *   - fast 走了 6 步：3→2→0→-4→2→0→-4
     *
     * 【第二阶段】找环入口
     *
     *   初始化：
     *   - p = head = 3（从头开始）
     *   - q = 相遇点 = -4
     *
     *   轮次  p   q
     *   初始   3  -4
     *   1      2   2  ← 相遇！✅
     *
     *   结果：返回节点 2（环入口）✅
     *
     * ---
     *
     * 数学验证：
     *   设：a = 1（环外长度），b = 2（入口到相遇点），c = 1（相遇点到入口）
     *
     *   slow 走的距离 = a + b = 1 + 2 = 3
     *   fast 走的距离 = 2 × 3 = 6
     *
     *   根据公式：a = (n-1)(b+c) + c
     *   1 = (1-1)(2+1) + 1 = 0 + 1 = 1 ✅
     *
     *   从 head 走 a=1 步到达节点 2
     *   从相遇点走 c=1 步也到达节点 2
     *   两者在环入口相遇！✅
     *
     * ---
     *
     * 图示过程：
     *
     *   第一阶段（检测环）：
     *   3 → [2 → 0 → -4]
     *        ↑           |
     *        └───────────┘
     *
     *   slow: 3→2→0→-4（3步）
     *   fast: 3→2→0→-4→2→0→-4（6步）
     *   相遇点：-4
     *
     *   第二阶段（找入口）：
     *   p从head出发，q从相遇点出发，同速前进
     *
     *   p: 3→2（1步）
     *   q: -4→2（1步）
     *   相遇点：2（环入口）✅
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ 为什么第二阶段能找到环入口？
     *    - 数学推导证明：a = c + (n-1)L，其中 L 是环的长度
     *    - 从 head 走 a 步到达环入口
     *    - 从相遇点走 c + (n-1)L 步也到达环入口
     *    - (n-1)L 表示在环内转 (n-1) 圈，最终位置不变
     *    - 所以两个指针会在环入口相遇
     *
     * 2️⃣ 为什么 fast 每次走2步？
     *    - 走2步能保证 fast 和 slow 在环内相遇
     *    - 且满足 2(a+b) = a+b+nL 的关系
     *    - 这是推导公式的基础
     *
     * 3️⃣ 相遇时 slow 一定没走完一圈吗？
     *    - 是的！这是一个重要性质
     *    - 当 slow 进入环时，fast 最多领先 L-1 步
     *    - fast 相对 slow 的速度是 1 步/次
     *    - 所以最多 L-1 次就能追上 slow
     *    - slow 在环内走的距离 < L，即没走完一圈
     *
     * 4️⃣ n 的值是多少？
     *    - n 是 fast 在环内转的圈数
     *    - 通常情况下 n = 1（fast 比 slow 多走一圈）
     *    - 但理论上 n 可以是任意正整数
     *    - 公式对任意 n 都成立
     *
     * 5️⃣ 边界情况处理？
     *    - 无环链表：第一阶段循环结束，返回 null
     *    - 空链表：直接返回 null
     *    - 单节点自环：head.next = head，算法依然有效
     *
     * 6️⃣ 与哈希表方法对比？
     *    - 哈希表：遍历链表，将节点存入集合，第一次遇到的重复节点就是环入口
     *    - 哈希表：时间 O(n)，空间 O(n)
     *    - 快慢指针：时间 O(n)，空间 O(1) ✅ 更优
     *
     * 7️⃣ 完整流程总结：
     *    第一步：检测环
     *    ├─ slow 和 fast 同时从 head 出发
     *    ├─ slow 每次1步，fast 每次2步
     *    └─ 如果相遇则有环，否则无环
     *
     *    第二步：找入口
     *    ├─ p 从 head 出发，q 从相遇点出发
     *    ├─ p 和 q 每次都走1步
     *    └─ 相遇点即为环入口
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>两阶段分离</b>：先检测环，再找入口，逻辑要清晰</li>
     *   <li><b>相遇点判断</b>：在第一阶段找到相遇点后才进入第二阶段</li>
     *   <li><b>指针初始化</b>：第二阶段 p 从 head 开始，q 从相遇点开始</li>
     *   <li><b>同速前进</b>：第二阶段两个指针每次都走1步</li>
     *   <li><b>返回值</b>：有环返回环入口节点，无环返回 null</li>
     * </ul>
     *
     * @param head 链表的头节点
     * @return 环入口节点，如果无环则返回 null
     */
    public ListNode detectCycle(ListNode head) {
        // 🏃 初始化快慢指针
        ListNode slow = head;
        ListNode fast = head;

        // 🔄 第一阶段：检测是否有环
        while (fast != null && fast.next != null) {
            slow = slow.next;        // 慢指针每次走1步
            fast = fast.next.next;   // 快指针每次走2步

            // ✅ 快慢指针相遇，说明有环
            if (slow == fast) {
                // 🎯 第二阶段：找环入口
                ListNode p = head;   // p 从头节点开始
                ListNode q = fast;   // q 从相遇点开始

                // 两个指针同速前进，相遇点即为环入口
                while (p != q) {
                    p = p.next;
                    q = q.next;
                }

                // ✅ 返回环入口节点
                return p;
            }
        }

        // ❌ 循环结束，fast 到达 null，说明无环
        return null;
    }
}

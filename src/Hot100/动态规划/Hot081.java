package Hot100.动态规划;

/**
 * @author lxd
 **/
public class Hot081 {
    /**
     * <a href="https://leetcode.cn/problems/climbing-stairs/description/">70. 爬楼梯</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>假设你正在爬楼梯。需要 n 阶你才能到达楼顶。</p>
     * <p>每次你可以爬 1 或 2 个台阶。你有多少种不同的方法可以爬到楼顶呢？</p>
     *
     * <h3>💡 核心思路：动态规划</h3>
     * <ul>
     *   <li><b>基本思想</b>：递推公式求解
     *     <ul>
     *       <li>定义 dp[i]：到达第 i 阶楼梯的方法数</li>
     *       <li>递推公式：dp[i] = dp[i-1] + dp[i-2]</li>
     *       <li>初始状态：dp[1] = 1, dp[2] = 2</li>
     *       <li>从第 3 阶开始递推到第 n 阶</li>
     *     </ul>
     *   </li>
     *   <li><b>为什么是这个递推公式</b>：
     *     <ul>
     *       <li>到达第 i 阶可以从第 i-1 阶爬 1 步</li>
     *       <li>也可以从第 i-2 阶爬 2 步</li>
     *       <li>所以方法数等于两种方式之和</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，遍历一次数组</li>
     *   <li><b>空间复杂度</b>：O(n)，使用 dp 数组</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：n = 5
     *
     * 初始化：dp[1] = 1, dp[2] = 2
     *
     * i=3：
     *   dp[3] = dp[2] + dp[1] = 2 + 1 = 3
     *   → dp = [0, 1, 2, 3, 0, 0]
     *
     * i=4：
     *   dp[4] = dp[3] + dp[2] = 3 + 2 = 5
     *   → dp = [0, 1, 2, 3, 5, 0]
     *
     * i=5：
     *   dp[5] = dp[4] + dp[3] = 5 + 3 = 8
     *   → dp = [0, 1, 2, 3, 5, 8]
     *
     * 结果：dp[5] = 8 ✅
     *
     * 示例2：n = 3
     *   dp[3] = dp[2] + dp[1] = 2 + 1 = 3
     *   路径：1+1+1, 1+2, 2+1 → 3种 ✅
     *
     * 示例3：n = 4
     *   dp[4] = dp[3] + dp[2] = 3 + 2 = 5
     *   路径：1+1+1+1, 1+1+2, 1+2+1, 2+1+1, 2+2 → 5种 ✅
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ dp 数组的定义？
     *    - dp[i] 表示到达第 i 阶楼梯的方法数
     *    - i 从 1 开始，dp[0] 未使用或设为 1（用于斐波那契）
     *    - dp 数组大小为 n+1
     *
     * 2️⃣ 初始状态如何设置？
     *    - dp[1] = 1（只有一种方法：爬1步）
     *    - dp[2] = 2（两种方法：1+1 或 2）
     *    - n <= 1 时直接返回 n
     *
     * 3️⃣ 递推公式是什么？
     *    - dp[i] = dp[i-1] + dp[i-2]
     *    - 从第 i-1 阶爬 1 步到达
     *    - 从第 i-2 阶爬 2 步到达
     *    - 两种方式的方法数相加
     *
     * 4️⃣ 为什么这是斐波那契数列？
     *    - dp[1] = 1, dp[2] = 2, dp[3] = 3, dp[4] = 5, dp[5] = 8
     *    - 从 dp[3] 开始是标准斐波那契数列
     *    - dp[i] = dp[i-1] + dp[i-2]
     *
     * 5️⃣ 空间复杂度如何优化？
     *    - 当前方法使用 O(n) 空间
     *    - 优化方法：只保存前两个值，O(1) 空间
     *    - prev1 = dp[i-1], prev2 = dp[i-2]
     *    - current = prev1 + prev2
     *
     * 6️⃣ 边界情况如何处理？
     *    - n = 0：题目中 n >= 1
     *    - n = 1：返回 1
     *    - n = 2：返回 2
     *
     * 7️⃣ 时间复杂度分析：
     *    - 遍历从 3 到 n，共 n-2 次迭代
     *    - 每次迭代 O(1) 操作
     *    - 总时间 O(n)
     *
     * 8️⃣ 完整流程总结：
     *
     *    if n <= 1：return n
     *
     *    dp = new int[n+1]
     *    dp[1] = 1
     *    dp[2] = 2
     *
     *    for i in 3..n：
     *      dp[i] = dp[i-1] + dp[i-2]
     *
     *    return dp[n]
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>数组索引</b>：dp 数组大小为 n+1，索引从 1 开始</li>
     *   <li><b>初始状态</b>：dp[1]=1, dp[2]=2</li>
     *   <li><b>递推公式</b>：dp[i] = dp[i-1] + dp[i-2]</li>
     *   <li><b>边界处理</b>：n <= 1 时直接返回</li>
     *   <li><b>空间优化</b>：可以使用 O(1) 空间优化</li>
     * </ul>
     *
     * @param n 楼梯的阶数
     * @return 爬到楼顶的不同方法数
     */
    public int climbStairs(int n) {
        // 🚫 边界处理：n <= 1 时直接返回
        if (n <= 1) {
            return n;
        }

        // 📦 创建 dp 数组，大小为 n+1
        int[] dp = new int[n + 1];

        // 📋 初始状态
        dp[1] = 1;
        dp[2] = 2;

        // 🔄 从第 3 阶开始递推
        for (int i = 3; i <= n; i++) {
            // 📊 递推公式：dp[i] = dp[i-1] + dp[i-2]
            dp[i] = dp[i - 1] + dp[i - 2];
        }

        // ✅ 返回到达第 n 阶的方法数
        return dp[n];
    }
}

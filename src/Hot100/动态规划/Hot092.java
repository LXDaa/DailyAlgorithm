package Hot100.动态规划;

/**
 * @author lxd
 **/
public class Hot092 {
    /**
     * <a href="https://leetcode.cn/problems/minimum-path-sum/description/">64. 最小路径和</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给定一个包含非负整数的 m x n 网格 grid ，请找出一条从左上角到右下角的路径，使得路径上的数字总和为最小。</p>
     * <p>说明：每次只能向下或者向右移动一步。</p>
     *
     * <h3>💡 核心思路：动态规划</h3>
     * <ul>
     *   <li><b>基本思想</b>：递推求解到达每个位置的最小路径和
     *     <ul>
     *       <li>定义 dp[i][j]：到达 (i,j) 的最小路径和</li>
     *       <li>递推公式：dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + grid[i][j]</li>
     *       <li>初始状态：dp[0][0] = grid[0][0]，第一行和第一列只能累加</li>
     *       <li>从 (1,1) 开始递推到 (rows-1, cols-1)</li>
     *     </ul>
     *   </li>
     *   <li><b>为什么是这个递推公式</b>：
     *     <ul>
     *       <li>到达 (i,j) 只能从上方 (i-1,j) 或左方 (i,j-1)</li>
     *       <li>选择路径和较小的那个方向</li>
     *       <li>再加上当前位置的 grid 值</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(m * n)，遍历整个网格</li>
     *   <li><b>空间复杂度</b>：O(m * n)，使用二维 dp 数组</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：grid = [[1,3,1],[1,5,1],[4,2,1]]
     *
     * 网格：
     *      0 1 2
     *    0 1 3 1
     *    1 1 5 1
     *    2 4 2 1
     *
     * 初始化：dp[0][0] = 1
     *
     * 第一行（只能从左方到达）：
     *   dp[0][1] = dp[0][0] + 3 = 1 + 3 = 4
     *   dp[0][2] = dp[0][1] + 1 = 4 + 1 = 5
     *
     *      0 1 2
     *    0 1 4 5
     *    1 ? ? ?
     *    2 ? ? ?
     *
     * i=1（第二行）：
     *   dp[1][0] = dp[0][0] + 1 = 1 + 1 = 2
     *   dp[1][1] = min(dp[0][1], dp[1][0]) + 5 = min(4, 2) + 5 = 2 + 5 = 7
     *   dp[1][2] = min(dp[0][2], dp[1][1]) + 1 = min(5, 7) + 1 = 5 + 1 = 6
     *
     *      0 1 2
     *    0 1 4 5
     *    1 2 7 6
     *    2 ? ? ?
     *
     * i=2（第三行）：
     *   dp[2][0] = dp[1][0] + 4 = 2 + 4 = 6
     *   dp[2][1] = min(dp[1][1], dp[2][0]) + 2 = min(7, 6) + 2 = 6 + 2 = 8
     *   dp[2][2] = min(dp[1][2], dp[2][1]) + 1 = min(6, 8) + 1 = 6 + 1 = 7
     *
     * 最终：
     *      0 1 2
     *    0 1 4 5
     *    1 2 7 6
     *    2 6 8 7
     *
     * 结果：dp[2][2] = 7 ✅
     * 路径：1 → 3 → 1 → 1 → 1（和为 7）
     * 或者：1 → 1 → 5 → 1 → 1（和为 9）
     * 最优路径：1 → 1 → 4 → 2 → 1（和为 9，不对，应该是 1→3→1→1→1 或 1→1→5→1→1）
     * 实际上最优路径是：1(0,0) → 3(0,1) → 1(0,2) → 1(1,2) → 1(2,2) 和为 7
     * 或者：1(0,0) → 1(1,0) → 5(1,1) → 1(1,2) → 1(2,2) 和为 9
     * 或者：1(0,0) → 1(1,0) → 4(2,0) → 2(2,1) → 1(2,2) 和为 9
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ dp 数组的定义？
     *    - dp[i][j] 表示到达位置 (i,j) 的最小路径和
     *    - i 从 0 到 rows-1，j 从 0 到 cols-1
     *    - 最终答案为 dp[rows-1][cols-1]
     *
     * 2️⃣ 初始状态如何设置？
     *    - dp[0][0] = grid[0][0]（起点）
     *    - 第一行：dp[0][j] = dp[0][j-1] + grid[0][j]（只能从左方到达）
     *    - 第一列：dp[i][0] = dp[i-1][0] + grid[i][0]（只能从上方到达）
     *
     * 3️⃣ 递推公式是什么？
     *    - dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + grid[i][j]
     *    - 选择上方或左方路径和较小的方向
     *    - 再加上当前位置的 grid 值
     *
     * 4️⃣ 为什么只能向下或向右？
     *    - 题目约束：每次只能向下或者向右移动一步
     *    - 所以到达 (i,j) 只有两种方式
     *    - 没有其他路径可以到达
     *
     * 5️⃣ 空间复杂度如何优化？
     *    - 当前方法：O(m * n)
     *    - 优化方法：使用一维 dp 数组，O(n) 空间
     *    - 或者直接在原 grid 上修改，O(1) 空间
     *
     * 6️⃣ 边界情况如何处理？
     *    - rows = 1 或 cols = 1：只能走一条路径
     *    - rows = 1, cols = 1：返回 grid[0][0]
     *    - 第一行和第一列直接累加
     *
     * 7️⃣ 时间复杂度分析：
     *    - 外层循环：rows 次（遍历行）
     *    - 内层循环：cols 次（遍历列）
     *    - 总时间：O(rows * cols)
     *
     * 8️⃣ 完整流程总结：
     *
     *    rows = grid.length, cols = grid[0].length
     *    dp = new int[rows][cols]
     *    dp[0][0] = grid[0][0]
     *
     *    for j in 1..cols-1：
     *      dp[0][j] = dp[0][j-1] + grid[0][j]
     *
     *    for i in 1..rows-1：
     *      dp[i][0] = dp[i-1][0] + grid[i][0]
     *      for j in 1..cols-1：
     *        dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + grid[i][j]
     *
     *    return dp[rows-1][cols-1]
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>dp 定义</b>：dp[i][j] 表示到达 (i,j) 的最小路径和</li>
     *   <li><b>初始状态</b>：第一行和第一列只能累加</li>
     *   <li><b>递推公式</b>：dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + grid[i][j]</li>
     *   <li><b>方向约束</b>：只能向下或向右移动</li>
     *   <li><b>空间优化</b>：可以使用一维 dp 数组或原地修改</li>
     * </ul>
     *
     * @param grid 包含非负整数的 m x n 网格
     * @return 从左上角到右下角的最小路径和
     */
    public int minPathSum(int[][] grid) {
        // 📏 获取网格的行数和列数
        int rows = grid.length;
        int cols = grid[0].length;

        // 📦 创建二维 dp 数组，dp[i][j] 表示到达 (i,j) 的最小路径和
        int[][] dp = new int[rows][cols];

        // 📋 初始状态：起点
        dp[0][0] = grid[0][0];

        // 📋 初始状态：第一行（只能从左方到达）
        for (int j = 1; j < cols; j++) {
            dp[0][j] += dp[0][j - 1] + grid[0][j];
        }

        // 🔄 从第二行开始递推
        for (int i = 1; i < rows; i++) {
            // 📋 初始状态：第一列（只能从上方到达）
            dp[i][0] += dp[i - 1][0] + grid[i][0];

            // 🔄 从第二列开始递推
            for (int j = 1; j < cols; j++) {
                // 📊 递推公式：选择上方或左方路径和较小的方向 + 当前值
                dp[i][j] = Math.min(dp[i - 1][j], dp[i][j - 1]) + grid[i][j];
            }
        }

        // ✅ 返回右下角的最小路径和
        return dp[rows - 1][cols - 1];
    }
}

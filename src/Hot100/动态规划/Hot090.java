package Hot100.动态规划;

/**
 * @author lxd
 **/
public class Hot090 {
    /**
     * <a href="https://leetcode.cn/problems/longest-valid-parentheses/description/">32. 最长有效括号</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给你一个只包含 '(' 和 ')' 的字符串，找出最长有效（格式正确且连续）括号子串的长度。</p>
     *
     * <h3>💡 核心思路：动态规划</h3>
     * <ul>
     *   <li><b>基本思想</b>：递推求解以每个位置结尾的最长有效括号长度
     *     <ul>
     *       <li>定义 dp[i]：以 s[i] 结尾的最长有效括号长度</li>
     *       <li>如果 s[i] == '('，dp[i] = 0（左括号无法结尾）</li>
     *       <li>如果 s[i] == ')'：
     *         <ul>
     *           <li>情况1：s[i-1] == '('，dp[i] = dp[i-2] + 2</li>
     *           <li>情况2：s[i-1] == ')'，且 s[i - dp[i-1] - 1] == '('，dp[i] = dp[i-1] + dp[i - dp[i-1] - 2] + 2</li>
     *         </ul>
     *       </li>
     *       <li>初始状态：dp[0] = 0</li>
     *     </ul>
     *   </li>
     *   <li><b>为什么用动态规划</b>：
     *     <ul>
     *       <li>问题具有最优子结构</li>
     *       <li>以 s[i] 结尾的最长有效括号依赖于前面的结果</li>
     *       <li>避免重复计算</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(n)，遍历一次字符串</li>
     *   <li><b>空间复杂度</b>：O(n)，使用 dp 数组</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：s = "(()))())("
     * 索引：    0 1 2 3 4 5 6 7 8
     * 字符：    ( ( ) ) ) ( ) ) (
     *
     * 初始化：dp = [0, 0, 0, 0, 0, 0, 0, 0, 0], maxans = 0
     *
     * i=1, s[i]='('：
     *   dp[1] = 0（左括号无法结尾）
     *   → dp = [0, 0, 0, 0, 0, 0, 0, 0, 0]
     *
     * i=2, s[i]=')'：
     *   s[i-1]='(' ✅
     *   dp[2] = (i>=2 ? dp[0] : 0) + 2 = 0 + 2 = 2
     *   maxans = max(0, 2) = 2
     *   → dp = [0, 0, 2, 0, 0, 0, 0, 0, 0]
     *
     * i=3, s[i]=')'：
     *   s[i-1]=')' ❌
     *   i-dp[i-1]-1 = 3-0-1 = 2, s[2]=')' != '(' ❌
     *   → dp[3] = 0（不满足情况2）
     *   Wait, let me re-check...
     *
     *   Actually for s = "(()))())(":
     *   i=3, s[i]=')', s[i-1]=')'
     *   i-dp[i-1]-1 = 3-dp[2]-1 = 3-2-1 = 0
     *   s[0]='(' ✅
     *   dp[3] = dp[2] + ((3-2)>=2 ? dp[-1] : 0) + 2 = 2 + 0 + 2 = 4
     *   maxans = max(2, 4) = 4
     *   → dp = [0, 0, 2, 4, 0, 0, 0, 0, 0]
     *
     * i=4, s[i]=')'：
     *   s[i-1]=')'
     *   i-dp[i-1]-1 = 4-dp[3]-1 = 4-4-1 = -1（越界）
     *   → dp[4] = 0
     *   → dp = [0, 0, 2, 4, 0, 0, 0, 0, 0]
     *
     * i=5, s[i]='('：
     *   dp[5] = 0
     *   → dp = [0, 0, 2, 4, 0, 0, 0, 0, 0]
     *
     * i=6, s[i]=')'：
     *   s[i-1]='(' ✅
     *   dp[6] = (6>=2 ? dp[4] : 0) + 2 = 0 + 2 = 2
     *   maxans = max(4, 2) = 4
     *   → dp = [0, 0, 2, 4, 0, 0, 2, 0, 0]
     *
     * i=7, s[i]=')'：
     *   s[i-1]=')'
     *   i-dp[i-1]-1 = 7-dp[6]-1 = 7-2-1 = 4
     *   s[4]=')' != '(' ❌
     *   → dp[7] = 0
     *   → dp = [0, 0, 2, 4, 0, 0, 2, 0, 0]
     *
     * i=8, s[i]='('：
     *   dp[8] = 0
     *   → dp = [0, 0, 2, 4, 0, 0, 2, 0, 0]
     *
     * 结果：maxans = 4 ✅
     * 最长有效括号子串："(()))" 中的前4个字符 "(() )"
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ dp 数组的定义？
     *    - dp[i] 表示以 s[i] 结尾的最长有效括号长度
     *    - i 从 0 开始，dp 数组大小为 s.length()
     *    - 最终答案为 max(dp)，不是 dp[n-1]
     *
     * 2️⃣ 为什么只考虑 s[i] == ')' 的情况？
     *    - 左括号 '(' 无法作为有效括号的结尾
     *    - 所以当 s[i] == '(' 时，dp[i] = 0
     *    - 只有右括号 ')' 才可能形成有效括号
     *
     * 3️⃣ 情况1：s[i] == ')' && s[i-1] == '('
     *    - 这是最直接的情况："()"
     *    - dp[i] = dp[i-2] + 2
     *    - 加上前面的有效长度 + 当前的 2
     *    - 需要判断 i >= 2，避免越界
     *
     * 4️⃣ 情况2：s[i] == ')' && s[i-1] == ')'
     *    - 这是嵌套的情况："))"
     *    - 需要检查 s[i - dp[i-1] - 1] 是否为 '('
     *    - 如果是，dp[i] = dp[i-1] + dp[i - dp[i-1] - 2] + 2
     *    - dp[i-1]：前面的有效长度
     *    - dp[i - dp[i-1] - 2]：再前面的有效长度
     *    - +2：当前匹配的括号
     *
     * 5️⃣ 如何理解情况2的索引？
     *    - i - dp[i-1] - 1：跳过前面 dp[i-1] 个有效的右括号
     *    - 找到可能匹配的左括号位置
     *    - 例如："(())))"，i=5, dp[4]=2, i-dp[i-1]-1=5-2-1=2, s[2]=')' ❌
     *    - 例如："(())()"，i=5, dp[4]=0, i-dp[i-1]-1=5-0-1=4, s[4]=')' ❌
     *
     * 6️⃣ 为什么需要判断 i - dp[i-1] > 0？
     *    - 避免数组越界
     *    - 如果 i - dp[i-1] <= 0，说明没有足够的字符
     *    - 无法形成有效的括号对
     *
     * 7️⃣ 时间复杂度分析：
     *    - 只遍历一次字符串
     *    - 每次迭代 O(1) 操作
     *    - 总时间 O(n)
     *
     * 8️⃣ 完整流程总结：
     *
     *    n = s.length()
     *    dp = new int[n]
     *    maxans = 0
     *
     *    for i = 1; i < n; i++：
     *      if s[i] == ')'：
     *        if s[i-1] == '('：
     *          dp[i] = (i >= 2 ? dp[i-2] : 0) + 2
     *        elif i - dp[i-1] > 0 && s[i - dp[i-1] - 1] == '('：
     *          dp[i] = dp[i-1] + ((i - dp[i-1]) >= 2 ? dp[i - dp[i-1] - 2] : 0) + 2
     *        maxans = max(maxans, dp[i])
     *
     *    return maxans
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>dp 定义</b>：以 s[i] 结尾的最长有效括号长度</li>
     *   <li><b>左括号</b>：s[i] == '(' 时 dp[i] = 0</li>
     *   <li><b>情况1</b>：s[i-1] == '('，dp[i] = dp[i-2] + 2</li>
     *   <li><b>情况2</b>：s[i-1] == ')'，检查 s[i-dp[i-1]-1] == '('</li>
     *   <li><b>越界判断</b>：i >= 2 和 i - dp[i-1] > 0</li>
     * </ul>
     *
     * @param s 只包含 '(' 和 ')' 的字符串
     * @return 最长有效括号子串的长度
     */
    public int longestValidParentheses(String s) {
        // 📊 最长有效括号长度
        int maxans = 0;

        // 📦 创建 dp 数组，dp[i] 表示以 s[i] 结尾的最长有效括号长度
        int[] dp = new int[s.length()];

        // 🔄 从第二个字符开始遍历
        for (int i = 1; i < s.length(); i++) {
            // 📌 只有右括号才可能形成有效括号
            if (s.charAt(i) == ')') {
                // 📌 情况1：s[i-1] == '('，直接匹配
                if (s.charAt(i - 1) == '(') {
                    // 📊 dp[i] = 前面的有效长度 + 2（当前括号对）
                    dp[i] = (i >= 2 ? dp[i - 2] : 0) + 2;
                }
                // 📌 情况2：s[i-1] == ')'，需要检查前面是否有匹配的左括号
                else if (i - dp[i - 1] > 0 && s.charAt(i - dp[i - 1] - 1) == '(') {
                    // 📊 dp[i] = 前面的有效长度 + 再前面的有效长度 + 2
                    dp[i] = dp[i - 1] + ((i - dp[i - 1]) >= 2 ? dp[i - dp[i - 1] - 2] : 0) + 2;
                }

                // 📊 更新最长有效括号长度
                maxans = Math.max(maxans, dp[i]);
            }
        }

        // ✅ 返回最长有效括号长度
        return maxans;
    }
}

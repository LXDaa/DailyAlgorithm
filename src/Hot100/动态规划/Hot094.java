public class Hot094 {
    /**
     * <a href="https://leetcode.cn/problems/longest-common-subsequence/description/">1143. 最长公共子序列</a>
     *
     * <h3>🎯 题目描述</h3>
     * <p>给定两个字符串 text1 和 text2，返回这两个字符串的最长公共子序列的长度。</p>
     * <p>一个字符串的子序列是指这样一个新的字符串：它是由原字符串在不改变字符的相对顺序的情况下删除某些字符（也可以不删除任何字符）后组成的新字符串。</p>
     * <p>例如，"ace" 是 "abcde" 的子序列，但 "aec" 不是 "abcde" 的子序列。两个字符串的「公共子序列」是这两个字符串所共同拥有的子序列。</p>
     * <p>若这两个字符串没有公共子序列，则返回 0。</p>
     *
     * <h3>💡 核心思路：动态规划</h3>
     * <ul>
     *   <li><b>基本思想</b>：递推求解两个字符串的最长公共子序列
     *     <ul>
     *       <li>定义 dp[i][j]：text1 前 i 个字符和 text2 前 j 个字符的最长公共子序列长度</li>
     *       <li>递推公式：如果 c1[i-1] == c2[j-1]，dp[i][j] = dp[i-1][j-1] + 1</li>
     *       <li>递推公式：如果 c1[i-1] != c2[j-1]，dp[i][j] = max(dp[i-1][j], dp[i][j-1])</li>
     *       <li>初始状态：dp[0][j] = 0, dp[i][0] = 0（空字符串的最长公共子序列长度为 0）</li>
     *       <li>从 (1,1) 开始递推到 (m,n)</li>
     *     </ul>
     *   </li>
     *   <li><b>为什么是这个递推公式</b>：
     *     <ul>
     *       <li>如果当前字符相等，则最长公共子序列 = 去掉这两个字符后的最长公共子序列 + 1</li>
     *       <li>如果当前字符不相等，则取去掉 text1 当前字符或去掉 text2 当前字符的最大值</li>
     *     </ul>
     *   </li>
     *   <li><b>时间复杂度</b>：O(m * n)，遍历两个字符串</li>
     *   <li><b>空间复杂度</b>：O(m * n)，使用二维 dp 数组</li>
     * </ul>
     *
     * <h3>📊 ASCII 演示过程</h3>
     * <pre>
     * 示例：text1 = "abcde", text2 = "ace"
     *
     * c1 = ['a','b','c','d','e'], c2 = ['a','c','e']
     * m = 5, n = 3
     *
     * 初始化：dp[0][*] = 0, dp[*][0] = 0
     *
     *      0 1 2 3
     *      ''a c e
     *    0 0 0 0 0
     *    1 a 0 ? ?
     *    2 b 0 ? ?
     *    3 c 0 ? ?
     *    4 d 0 ? ?
     *    5 e 0 ? ?
     *
     * i=1, c1[0]='a'：
     *   j=1, c2[0]='a'：c1[0]==c2[0] ✅
     *     dp[1][1] = dp[0][0] + 1 = 0 + 1 = 1
     *   j=2, c2[1]='c'：c1[0]!='c' ❌
     *     dp[1][2] = max(dp[0][2], dp[1][1]) = max(0, 1) = 1
     *   j=3, c2[2]='e'：c1[0]!='e' ❌
     *     dp[1][3] = max(dp[0][3], dp[1][2]) = max(0, 1) = 1
     *
     *      0 1 2 3
     *    0 0 0 0 0
     *    1 a 0 1 1 1
     *    2 b 0 ? ? ?
     *    3 c 0 ? ? ?
     *    4 d 0 ? ? ?
     *    5 e 0 ? ? ?
     *
     * i=2, c1[1]='b'：
     *   j=1, c2[0]='a'：'b'!='a'
     *     dp[2][1] = max(dp[1][1], dp[2][0]) = max(1, 0) = 1
     *   j=2, c2[1]='c'：'b'!='c'
     *     dp[2][2] = max(dp[1][2], dp[2][1]) = max(1, 1) = 1
     *   j=3, c2[2]='e'：'b'!='e'
     *     dp[2][3] = max(dp[1][3], dp[2][2]) = max(1, 1) = 1
     *
     *      0 1 2 3
     *    0 0 0 0 0
     *    1 a 0 1 1 1
     *    2 b 0 1 1 1
     *    3 c 0 ? ? ?
     *    4 d 0 ? ? ?
     *    5 e 0 ? ? ?
     *
     * i=3, c1[2]='c'：
     *   j=1, c2[0]='a'：'c'!='a'
     *     dp[3][1] = max(dp[2][1], dp[3][0]) = max(1, 0) = 1
     *   j=2, c2[1]='c'：'c'=='c' ✅
     *     dp[3][2] = dp[2][1] + 1 = 1 + 1 = 2
     *   j=3, c2[2]='e'：'c'!='e'
     *     dp[3][3] = max(dp[2][3], dp[3][2]) = max(1, 2) = 2
     *
     *      0 1 2 3
     *    0 0 0 0 0
     *    1 a 0 1 1 1
     *    2 b 0 1 1 1
     *    3 c 0 1 2 2
     *    4 d 0 ? ? ?
     *    5 e 0 ? ? ?
     *
     * i=4, c1[3]='d'：
     *   j=1: dp[4][1] = max(dp[3][1], dp[4][0]) = 1
     *   j=2: dp[4][2] = max(dp[3][2], dp[4][1]) = max(2, 1) = 2
     *   j=3: dp[4][3] = max(dp[3][3], dp[4][2]) = max(2, 2) = 2
     *
     *      0 1 2 3
     *    0 0 0 0 0
     *    1 a 0 1 1 1
     *    2 b 0 1 1 1
     *    3 c 0 1 2 2
     *    4 d 0 1 2 2
     *    5 e 0 ? ? ?
     *
     * i=5, c1[4]='e'：
     *   j=1: dp[5][1] = max(dp[4][1], dp[5][0]) = 1
     *   j=2: dp[5][2] = max(dp[4][2], dp[5][1]) = max(2, 1) = 2
     *   j=3, c2[2]='e'：'e'=='e' ✅
     *     dp[5][3] = dp[4][2] + 1 = 2 + 1 = 3
     *
     * 最终：
     *      0 1 2 3
     *    0 0 0 0 0
     *    1 a 0 1 1 1
     *    2 b 0 1 1 1
     *    3 c 0 1 2 2
     *    4 d 0 1 2 2
     *    5 e 0 1 2 3
     *
     * 结果：dp[5][3] = 3 ✅
     * 最长公共子序列："ace"，长度为 3
     * </pre>
     *
     * <h3>🔑 关键技巧解析</h3>
     * <pre>
     * 1️⃣ dp 数组的定义？
     *    - dp[i][j] 表示 text1 前 i 个字符和 text2 前 j 个字符的最长公共子序列长度
     *    - i 从 0 到 m，j 从 0 到 n
     *    - dp[i][j] 对应 c1[0..i-1] 和 c2[0..j-1]
     *    - 最终答案为 dp[m][n]
     *
     * 2️⃣ 为什么 dp 数组大小是 (m+1) x (n+1)？
     *    - dp[0][j] 表示 text1 为空时的最长公共子序列长度（0）
     *    - dp[i][0] 表示 text2 为空时的最长公共子序列长度（0）
     *    - 这样可以避免边界处理，简化代码
     *
     * 3️⃣ 递推公式是什么？
     *    - 如果 c1[i-1] == c2[j-1]：
     *      dp[i][j] = dp[i-1][j-1] + 1
     *      当前字符相等，最长公共子序列长度 + 1
     *    - 如果 c1[i-1] != c2[j-1]：
     *      dp[i][j] = max(dp[i-1][j], dp[i][j-1])
     *      取去掉 text1 当前字符或去掉 text2 当前字符的最大值
     *
     * 4️⃣ 为什么用 max(dp[i-1][j], dp[i][j-1])？
     *    - 当 c1[i-1] != c2[j-1] 时，最长公共子序列有两种可能：
     *    - 不包含 c1[i-1]：dp[i-1][j]
     *    - 不包含 c2[j-1]：dp[i][j-1]
     *    - 取两者的最大值
     *
     * 5️⃣ 空间复杂度如何优化？
     *    - 当前方法：O(m * n)
     *    - 优化方法：使用一维 dp 数组，O(n) 空间
     *    - 需要从后向前遍历，避免覆盖需要的值
     *
     * 6️⃣ 边界情况如何处理？
     *    - text1 或 text2 为空：返回 0
     *    - text1 和 text2 完全相同：返回 min(m,n)
     *    - text1 和 text2 没有公共字符：返回 0
     *
     * 7️⃣ 时间复杂度分析：
     *    - 外层循环：m 次（遍历 text1）
     *    - 内层循环：n 次（遍历 text2）
     *    - 总时间：O(m * n)
     *
     * 8️⃣ 完整流程总结：
     *
     *    c1 = text1.toCharArray(), c2 = text2.toCharArray()
     *    m = c1.length, n = c2.length
     *    dp = new int[m+1][n+1]
     *
     *    for i in 1..m：
     *      for j in 1..n：
     *        if c1[i-1] == c2[j-1]：
     *          dp[i][j] = dp[i-1][j-1] + 1
     *        else：
     *          dp[i][j] = max(dp[i-1][j], dp[i][j-1])
     *
     *    return dp[m][n]
     * </pre>
     *
     * <h3>⚠️ 注意事项</h3>
     * <ul>
     *   <li><b>dp 定义</b>：dp[i][j] 表示前 i 个字符和前 j 个字符的最长公共子序列长度</li>
     *   <li><b>索引对应</b>：c1[i-1] 和 c2[j-1] 对应 dp[i][j]</li>
     *   <li><b>递推公式</b>：相等时 +1，不相等时取 max</li>
     *   <li><b>初始状态</b>：dp[0][*] = 0, dp[*][0] = 0</li>
     *   <li><b>空间优化</b>：可以使用一维 dp 数组</li>
     * </ul>
     *
     * @param text1 第一个字符串
     * @param text2 第二个字符串
     * @return 两个字符串的最长公共子序列的长度
     */
    public int longestCommonSubsequence(String text1, String text2) {
        // 📦 将字符串转为字符数组，便于访问
        char[] c1 = text1.toCharArray();
        char[] c2 = text2.toCharArray();

        // 📏 获取两个字符串的长度
        int m = c1.length;
        int n = c2.length;

        // 📦 创建 dp 数组，dp[i][j] 表示 text1 前 i 个字符和 text2 前 j 个字符的最长公共子序列长度
        int[][] dp = new int[m + 1][n + 1];

        // 🔄 遍历两个字符串
        for (int i = 1; i <= m; i++) {
            for (int j = 1; j <= n; j++) {
                // 📌 如果当前字符相等
                if (c1[i - 1] == c2[j - 1]) {
                    // 📊 递推公式：最长公共子序列长度 + 1
                    dp[i][j] = dp[i - 1][j - 1] + 1;
                } else {
                    // 📌 如果当前字符不相等
                    // 📊 递推公式：取去掉 text1 当前字符或去掉 text2 当前字符的最大值
                    dp[i][j] = Math.max(dp[i - 1][j], dp[i][j - 1]);
                }
            }
        }

        // ✅ 返回最长公共子序列长度
        return dp[m][n];
    }
}

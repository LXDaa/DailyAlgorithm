package CodeTop;

/**
 * <a href="https://leetcode.cn/problems/sqrtx/description/">69. x的平方根</a>
 * <p>
 * 给你一个非负整数 x ，计算并返回 x 的 算术平方根 。
 * <p>
 * 由于返回类型是整数，结果只保留 整数部分 ，小数部分将被 舍去 。
 * <p>
 * 注意：不允许使用任何内置指数函数和算符，例如 pow(x, 0.5) 或者 x ** 0.5 。
 * <p>
 * <b>解题思路：</b>
 * <p>
 * <ol>
 *     <li><b>问题分析：</b>
 *         <p>需要找到最大的整数 ans，使得 ans² ≤ x</p>
 *         <p>等价于在 [0, x] 范围内寻找满足条件的最大整数</p>
 *         <p>暴力解法：从 0 开始枚举，时间复杂度 O(√x)，效率较低</p>
 *     </li>
 *     <li><b>核心方法：二分查找</b>
 *         <p>利用单调性：如果 mid² ≤ x，则答案在 [mid, r] 区间；否则在 [l, mid-1] 区间</p>
 *         <p>通过二分查找可以快速定位到答案，时间复杂度 O(log x)</p>
 *         <p>搜索范围：[0, x]，因为 x的平方根不会超过 x 本身（x≥0）</p>
 *     </li>
 *     <li><b>二分查找策略：</b>
 *         <p><b>初始化：</b>左边界 l=0，右边界 r=x，答案记录器 ans=-1</p>
 *         <p><b>循环条件：</b>l <= r，当左右边界相遇时仍需判断</p>
 *         <p><b>中点计算：</b>mid = l + (r - l) / 2，避免 (l+r) 可能溢出</p>
 *         <p><b>判断逻辑：</b></p>
 *             <ul>
 *                 <li>如果 mid² > x：说明 mid 太大，答案在左半部分，r = mid - 1</li>
 *                 <li>如果 mid² ≤ x：说明 mid 可能是答案，但可能有更大的，记录 ans = mid，继续在右半部分查找，l = mid + 1</li>
 *             </ul>
 *     </li>
 *     <li><b>关键点：</b>
 *         <p>为什么需要 ans 变量？因为最终要找的是满足 mid² ≤ x 的最大值</p>
 *         <p>每次找到满足条件的 mid 时，都要记录下来，然后尝试找更大的</p>
 *         <p>当循环结束时，ans 就是最终答案</p>
 *     </li>
 *     <li><b>防止溢出：</b>
 *         <p>计算 mid² 时使用 (long) 强制转换，避免 int 相乘溢出</p>
 *         <p>例如：当 x 接近 Integer.MAX_VALUE 时，mid² 可能超出 int 范围</p>
 *     </li>
 *     <li><b>时间复杂度：</b>O(log x)，二分查找的时间复杂度</p>
 *     <li><b>空间复杂度：</b>O(1)，只使用了常数额外空间</p>
 * </ol>
 *
 * @author lxd
 **/
public class Day20260320 {
    /**
     * 使用二分查找计算 x 的算术平方根（只保留整数部分）
     * <p>
     * <b>实现步骤：</b>
     * <p>
     * <ol>
     *     <li>初始化二分查找边界：l = 0, r = x, ans = -1</li>
     *     <li>当 l <= r 时执行循环：</li>
     *         <ul>
     *             <li>计算中点 mid = l + (r - l) / 2</li>
     *             <li>判断 mid² 与 x 的大小关系：</li>
     *                 <ul>
     *                     <li>如果 mid² > x：说明 mid 太大，调整右边界 r = mid - 1</li>
     *                     <li>如果 mid² ≤ x：记录 ans = mid，并尝试更大的值，调整左边界 l = mid + 1</li>
     *                 </ul>
     *         </ul>
     *     <li>循环结束后，ans 即为所求的平方根（向下取整）</li>
     * </ol>
     * <p>
     * <b>关键点说明：</b>
     * <p>
     * <ul>
     *     <li>搜索范围：[0, x]，因为非负数 x的平方根不会超过 x 本身</li>
     *     <li>为什么用 ans 记录答案？因为要找的是满足 mid² ≤ x 的最大整数</li>
     *     <li>每次发现 mid² ≤ x 时，都要记录 mid，因为它可能是最终答案</li>
     *     <li>即使 mid² ≤ x，仍然要继续向右搜索，因为可能存在更大的满足条件的值</li>
     *     <li>溢出处理：计算 mid² 时使用 (long) 转换，防止 int 相乘溢出</li>
     *     <li>中点公式：l + (r - l) / 2 比 (l + r) / 2 更安全，避免加法溢出</li>
     * </ul>
     *
     * @param x 非负整数
     * @return x的算术平方根（整数部分，向下取整）
     */
    public int mySqrt(int x) {
        // 初始化二分查找的左右边界和答案记录器
        // l: 左边界，从 0 开始
        // r: 右边界，最大为 x（x 的平方根不会超过 x）
        // ans: 用于记录满足条件的最大 mid 值，初始为 -1
        int l = 0, r = x, ans = -1;

        // 二分查找主循环
        // 当 l <= r 时继续搜索，确保覆盖所有可能的值
        while (l <= r) {
            // 计算中点位置
            // 使用 l + (r - l) / 2 而不是 (l + r) / 2，防止 l + r 溢出
            int mid = l + (r - l) / 2;

            // 判断 mid 的平方与 x 的大小关系
            // 使用 (long) 强制转换，避免 mid * mid 计算时 int 溢出
            // 例如：当 x = 2147483647 时，mid 可能达到 46340，mid² = 2147395600，接近 int 最大值
            if ((long) mid * mid > x) {
                // 情况 1：mid² > x
                // 说明 mid 太大，不满足条件，需要在左半部分 [l, mid-1] 继续查找
                r = mid - 1;
            } else {
                // 情况 2：mid² ≤ x
                // 说明 mid 是一个可能的答案，但要找的是满足条件的最大值
                // 所以先记录当前 mid，然后在右半部分 [mid+1, r] 继续查找是否有更大的满足条件的值
                ans = mid;
                l = mid + 1;
            }
        }

        // 返回最终答案
        // 循环结束时，ans 记录的就是满足 ans² ≤ x 的最大整数，即 x的平方根（向下取整）
        return ans;
    }
}

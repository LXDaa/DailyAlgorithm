package CodeTop;

/**
 * <a href="https://leetcode.cn/problems/sum-root-to-leaf-numbers/description/">129. 求根节点到叶节点数字之和</a>
 *
 * <h3>🎯 题目描述</h3>
 * <p>给你一个二叉树的根节点 root，树中每个节点都存放有一个 0 到 9 之间的数字。</p>
 * <p>每条从根节点到叶节点的路径都代表一个数字：</p>
 * <p>例如，从根节点到叶节点的路径 1 -> 2 -> 3 表示数字 123。</p>
 * <p>计算从根节点到叶节点生成的所有数字之和。</p>
 * <p><b>叶节点</b>是指没有子节点的节点。</p>
 *
 * <h3>💡 核心思路：深度优先搜索（DFS）</h3>
 * <ul>
 *   <li><b>问题本质</b>：遍历所有从根到叶的路径，将每条路径组成的数字相加</li>
 *   <li><b>数字构建</b>：从根到当前节点的路径数字 = 父节点路径数字 × 10 + 当前节点值</li>
 *   <li><b>递归策略</b>：
 *     <ul>
 *       <li>向下传递：将当前累积的数字传递给子节点</li>
 *       <li>叶子节点：到达叶子时，返回当前累积的数字</li>
 *       <li>非叶子节点：返回左子树和右子树的结果之和</li>
 *     </ul>
 *   </li>
 *   <li><b>回溯思想</b>：通过参数传递 sum，避免使用全局变量，天然回溯</li>
 * </ul>
 *
 * <h3>🔄 算法步骤</h3>
 * <ol>
 *   <li><b>递归基</b>：如果节点为空，返回 0</li>
 *   <li><b>更新路径数字</b>：sum = sum × 10 + root.val</li>
 *   <li><b>叶子判断</b>：如果是叶子节点（无左右子节点），返回当前 sum</li>
 *   <li><b>递归求和</b>：否则返回左子树结果 + 右子树结果</li>
 * </ol>
 *
 * <h3>📊 ASCII 演示过程</h3>
 * <pre>
 * 示例：
 *        1
 *       / \
 *      2   3
 *
 * 路径1: 1 -> 2 = 12
 * 路径2: 1 -> 3 = 13
 * 结果: 12 + 13 = 25
 *
 * 【递归展开】
 *
 * dfs(1, 0)
 *   sum = 0 * 10 + 1 = 1
 *   不是叶子节点，继续递归
 *   ├── dfs(2, 1)
 *   │     sum = 1 * 10 + 2 = 12
 *   │     是叶子节点 ✅
 *   │     返回 12
 *   │
 *   └── dfs(3, 1)
 *         sum = 1 * 10 + 3 = 13
 *         是叶子节点 ✅
 *         返回 13
 *
 *   返回 12 + 13 = 25 ✅
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 更复杂的例子：
 *        4
 *       / \
 *      9   0
 *     / \
 *    5   1
 *
 * 路径1: 4 -> 9 -> 5 = 495
 * 路径2: 4 -> 9 -> 1 = 491
 * 路径3: 4 -> 0     = 40
 * 结果: 495 + 491 + 40 = 1026
 *
 * 【递归展开】
 *
 * dfs(4, 0)
 *   sum = 0 * 10 + 4 = 4
 *   ├── dfs(9, 4)
 *   │     sum = 4 * 10 + 9 = 49
 *   │     ├── dfs(5, 49)
 *   │     │     sum = 49 * 10 + 5 = 495
 *   │     │     是叶子 ✅ → 返回 495
 *   │     │
 *   │     └── dfs(1, 49)
 *   │           sum = 49 * 10 + 1 = 491
 *   │           是叶子 ✅ → 返回 491
 *   │
 *   │     返回 495 + 491 = 986
 *   │
 *   └── dfs(0, 4)
 *         sum = 4 * 10 + 0 = 40
 *         是叶子 ✅ → 返回 40
 *
 *   返回 986 + 40 = 1026 ✅
 * </pre>
 *
 * <h3>🔑 关键技巧解析</h3>
 * <pre>
 * 1️⃣ 为什么用 sum * 10 + root.val？
 *    - 模拟十进制数的构建过程
 *    - 例如：路径 1->2->3
 *      第1步: 0 * 10 + 1 = 1
 *      第2步: 1 * 10 + 2 = 12
 *      第3步: 12 * 10 + 3 = 123
 *
 * 2️⃣ 为什么通过参数传递 sum？
 *    - 避免使用全局变量或成员变量
 *    - 每次递归调用有独立的 sum 副本
 *    - 回溯时自动恢复上一层的 sum 值
 *    - 代码更简洁，无线程安全问题
 *
 * 3️⃣ 叶子节点的判断条件？
 *    - root.left == null && root.right == null
 *    - 必须同时满足左右子节点都为空
 *    - 只有叶子节点才返回最终的数字
 *
 * 4️⃣ 空节点为什么返回 0？
 *    - 空节点不构成路径，对总和贡献为 0
 *    - 例如：某节点只有左子树，右子树为空
 *    - 右子树返回 0，不影响左子树的结果
 *
 * 5️⃣ 时间复杂度：O(n)
 *    - 遍历每个节点一次
 *    - n 为节点总数
 *
 * 6️⃣ 空间复杂度：O(h)
 *    - h 为树的高度（递归栈深度）
 *    - 最坏情况（链表）：O(n)
 *    - 最好情况（平衡树）：O(log n)
 * </pre>
 *
 * <h3>⚠️ 注意事项</h3>
 * <ul>
 *   <li><b>整数溢出</b>：如果树很深，路径数字可能超出 int 范围，实际应考虑 long</li>
 *   <li><b>叶子判断</b>：必须先更新 sum，再判断是否为叶子节点</li>
 *   <li><b>空节点处理</b>：root == null 时返回 0，作为递归终止条件</li>
 *   <li><b>参数传递</b>：sum 是值传递，每层递归有独立副本，无需手动回溯</li>
 *   <li><b>节点值范围</b>：题目保证节点值为 0-9，符合十进制规则</li>
 * </ul>
 *
 * @author lxd
 **/
public class Day20260415 {
    /**
     * 🌳 计算根节点到叶节点数字之和
     *
     * @param root 二叉树根节点
     * @return 所有路径数字之和
     */
    public int sumNumbers(TreeNode root) {
        // 🚀 启动 DFS，初始路径数字为 0
        return dfs(root, 0);
    }

    /**
     * 🔄 深度优先搜索：递归计算路径数字之和
     *
     * @param root 当前节点
     * @param sum  从根到父节点的路径数字
     * @return 以当前节点为根的子树中，所有路径数字之和
     */
    private int dfs(TreeNode root, int sum) {
        // 🛑 递归基：空节点，贡献为 0
        if (root == null) {
            return 0;
        }

        // 🔢 更新路径数字：父节点路径 * 10 + 当前节点值
        sum = sum * 10 + root.val;

        // 🍃 叶子节点：返回当前路径数字
        if (root.left == null && root.right == null) {
            return sum;
        }

        // 🌿 非叶子节点：返回左子树 + 右子树的结果
        return dfs(root.left, sum) + dfs(root.right, sum);
    }
}

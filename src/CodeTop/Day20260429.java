package CodeTop;

/**
 * <a href="https://leetcode.cn/problems/best-time-to-buy-and-sell-stock-ii/description/">122. 买卖股票的最佳时机 II</a>
 *
 * <h3>🎯 题目描述</h3>
 * <p>给你一个整数数组 prices，其中 prices[i] 表示某支股票第 i 天的价格。</p>
 * <p>在每一天，你可以决定是否购买和/或出售股票。你在任何时候最多只能持有一股股票。然而，你可以在同一天多次买卖该股票，但要确保你持有的股票不超过一股。</p>
 * <p>返回你能获得的最大利润。</p>
 *
 * <h3>💡 核心思路：动态规划（状态机 DP）</h3>
 * <ul>
 *   <li><b>问题特点</b>：可以多次交易，但同一时间只能持有一股</li>
 *   <li><b>状态定义</b>：dp[i][j] 表示第 i 天结束时的最大利润
 *     <ul>
 *       <li>j = 0：第 i 天结束时<b>不持有</b>股票的最大利润</li>
 *       <li>j = 1：第 i 天结束时<b>持有</b>股票的最大利润</li>
 *     </ul>
 *   </li>
 *   <li><b>状态转移</b>：
 *     <ul>
 *       <li>dp[i][0] = max(昨天也不持有, 昨天持有今天卖出)</li>
 *       <li>dp[i][1] = max(昨天也持有, 昨天不持有今天买入)</li>
 *     </ul>
 *   </li>
 *   <li><b>最终结果</b>：dp[n-1][0]（最后一天不持有股票的利润最大）</li>
 * </ul>
 *
 * <h3>🔄 算法步骤</h3>
 * <ol>
 *   <li><b>初始化</b>：
 *     <ul>
 *       <li>dp[0][0] = 0（第 0 天不持有，利润为 0）</li>
 *       <li>dp[0][1] = -prices[0]（第 0 天持有，即买入，利润为 -prices[0]）</li>
 *     </ul>
 *   </li>
 *   <li><b>状态转移</b>：从第 1 天开始遍历
 *     <ul>
 *       <li>dp[i][0] = max(dp[i-1][0], dp[i-1][1] + prices[i])
 *         <ul>
 *           <li>选择1：昨天也不持有，今天继续不持有</li>
 *           <li>选择2：昨天持有，今天卖出（+prices[i]）</li>
 *         </ul>
 *       </li>
 *       <li>dp[i][1] = max(dp[i-1][1], dp[i-1][0] - prices[i])
 *         <ul>
 *           <li>选择1：昨天也持有，今天继续持有</li>
 *           <li>选择2：昨天不持有，今天买入（-prices[i]）</li>
 *         </ul>
 *       </li>
 *     </ul>
 *   </li>
 *   <li><b>返回结果</b>：dp[n-1][0]（最后一天不持有股票）</li>
 * </ol>
 *
 * <h3>📊 ASCII 演示过程</h3>
 * <pre>
 * 示例：prices = [7, 1, 5, 3, 6, 4]
 *
 * 【构建 DP 表格】
 *
 * 初始状态 (i=0):
 *   dp[0][0] = 0        (不持有)
 *   dp[0][1] = -7       (持有，买入价格 7)
 *
 * i=1 (price=1):
 *   dp[1][0] = max(dp[0][0], dp[0][1]+1) = max(0, -7+1) = max(0, -6) = 0
 *   dp[1][1] = max(dp[0][1], dp[0][0]-1) = max(-7, 0-1) = max(-7, -1) = -1
 *   → 不持有: 0, 持有: -1 (在价格 1 时买入更优)
 *
 * i=2 (price=5):
 *   dp[2][0] = max(dp[1][0], dp[1][1]+5) = max(0, -1+5) = max(0, 4) = 4
 *   dp[2][1] = max(dp[1][1], dp[1][0]-5) = max(-1, 0-5) = max(-1, -5) = -1
 *   → 不持有: 4 (卖出获利), 持有: -1
 *
 * i=3 (price=3):
 *   dp[3][0] = max(dp[2][0], dp[2][1]+3) = max(4, -1+3) = max(4, 2) = 4
 *   dp[3][1] = max(dp[2][1], dp[2][0]-3) = max(-1, 4-3) = max(-1, 1) = 1
 *   → 不持有: 4, 持有: 1 (之前获利 4，现在花 3 买入，净剩 1)
 *
 * i=4 (price=6):
 *   dp[4][0] = max(dp[3][0], dp[3][1]+6) = max(4, 1+6) = max(4, 7) = 7
 *   dp[4][1] = max(dp[3][1], dp[3][0]-6) = max(1, 4-6) = max(1, -2) = 1
 *   → 不持有: 7 (卖出获利), 持有: 1
 *
 * i=5 (price=4):
 *   dp[5][0] = max(dp[4][0], dp[4][1]+4) = max(7, 1+4) = max(7, 5) = 7
 *   dp[5][1] = max(dp[4][1], dp[4][0]-4) = max(1, 7-4) = max(1, 3) = 3
 *   → 不持有: 7, 持有: 3
 *
 * 最终 DP 表格:
 *   天数  价格  不持有  持有
 *   0     7     0      -7
 *   1     1     0      -1
 *   2     5     4      -1
 *   3     3     4       1
 *   4     6     7       1
 *   5     4     7       3
 *
 * 最大利润 = dp[5][0] = 7 ✅
 *
 * 【交易策略回溯】
 *   第1天买入 (price=1)
 *   第2天卖出 (price=5) → 利润 4
 *   第3天买入 (price=3)
 *   第4天卖出 (price=6) → 利润 3
 *   总利润: 4 + 3 = 7 ✅
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 贪心解法对比：
 *   只要明天价格比今天高，今天就买入，明天卖出
 *   (5-1) + (6-3) = 4 + 3 = 7 ✅
 *   DP 解法和贪心解法结果相同！
 * </pre>
 *
 * <h3>🔑 关键技巧解析</h3>
 * <pre>
 * 1️⃣ 为什么用二维 DP？
 *    - 需要区分"持有"和"不持有"两种状态
 *    - 持有股票时的利润可能是负数（刚买入）
 *    - 不持有股票时的利润是已实现的收益
 *
 * 2️⃣ 状态转移方程的含义？
 *    - dp[i][0]: 今天不持有 = max(昨天也不持有, 昨天持有今天卖出)
 *    - dp[i][1]: 今天持有 = max(昨天也持有, 昨天不持有今天买入)
 *    - 每次选择都取最大值，保证最优
 *
 * 3️⃣ 为什么最后返回 dp[n-1][0]？
 *    - 最后一天不持有股票，说明所有交易已完成
 *    - 如果还持有股票，说明还有未实现的收益
 *    - 不持有的利润一定 >= 持有的利润
 *
 * 4️⃣ 与贪心算法的关系？
 *    - 贪心：只要 prices[i] > prices[i-1]，就累加差值
 *    - DP：更通用，可以处理更复杂的股票问题
 *    - 本题中两者等价，但 DP 思路更清晰
 *
 * 5️⃣ 时间复杂度：O(n)
 *    - n 为天数
 *    - 遍历一次价格数组
 *
 * 6️⃣ 空间复杂度：O(n)
 *    - 使用了 n×2 的 DP 表格
 *    - 优化：可以只用两个变量，降到 O(1)
 *
 * 7️⃣ 与其他股票问题的区别？
 *    - 本题：无限次交易 → 简单 DP
 *    - 最多一次交易：记录最小价格
 *    - 最多 k 次交易：三维 DP
 *    - 有冷却期：增加状态
 * </pre>
 *
 * <h3>⚠️ 注意事项</h3>
 * <ul>
 *   <li><b>初始化</b>：dp[0][1] = -prices[0]，表示第一天买入</li>
 *   <li><b>返回值</b>：返回 dp[n-1][0]，不是 dp[n-1][1]</li>
 *   <li><b>状态含义</b>：dp[i][j] 是第 i 天<b>结束时</b>的状态</li>
 *   <li><b>同一天买卖</b>：允许，但不影响结果（相当于没操作）</li>
 *   <li><b>空间优化</b>：可以只用 prevHold 和 prevNotHold 两个变量</li>
 *   <li><b>边界条件</b>：如果 prices 为空，返回 0</li>
 * </ul>
 *
 * @author lxd
 **/
public class Day20260429 {
    /**
     * 💰 计算股票交易的最大利润（动态规划解法）
     *
     * @param prices 股票价格数组，prices[i] 表示第 i 天的价格
     * @return 最大利润
     */
    public int maxProfit(int[] prices) {
        int n = prices.length;

        // 📊 DP 表格：dp[i][0]=不持有, dp[i][1]=持有
        int[][] dp = new int[n][2];

        // 📍 初始化第 0 天的状态
        dp[0][0] = 0;           // 不持有：利润为 0
        dp[0][1] = -prices[0];  // 持有：买入股票，利润为 -prices[0]

        // 🔄 状态转移：从第 1 天开始遍历
        for (int i = 1; i < n; i++) {
            // 💵 今天不持有 = max(昨天也不持有, 昨天持有今天卖出)
            dp[i][0] = Math.max(dp[i - 1][0], dp[i - 1][1] + prices[i]);

            // 📦 今天持有 = max(昨天也持有, 昨天不持有今天买入)
            dp[i][1] = Math.max(dp[i - 1][1], dp[i - 1][0] - prices[i]);
        }

        // ✅ 返回最后一天不持有股票的最大利润
        return dp[n - 1][0];
    }
}

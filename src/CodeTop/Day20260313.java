package CodeTop;

/**
 * <a href="https://leetcode.cn/problems/compare-version-numbers/description/">165. 比较版本号</a>
 * <p>
 * 给你两个 版本号字符串 version1 和 version2 ，请你比较它们。版本号由被点 '.' 分开的修订号组成。修订号的值 是它 转换为整数 并忽略前导零。
 * <p>
 * 比较版本号时，请按 从左到右的顺序 依次比较它们的修订号。如果其中一个版本字符串的修订号较少，则将缺失的修订号视为 0。
 * <p>
 * 返回规则如下：
 * <p>
 * 如果 version1 < version2 返回 -1，
 * 如果 version1 > version2 返回 1，
 * 除此之外返回 0。
 * <p>
 * <b>解题思路：</b>
 * <p>
 * <ol>
 *     <li><b>问题分析：</b>
 *         <p>需要比较两个版本号的大小，版本号由点号分隔的多个修订号组成</p>
 *         <p>比较规则：从左到右依次比较每个修订号，修订号转换为整数进行比较（自动忽略前导零）</p>
 *         <p>如果某个版本号的修订号用完了，则视为 0</p>
 *     </li>
 *     <li><b>核心方法：双指针同步遍历</b>
 *         <p>使用两个指针 i 和 j 分别遍历 version1 和 version2</p>
 *         <p>不需要预先分割字符串，而是边遍历边解析每个修订号的数值</p>
 *         <p>这样可以节省空间复杂度，避免创建额外的数组</p>
 *     </li>
 *     <li><b>修订号解析技巧：</b>
 *         <p>通过循环读取字符，直到遇到点号或字符串末尾</p>
 *         <p>使用 x = x * 10 + (char - '0') 的方式逐位构建整数</p>
 *         <p>这种方式天然地忽略了前导零（因为 0*10+0=0）</p>
 *     </li>
 *     <li><b>比较逻辑：</b>
 *         <p>每解析出一对修订号就立即比较</p>
 *         <p>如果发现不相等，直接返回结果（-1 或 1）</p>
 *         <p>如果所有修订号都相等，返回 0</p>
 *     </li>
 *     <li><b>时间复杂度：</b>O(max(m,n))，其中 m 和 n 分别是两个版本号的长度</p>
 *     <li><b>空间复杂度：</b>O(1)，只使用了常数额外空间</p>
 * </ol>
 *
 * @author lxd
 **/
public class Day20260313 {
    /**
     * 比较两个版本号的大小
     * <p>
     * <b>实现步骤：</b>
     * <p>
     * <ol>
     *     <li>初始化两个指针 i 和 j，分别指向 version1 和 version2 的起始位置</li>
     *     <li>当任一指针未到达字符串末尾时，继续循环（确保处理完所有修订号）</li>
     *     <li>解析 version1 的当前修订号：</li>
     *         <ul>
     *             <li>初始化 x = 0</li>
     *             <li>循环读取字符，直到遇到点号或字符串末尾</li>
     *             <li>使用公式 x = x * 10 + (charAt(i) - '0') 逐位构建整数</li>
     *             <li>i++ 移动到下一个位置（跳过点号）</li>
     *         </ul>
     *     <li>同样方式解析 version2 的当前修订号 y</li>
     *     <li>比较 x 和 y：</li>
     *         <ul>
     *             <li>如果 x != y，直接返回比较结果（x<y 返回 -1，否则返回 1）</li>
     *             <li>如果 x == y，继续下一轮循环比较下一个修订号</li>
     *         </ul>
     *     <li>所有修订号都比较完毕且全部相等，返回 0</li>
     * </ol>
     * <p>
     * <b>关键点说明：</b>
     * <p>
     * <ul>
     *     <li>指针越界处理：当 i >= m 时，内层 for 循环不会执行，x 保持为 0，符合"缺失的修订号视为 0"的规则</li>
     *     <li>前导零处理：通过数学计算方式构建整数，前导零会自动被忽略（例如 "007" -> 7）</li>
     *     <li>点号跳过：每次内层循环结束后，通过 i++ 和 j++ 跳过点号，准备解析下一个修订号</li>
     * </ul>
     *
     * @param version1 版本号字符串 1
     * @param version2 版本号字符串 2
     * @return version1 < version2 返回 -1；version1 > version2 返回 1；否则返回 0
     */
    public int compareVersion(String version1, String version2) {
        // 获取两个版本号字符串的长度
        int m = version1.length();
        int n = version2.length();

        // 初始化两个指针，分别指向两个字符串的起始位置
        int i = 0, j = 0;

        // 当任一指针未到达字符串末尾时继续循环
        // 这样可以确保即使两个版本号长度不同，也能完整比较所有修订号
        while (i < m || j < n) {
            // 解析 version1 的当前修订号
            int x = 0;
            // 循环读取字符直到遇到点号或字符串末尾
            // 通过 x * 10 + (字符 - '0') 的方式逐位构建整数
            // 这种方式自动忽略前导零（例如："007" -> 0*10+0=0, 0*10+7=7）
            for (; i < m && version1.charAt(i) != '.'; i++) {
                x = x * 10 + version1.charAt(i) - '0';
            }
            // 跳过点号，移动到下一个修订号的起始位置
            i++;

            // 同样的方式解析 version2 的当前修订号
            int y = 0;
            for (; j < n && version2.charAt(j) != '.'; j++) {
                y = y * 10 + version2.charAt(j) - '0';
            }
            // 跳过点号
            j++;

            // 比较当前两个修订号的大小
            // 如果不相等，直接返回比较结果，无需继续比较后续修订号
            if (x != y) {
                return x < y ? -1 : 1;
            }
        }

        // 所有修订号都比较完毕且全部相等，返回 0
        return 0;
    }
}

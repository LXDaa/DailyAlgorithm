package CodeTop;

import java.util.Deque;
import java.util.LinkedList;

/**
 * <a href="https://leetcode.cn/problems/sliding-window-maximum/description/">239. 滑动窗口最大值</a>
 * <p>
 * 给你一个整数数组 nums，有一个大小为 k 的滑动窗口从数组的最左侧移动到数组的最右侧。你只可以看到在滑动窗口内的 k 个数字。滑动窗口每次只向右移动一位。
 * <p>
 * 返回 滑动窗口中的最大值 。
 * <p>
 * <b>解题思路：</b>
 * <p>
 * <ol>
 *     <li><b>问题分析：</b>
 *         <p>需要在滑动窗口中动态地找到最大值，窗口大小为 k，每次向右移动一位</p>
 *         <p>暴力解法：对每个窗口遍历找最大值，时间复杂度 O(n*k)，会超时</p>
 *         <p>需要一种数据结构能够高效地维护窗口内的最大值信息</p>
 *     </li>
 *     <li><b>核心方法：单调队列（双端队列）</b>
 *         <p>使用双端队列维护一个单调递减的序列，队列头部始终是当前窗口的最大值</p>
 *         <p>队列中存储的是数组元素的下标，而非元素值本身（方便判断元素是否已滑出窗口）</p>
 *         <p>单调递减：队列中下标对应的元素值从队首到队尾单调递减</p>
 *     </li>
 *     <li><b>维护单调队列的策略：</b>
 *         <p><b>入队操作：</b>当新元素要加入队列时，从队尾开始比较</p>
 *             <ul>
 *                 <li>如果新元素 >= 队尾元素对应的值，说明队尾元素不可能成为最大值，弹出队尾</li>
 *                 <li>重复此过程直到队列为空或新元素 < 队尾元素</li>
 *                 <li>将新元素下标加入队尾</li>
 *             </ul>
 *         <p><b>出队操作：</b>检查队首元素是否已滑出窗口</p>
 *             <ul>
 *                 <li>如果队首下标 <= 当前窗口左边界 - k，说明该元素已不在窗口内，弹出队首</li>
 *             </ul>
 *     </li>
 *     <li><b>算法流程：</b>
 *         <p>第一阶段：初始化前 k 个元素，构建初始的单调队列</p>
 *         <p>第二阶段：从第 k 个元素开始，每次移动窗口：</p>
 *             <ul>
 *                 <li>处理新元素入队（维护单调性）</li>
 *                 <li>检查队首是否过期（移除超出窗口的元素）</li>
 *                 <li>记录当前窗口最大值（队首元素对应的值）</li>
 *             </ul>
 *     </li>
 *     <li><b>时间复杂度：</b>O(n)，每个元素最多入队和出队各一次</p>
 *     <li><b>空间复杂度：</b>O(k)，双端队列最多存储 k 个元素</p>
 * </ol>
 *
 * @author lxd
 **/
public class Day20260319 {
    /**
     * 使用单调队列求滑动窗口中的最大值
     * <p>
     * <b>实现步骤：</b>
     * <p>
     * <ol>
     *     <li>创建双端队列 deque，用于维护单调递减序列（存储下标）</li>
     *     <li>初始化前 k 个元素：构建初始的单调队列</li>
     *         <ul>
     *             <li>遍历前 k 个元素，对每个元素 nums[i]：</li>
     *             <li>从队尾开始比较，如果 nums[i] >= nums[队尾下标]，弹出队尾（维护单调性）</li>
     *             <li>将 i 加入队尾</li>
     *         </ul>
     *     <li>创建结果数组 ans，长度为 n - k + 1</li>
     *     <li>记录第一个窗口的最大值：ans[0] = nums[deque.peekFirst()]</li>
     *     <li>处理后续窗口（从下标 k 到 n-1）：</li>
     *         <ul>
     *             <li>处理新元素 nums[i] 入队（同样维护单调性）</li>
     *             <li>检查队首是否过期：如果队首下标 <= i - k，弹出队首</li>
     *             <li>记录当前窗口最大值到 ans[i - k + 1]</li>
     *         </ul>
     *     <li>返回结果数组 ans</li>
     * </ol>
     * <p>
     * <b>关键点说明：</b>
     * <p>
     * <ul>
     *     <li>为什么存储下标而不是值？因为需要判断队首元素是否已经滑出窗口范围</li>
     *     <li>为什么可以安全地弹出队尾？因为新元素更大且在它右边，队尾元素永远不可能成为最大值</li>
     *     <li>队首元素的特性：队首始终是当前窗口内最大元素的下标（单调递减性质保证）</li>
     *     <li>窗口范围：[i-k+1, i]，其中 i 是当前遍历到的位置</li>
     * </ul>
     *
     * @param nums 输入的整数数组
     * @param k    滑动窗口的大小
     * @return 每个滑动窗口中的最大值组成的数组
     */
    public int[] maxSlidingWindow(int[] nums, int k) {
        // 获取数组长度
        int n = nums.length;

        // 创建双端队列，用于维护单调递减序列
        // 队列中存储的是数组下标，这样可以方便地判断元素是否已滑出窗口
        Deque<Integer> deque = new LinkedList<>();

        // 第一阶段：初始化前 k 个元素，构建初始的单调队列
        for (int i = 0; i < k; i++) {
            // 维护队列的单调性：从队尾开始，移除所有小于等于当前元素的值
            // 原因：如果 nums[i] >= 队尾元素，说明队尾元素不可能成为窗口最大值
            // 因为 nums[i] 更大且在它右边，会"覆盖"队尾元素的作用
            while (!deque.isEmpty() && nums[i] >= nums[deque.peekLast()]) {
                deque.pollLast();
            }
            // 将当前元素下标加入队尾
            deque.offerLast(i);
        }

        // 创建结果数组，用于存储每个窗口的最大值
        // 窗口数量为 n - k + 1（从下标 0 到 n-k）
        int[] ans = new int[n - k + 1];

        // 记录第一个窗口的最大值：队列头部下标对应的元素值
        // 由于队列是单调递减的，队首元素就是当前窗口的最大值
        ans[0] = nums[deque.peekFirst()];

        // 第二阶段：处理后续的滑动窗口（从第 k 个元素开始）
        for (int i = k; i < n; i++) {
            // 步骤 1：处理新元素 nums[i] 入队，维护队列单调性
            // 与初始化阶段相同，移除队尾所有小于等于当前元素的值
            while (!deque.isEmpty() && nums[i] >= nums[deque.peekLast()]) {
                deque.pollLast();
            }
            // 将当前元素下标加入队尾
            deque.offerLast(i);

            // 步骤 2：检查队首元素是否已滑出窗口
            // 当前窗口范围是 [i-k+1, i]，如果队首下标 <= i-k，说明已不在窗口内
            // 例如：k=3, i=3 时，窗口是 [1,2,3]，下标 0 应该被移除
            while (deque.peekFirst() <= i - k) {
                deque.pollFirst();
            }

            // 步骤 3：记录当前窗口的最大值
            // 队首下标对应的元素即为当前窗口的最大值
            // 结果数组下标：i - k + 1（第几个窗口，从 0 开始）
            ans[i - k + 1] = nums[deque.peekFirst()];
        }

        // 返回结果数组
        return ans;
    }
}

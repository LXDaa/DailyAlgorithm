/**
 * <a href="https://leetcode.cn/problems/maximum-product-subarray/description/">152. 乘积最大子数组</a>
 *
 * <h3>🎯 题目描述</h3>
 * <p>给你一个整数数组 nums ，请你找出数组中乘积最大的非空连续 <b>子数组</b>（该子数组中至少包含一个数字），并返回该子数组所对应的乘积。</p>
 * <p>测试用例的答案是一个 <b>32-位</b> 整数。</p>
 * <p><b>注意</b>：一个只包含一个元素的数组的乘积是这个元素的值。</p>
 *
 * <h3>💡 核心思路：动态规划（维护最大值和最小值）</h3>
 * <ul>
 *   <li><b>问题挑战</b>：负数的存在使得最大值可能变成最小值，最小值可能变成最大值
 *     <ul>
 *       <li>例如：[-2, 3, -4]，当遇到第二个负数时，之前的最小值（负数）乘以负数会变成最大值</li>
 *     </ul>
 *   </li>
 *   <li><b>解决方案</b>：同时维护以当前元素结尾的最大乘积和最小乘积
 *     <ul>
 *       <li><b>imax</b>：以当前元素结尾的最大乘积</li>
 *       <li><b>imin</b>：以当前元素结尾的最小乘积</li>
 *       <li>遇到负数时，交换 imax 和 imin（因为负数会使最大值变最小，最小值变最大）</li>
 *     </ul>
 *   </li>
 *   <li><b>状态转移</b>：
 *     <ul>
 *       <li>imax = max(imax * nums[i], nums[i])</li>
 *       <li>imin = min(imin * nums[i], nums[i])</li>
 *     </ul>
 *   </li>
 *   <li><b>时间复杂度</b>：O(n)，只需一次遍历</li>
 *   <li><b>空间复杂度</b>：O(1)，只使用了常数个变量</li>
 * </ul>
 *
 * <h3>🔄 算法步骤</h3>
 * <ol>
 *   <li><b>初始化变量</b>：
 *     <ul>
 *       <li>max：全局最大乘积，初始化为 Integer.MIN_VALUE</li>
 *       <li>imax：以当前元素结尾的最大乘积，初始化为 1</li>
 *       <li>imin：以当前元素结尾的最小乘积，初始化为 1</li>
 *     </ul>
 *   </li>
 *   <li><b>遍历数组</b>：对于每个元素 nums[i]
 *     <ol>
 *       <li><b>处理负数</b>：如果 nums[i] < 0，交换 imax 和 imin</li>
 *       <li><b>更新 imax</b>：imax = max(imax * nums[i], nums[i])
 *         <ul>
 *           <li>要么延续之前的乘积（imax * nums[i]）</li>
 *           <li>要么从当前元素重新开始（nums[i]）</li>
 *         </ul>
 *       </li>
 *       <li><b>更新 imin</b>：imin = min(imin * nums[i], nums[i])</li>
 *       <li><b>更新全局最大值</b>：max = max(max, imax)</li>
 *     </ol>
 *   </li>
 *   <li><b>返回结果</b>：返回 max</li>
 * </ol>
 *
 * <h3>📊 ASCII 演示过程</h3>
 * <pre>
 * 示例1：包含负数的数组 [2, 3, -2, 4]
 *
 * 初始化：max = MIN_VALUE, imax = 1, imin = 1
 *
 * 【i=0】nums[0] = 2（正数）
 *   - 不需要交换 imax 和 imin
 *   - imax = max(1 * 2, 2) = max(2, 2) = 2
 *   - imin = min(1 * 2, 2) = min(2, 2) = 2
 *   - max = max(MIN_VALUE, 2) = 2
 *
 *   状态：imax=2, imin=2, max=2
 *   解释：以 2 结尾的最大/最小乘积都是 2
 *
 * 【i=1】nums[1] = 3（正数）
 *   - 不需要交换
 *   - imax = max(2 * 3, 3) = max(6, 3) = 6
 *   - imin = min(2 * 3, 3) = min(6, 3) = 3
 *   - max = max(2, 6) = 6
 *
 *   状态：imax=6, imin=3, max=6
 *   解释：以 3 结尾的最大乘积是 6（2×3），最小是 3
 *
 * 【i=2】nums[2] = -2（负数 ⚠️）
 *   - 🔄 交换 imax 和 imin：imax=3, imin=6
 *   - imax = max(3 * (-2), -2) = max(-6, -2) = -2
 *   - imin = min(6 * (-2), -2) = min(-12, -2) = -12
 *   - max = max(6, -2) = 6
 *
 *   状态：imax=-2, imin=-12, max=6
 *   解释：遇到负数，最大值变最小，最小值变最大
 *         以 -2 结尾的最大乘积是 -2（重新开始），最小是 -12（6×(-2)）
 *
 * 【i=3】nums[3] = 4（正数）
 *   - 不需要交换
 *   - imax = max(-2 * 4, 4) = max(-8, 4) = 4
 *   - imin = min(-12 * 4, 4) = min(-48, 4) = -48
 *   - max = max(6, 4) = 6
 *
 *   状态：imax=4, imin=-48, max=6
 *
 * 最终结果：6 ✅
 * 最大乘积子数组：[2, 3]，乘积为 6
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 示例2：负负得正 [-2, 0, -1]
 *
 * 初始化：max = MIN_VALUE, imax = 1, imin = 1
 *
 * 【i=0】nums[0] = -2（负数）
 *   - 🔄 交换：imax=1, imin=1（交换后不变）
 *   - imax = max(1 * (-2), -2) = -2
 *   - imin = min(1 * (-2), -2) = -2
 *   - max = max(MIN_VALUE, -2) = -2
 *
 *   状态：imax=-2, imin=-2, max=-2
 *
 * 【i=1】nums[1] = 0
 *   - 不需要交换
 *   - imax = max(-2 * 0, 0) = max(0, 0) = 0
 *   - imin = min(-2 * 0, 0) = min(0, 0) = 0
 *   - max = max(-2, 0) = 0
 *
 *   状态：imax=0, imin=0, max=0
 *   解释：遇到 0，最大最小都重置为 0
 *
 * 【i=2】nums[2] = -1（负数）
 *   - 🔄 交换：imax=0, imin=0（交换后不变）
 *   - imax = max(0 * (-1), -1) = max(0, -1) = 0
 *   - imin = min(0 * (-1), -1) = min(0, -1) = -1
 *   - max = max(0, 0) = 0
 *
 *   状态：imax=0, imin=-1, max=0
 *
 * 最终结果：0 ✅
 * 最大乘积子数组：[0]，乘积为 0
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 示例3：多个负数 [-2, 3, -4]
 *
 * 初始化：max = MIN_VALUE, imax = 1, imin = 1
 *
 * 【i=0】nums[0] = -2（负数）
 *   - 🔄 交换：imax=1, imin=1
 *   - imax = max(-2, -2) = -2
 *   - imin = min(-2, -2) = -2
 *   - max = -2
 *
 *   状态：imax=-2, imin=-2, max=-2
 *
 * 【i=1】nums[1] = 3（正数）
 *   - 不交换
 *   - imax = max(-2 * 3, 3) = max(-6, 3) = 3
 *   - imin = min(-2 * 3, 3) = min(-6, 3) = -6
 *   - max = max(-2, 3) = 3
 *
 *   状态：imax=3, imin=-6, max=3
 *   解释：以 3 结尾，最大是 3（重新开始），最小是 -6（-2×3）
 *
 * 【i=2】nums[2] = -4（负数 ⚠️）
 *   - 🔄 交换：imax=-6, imin=3
 *   - imax = max(-6 * (-4), -4) = max(24, -4) = 24 ✨
 *   - imin = min(3 * (-4), -4) = min(-12, -4) = -12
 *   - max = max(3, 24) = 24
 *
 *   状态：imax=24, imin=-12, max=24
 *   解释：负负得正！之前的最小值 -6 乘以 -4 得到最大值 24
 *
 * 最终结果：24 ✅
 * 最大乘积子数组：[-2, 3, -4]，乘积为 24
 * </pre>
 *
 * <h3>🔑 关键技巧解析</h3>
 * <pre>
 * 1️⃣ 为什么要同时维护最大值和最小值？
 *    - 负数的存在使得「最小值 × 负数」可能变成「最大值」
 *    - 例如：imin = -10，遇到 -2，则 -10 × (-2) = 20（可能是新的最大值）
 *    - 如果只维护最大值，会丢失这种可能性
 *
 * 2️⃣ 为什么遇到负数要交换 imax 和 imin？
 *    - 负数会改变大小关系：大的变小的，小的变大的
 *    - 例如：imax=5, imin=-3，遇到 -2
 *      - 如果不交换：imax = max(5×(-2), -2) = max(-10, -2) = -2
 *      - 但实际应该考虑：imin×(-2) = -3×(-2) = 6（这才是真正的最大值）
 *    - 交换后：imax=-3, imin=5
 *      - imax = max(-3×(-2), -2) = max(6, -2) = 6 ✅
 *
 * 3️⃣ 为什么 imax 和 imin 要与 nums[i] 比较？
 *    - 当之前的乘积小于当前元素时，应该从当前元素重新开始
 *    - 例如：imax=-5, nums[i]=3
 *      - imax * nums[i] = -15，比 3 小
 *      - 所以选择从 3 重新开始，imax = 3
 *    - 这对应于「子数组可以重新开始」的特性
 *
 * 4️⃣ 为什么 imax 和 imin 初始化为 1？
 *    - 1 是乘法单位元，不影响第一次计算
 *    - 第一次迭代：imax = max(1 * nums[0], nums[0]) = nums[0]
 *    - 这样保证了第一个元素能正确被处理
 *
 * 5️⃣ 与「最大子数组和」的区别？
 *    - 最大子数组和（Kadane's Algorithm）：只需要维护一个最大值
 *    - 乘积最大子数组：需要同时维护最大值和最小值
 *    - 原因：加法没有「负负得正」的特性，乘法有
 *
 * 6️⃣ 时间复杂度：O(n)
 *    - 只需一次遍历数组
 *    - 每次迭代的操作都是 O(1)
 *    - 总体线性时间复杂度
 *
 * 7️⃣ 空间复杂度：O(1)
 *    - 只使用了三个变量：max, imax, imin
 *    - 没有额外的数据结构
 *    - 最优空间复杂度
 *
 * 8️⃣ 边界情况处理？
 *    - 单个元素：直接返回该元素
 *    - 包含 0：imax 和 imin 会被重置为 0，然后从下一个元素重新开始
 *    - 全负数：算法能正确处理，找到乘积最大的子数组
 *    - 包含正负混合：通过交换机制正确处理
 * </pre>
 *
 * <h3>⚠️ 注意事项</h3>
 * <ul>
 *   <li><b>负数处理</b>：遇到负数时必须交换 imax 和 imin</li>
 *   <li><b>初始化</b>：max 初始化为 Integer.MIN_VALUE，确保能被第一个元素更新</li>
 *   <li><b>重置机制</b>：通过与 nums[i] 比较，实现子数组的重新开始</li>
 *   <li><b>0 的处理</b>：遇到 0 时，imax 和 imin 都会变成 0，相当于重新开始</li>
 *   <li><b>溢出问题</b>：题目保证答案在 32 位整数范围内，无需特殊处理</li>
 *   <li><b>非空子数组</b>：子数组至少包含一个元素，不能为空</li>
 * </ul>
 *
 * @author lxd
 **/
public class Day20260508 {
    /**
     * 🔍 计算乘积最大的连续子数组的乘积
     * <p>使用动态规划，同时维护最大值和最小值来处理负数</p>
     *
     * @param nums 整数数组
     * @return 最大乘积
     */
    public int maxProduct(int[] nums) {
        // 📊 初始化变量
        int max = Integer.MIN_VALUE;  // 全局最大乘积
        int imax = 1;                 // 以当前元素结尾的最大乘积
        int imin = 1;                 // 以当前元素结尾的最小乘积

        // 🔄 遍历数组
        for (int i = 0; i < nums.length; i++) {
            // ⚠️ 如果当前元素是负数，交换最大值和最小值
            if (nums[i] < 0) {
                int temp = imax;
                imax = imin;
                imin = temp;
            }

            // 📈 更新以当前元素结尾的最大乘积
            // 要么延续之前的乘积，要么从当前元素重新开始
            imax = Math.max(imax * nums[i], nums[i]);

            // 📉 更新以当前元素结尾的最小乘积
            imin = Math.min(imin * nums[i], nums[i]);

            // 🏆 更新全局最大乘积
            max = Math.max(max, imax);
        }

        // ✅ 返回结果
        return max;
    }
}

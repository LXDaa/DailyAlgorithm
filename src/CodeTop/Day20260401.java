package CodeTop;

/**
 * <a href="https://leetcode.cn/problems/climbing-stairs/description/">70. 爬楼梯</a>
 * <p>
 * 假设你正在爬楼梯。需要 n 阶你才能到达楼顶。
 * <p>
 * 每次你可以爬 1 或 2 个台阶。你有多少种不同的方法可以爬到楼顶呢？
 *
 * <h2>解题思路</h2>
 * <h3>1. 题目分析</h3>
 * <ul>
 *     <li><strong>动态规划入门题</strong>：经典的动态规划问题，类似于斐波那契数列</li>
 *     <li><strong>最后一步分析</strong>：要到达第 n 阶，可以从第 n-1 阶爬 1 步，或从第 n-2 阶爬 2 步</li>
 *     <li><strong>无后效性</strong>：到达第 n 阶的方法数只与 n-1 和 n-2 有关</li>
 * </ul>
 * <p>
 * <h3>2. 动态规划五部曲</h3>
 * <h4>(1) dp 数组定义</h4>
 * <ul>
 *     <li><code>dp[i]</code> 表示爬到第 i 阶楼梯的方法数</li>
 *     <li>数组大小：n + 1（包含第 0 阶到第 n 阶）</li>
 * </ul>
 * <p>
 * <h4>(2) 递推公式</h4>
 * <ul>
 *     <li>要到达第 i 阶，有两种方式：</li>
 *     <li>从第 i-1 阶爬 1 步 → 方法数：<code>dp[i-1]</code></li>
 *     <li>从第 i-2 阶爬 2 步 → 方法数：<code>dp[i-2]</code></li>
 *     <li>总方法数：<code>dp[i] = dp[i-1] + dp[i-2]</code></li>
 * </ul>
 * <p>
 * <h4>(3) 初始化</h4>
 * <ul>
 *     <li><code>dp[1] = 1</code>：爬 1 阶楼梯只有 1 种方法（爬 1 步）</li>
 *     <li><code>dp[2] = 2</code>：爬 2 阶楼梯有 2 种方法（1+1 或直接 2 步）</li>
 *     <li>注意：代码中对 n ≤ 1 的情况做了特殊处理</li>
 * </ul>
 * <p>
 * <h4>(4) 遍历顺序</h4>
 * <ul>
 *     <li>从小到大遍历，从 i = 3 开始到 n</li>
 *     <li>保证计算 dp[i] 时，dp[i-1] 和 dp[i-2] 已经计算完成</li>
 * </ul>
 * <p>
 * <h4>(5) 返回值</h4>
 * <ul>
 *     <li>返回 <code>dp[n]</code>，即爬到第 n 阶的方法总数</li>
 * </ul>
 * <p>
 * <h3>3. 算法示意图</h3>
 * <pre>
 * 示例：n = 5
 *
 * 初始化:
 * dp[1] = 1 (1 阶：1 种方法)
 * dp[2] = 2 (2 阶：1+1 或 2，共 2 种方法)
 *
 * i = 3:
 * dp[3] = dp[2] + dp[1]
 *       = 2 + 1 = 3
 * (3 阶：1+1+1, 1+2, 2+1，共 3 种方法)
 *
 * i = 4:
 * dp[4] = dp[3] + dp[2]
 *       = 3 + 2 = 5
 * (4 阶：1+1+1+1, 1+1+2, 1+2+1, 2+1+1, 2+2，共 5 种方法)
 *
 * i = 5:
 * dp[5] = dp[4] + dp[3]
 *       = 5 + 3 = 8
 *
 * 最终结果：8 种方法
 *
 * 规律：这是一个斐波那契数列
 * 1, 2, 3, 5, 8, 13, 21...
 * </pre>
 * <p>
 * <h3>4. 复杂度分析</h3>
 * <ul>
 *     <li><strong>时间复杂度</strong>：O(n)，需要从 3 遍历到 n</li>
 *     <li><strong>空间复杂度</strong>：O(n)，dp 数组的大小</li>
 *     <li><strong>优化方案</strong>：可以使用滚动变量将空间优化到 O(1)</li>
 * </ul>
 *
 * @author lxd
 **/
public class Day20260401 {
    public int climbStairs(int n) {
        // 🛑 特殊情况处理：n <= 1 时直接返回
        if (n <= 1) {
            return n;
        }

        // 📦 dp 数组：dp[i] 表示爬到第 i 阶的方法数
        int[] dp = new int[n + 1];

        // 🎯 初始化基础情况
        dp[1] = 1;  // 1 阶楼梯：只有 1 种方法（爬 1 步）
        dp[2] = 2;  // 2 阶楼梯：有 2 种方法（1+1 或 直接 2 步）

        // 🔄 从第 3 阶开始，根据递推公式计算每阶的方法数
        for (int i = 3; i <= n; i++) {
            // ⚖️ 状态转移方程：
            // 当前阶的方法数 = 前一阶的方法数 + 前两阶的方法数
            // dp[i] = dp[i-1] + dp[i-2]
            dp[i] = dp[i - 1] + dp[i - 2];
        }

        // ✅ 返回爬到第 n 阶的总方法数
        return dp[n];
    }
}

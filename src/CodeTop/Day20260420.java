package CodeTop;

/**
 * <a href="https://leetcode.cn/problems/minimum-path-sum/description/">64. 最小路径和</a>
 *
 * <h3>🎯 题目描述</h3>
 * <p>给定一个包含非负整数的 m x n 网格 grid，请找出一条从左上角到右下角的路径，使得路径上的数字总和为最小。</p>
 * <p><b>说明</b>：每次只能向下或者向右移动一步。</p>
 *
 * <h3>💡 核心思路：动态规划（Dynamic Programming）</h3>
 * <ul>
 *   <li><b>问题特点</b>：
 *     <ul>
 *       <li>只能向右或向下移动 → 无后效性</li>
 *       <li>求最小值 → 最优子结构</li>
 *       <li>重叠子问题 → 适合 DP</li>
 *     </ul>
 *   </li>
 *   <li><b>状态定义</b>：dp[i][j] 表示从 (0,0) 到 (i,j) 的最小路径和</li>
 *   <li><b>状态转移</b>：dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + grid[i][j]
 *     <ul>
 *       <li>从上方来：dp[i-1][j] + grid[i][j]</li>
 *       <li>从左方来：dp[i][j-1] + grid[i][j]</li>
 *       <li>取两者较小值</li>
 *     </ul>
 *   </li>
 *   <li><b>边界条件</b>：第一行和第一列只能从一个方向来</li>
 * </ul>
 *
 * <h3>🔄 算法步骤</h3>
 * <ol>
 *   <li><b>初始化</b>：
 *     <ul>
 *       <li>dp[0][0] = grid[0][0]（起点）</li>
 *       <li>第一列：dp[i][0] = dp[i-1][0] + grid[i][0]（只能从上方来）</li>
 *       <li>第一行：dp[0][j] = dp[0][j-1] + grid[0][j]（只能从左方来）</li>
 *     </ul>
 *   </li>
 *   <li><b>填表</b>：从左到右、从上到下遍历，计算每个位置的最小路径和</li>
 *   <li><b>返回结果</b>：dp[row-1][col-1] 即为答案</li>
 * </ol>
 *
 * <h3>📊 ASCII 演示过程</h3>
 * <pre>
 * 示例：grid =
 *   [1, 3, 1]
 *   [1, 5, 1]
 *   [4, 2, 1]
 *
 * 【Step 1】初始化 dp[0][0]
 * dp:
 *   [1, ?, ?]
 *   [?, ?, ?]
 *   [?, ?, ?]
 *
 * 【Step 2】初始化第一列（只能从上方来）
 * dp[1][0] = dp[0][0] + grid[1][0] = 1 + 1 = 2
 * dp[2][0] = dp[1][0] + grid[2][0] = 2 + 4 = 6
 * dp:
 *   [1, ?, ?]
 *   [2, ?, ?]
 *   [6, ?, ?]
 *
 * 【Step 3】初始化第一行（只能从左方来）
 * dp[0][1] = dp[0][0] + grid[0][1] = 1 + 3 = 4
 * dp[0][2] = dp[0][1] + grid[0][2] = 4 + 1 = 5
 * dp:
 *   [1, 4, 5]
 *   [2, ?, ?]
 *   [6, ?, ?]
 *
 * 【Step 4】填充剩余格子
 *
 * dp[1][1] = min(dp[0][1], dp[1][0]) + grid[1][1]
 *          = min(4, 2) + 5 = 2 + 5 = 7
 *
 * dp[1][2] = min(dp[0][2], dp[1][1]) + grid[1][2]
 *          = min(5, 7) + 1 = 5 + 1 = 6
 *
 * dp[2][1] = min(dp[1][1], dp[2][0]) + grid[2][1]
 *          = min(7, 6) + 2 = 6 + 2 = 8
 *
 * dp[2][2] = min(dp[1][2], dp[2][1]) + grid[2][2]
 *          = min(6, 8) + 1 = 6 + 1 = 7
 *
 * 最终 dp:
 *   [1, 4, 5]
 *   [2, 7, 6]
 *   [6, 8, 7]
 *
 * 答案：dp[2][2] = 7 ✅
 *
 * 【路径回溯】
 * 从 (2,2) 往回找：
 *   (2,2)=7 ← min(6,8) 来自 (1,2)
 *   (1,2)=6 ← min(5,7) 来自 (0,2)
 *   (0,2)=5 ← 来自 (0,1)
 *   (0,1)=4 ← 来自 (0,0)
 *   (0,0)=1
 *
 * 路径：(0,0)→(0,1)→(0,2)→(1,2)→(2,2)
 * 对应值：1→3→1→1→1 = 7 ✅
 * </pre>
 *
 * <h3>🔑 关键技巧解析</h3>
 * <pre>
 * 1️⃣ 为什么可以先初始化第一行和第一列？
 *    - 第一行的每个格子只能从左方来（没有上方）
 *    - 第一列的每个格子只能从上方来（没有左方）
 *    - 所以它们的路径是唯一的，直接累加即可
 *
 * 2️⃣ 状态转移方程的含义？
 *    - dp[i][j] 依赖于 dp[i-1][j]（上方）和 dp[i][j-1]（左方）
 *    - 选择较小的那个，再加上当前格子的值
 *    - 这保证了到达 (i,j) 的路径和最小
 *
 * 3️⃣ 遍历顺序为什么是从左到右、从上到下？
 *    - 因为 dp[i][j] 依赖于左边和上边的值
 *    - 必须保证计算 dp[i][j] 时，dp[i-1][j] 和 dp[i][j-1] 已经计算好
 *    - 从左到右、从上到下满足这个依赖关系
 *
 * 4️⃣ 时间复杂度：O(m × n)
 *    - m 为行数，n 为列数
 *    - 需要填充整个 dp 表格
 *
 * 5️⃣ 空间复杂度：O(m × n)
 *    - 使用了二维 dp 数组
 *    - 优化：可以用一维数组或原地修改，降到 O(n) 或 O(1)
 *
 * 6️⃣ 与不同路径问题的区别？
 *    - 不同路径：求路径数量，dp[i][j] = dp[i-1][j] + dp[i][j-1]
 *    - 最小路径：求最小和，dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + grid[i][j]
 *    - 一个是加法，一个是取最小值
 * </pre>
 *
 * <h3>⚠️ 注意事项</h3>
 * <ul>
 *   <li><b>边界检查</b>：先判断 grid 是否为 null 或空数组</li>
 *   <li><b>初始化顺序</b>：必须先初始化 dp[0][0]，再初始化第一行和第一列</li>
 *   <li><b>索引范围</b>：注意 row 和 col 的边界，避免越界</li>
 *   <li><b>非负整数</b>：题目保证 grid 中都是非负整数，无需处理负数情况</li>
 *   <li><b>空间优化</b>：可以只用一维数组 dp[j]，因为每次只依赖上一行和左边</li>
 *   <li><b>路径回溯</b>：如果需要输出具体路径，可以从终点反向追踪</li>
 * </ul>
 *
 * @author lxd
 **/
public class Day20260420 {
    /**
     * 🛤️ 计算从左上角到右下角的最小路径和
     *
     * @param grid m x n 的非负整数网格
     * @return 最小路径和
     */
    public int minPathSum(int[][] grid) {
        // 🛑 边界检查：空数组处理
        if (grid == null || grid.length == 0) {
            return 0;
        }

        int row = grid.length, col = grid[0].length;
        int[][] dp = new int[row][col];  // 📊 DP 表格

        // 📍 初始化起点
        dp[0][0] = grid[0][0];

        // ⬇️ 初始化第一列：只能从上方来
        for (int i = 1; i < row; i++) {
            dp[i][0] = dp[i - 1][0] + grid[i][0];
        }

        // ➡️ 初始化第一行：只能从左方来
        for (int j = 1; j < col; j++) {
            dp[0][j] = dp[0][j - 1] + grid[0][j];
        }

        // 🔄 填充剩余格子：从左到右、从上到下
        for (int i = 1; i < row; i++) {
            for (int j = 1; j < col; j++) {
                // 状态转移：取上方和左方的较小值 + 当前值
                dp[i][j] = Math.min(dp[i - 1][j], dp[i][j - 1]) + grid[i][j];
            }
        }

        // ✅ 返回右下角的最小路径和
        return dp[row - 1][col - 1];
    }
}

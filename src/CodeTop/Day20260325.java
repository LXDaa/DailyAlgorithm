package CodeTop;

/**
 * <a href="https://leetcode.cn/problems/next-permutation/description/">31. 下一个排列</a>
 * <p>
 * 整数数组的一个 排列  就是将其所有成员以序列或线性顺序排列。
 * <p>
 * 例如，arr = [1,2,3] ，以下这些都可以视作 arr 的排列：[1,2,3]、[1,3,2]、[3,1,2]、[2,3,1] 。
 * 整数数组的 下一个排列 是指其整数的下一个字典序更大的排列。更正式地，如果数组的所有排列根据其字典顺序从小到大排列在一个容器中，那么数组的 下一个排列 就是在这个有序容器中排在它后面的那个排列。如果不存在下一个更大的排列，那么这个数组必须重排为字典序最小的排列（即，其元素按升序排列）。
 * <p>
 * 例如，arr = [1,2,3] 的下一个排列是 [1,3,2] 。
 * 类似地，arr = [2,3,1] 的下一个排列是 [3,1,2] 。
 * 而 arr = [3,2,1] 的下一个排列是 [1,2,3] ，因为 [3,2,1] 不存在一个字典序更大的排列。
 * 给你一个整数数组 nums ，找出 nums 的下一个排列。
 * <p>
 * 必须 原地 修改，只允许使用额外常数空间。
 *
 * <h2>解题思路</h2>
 * <h3>1. 字典序与下一个排列</h3>
 * <ul>
 *     <li><strong>字典序</strong>：按照数字的先后顺序比较大小，类似于字典中单词的排序方式</li>
 *     <li><strong>下一个排列</strong>：当前排列在字典序中的下一个更大的排列</li>
 *     <li>如果已经是最大排列，则返回最小排列（升序）</li>
 * </ul>
 * <p>
 * <h3>2. 算法步骤（两次扫描 + 一次反转）</h3>
 * <h4>第一步：找到第一个下降点</h4>
 * <ul>
 *     <li>从右向左扫描，找到第一个满足 <code>nums[i] < nums[i+1]</code> 的位置 i</li>
 *     <li>这个位置 i 就是需要调整的位置</li>
 *     <li>i 右侧的数字都是降序排列</li>
 * </ul>
 * <p>
 * <h4>第二步：找到交换位置</h4>
 * <ul>
 *     <li>如果找到了 i，从右向左找到第一个大于 nums[i] 的位置 j</li>
 *     <li>交换 nums[i] 和 nums[j]</li>
 *     <li>这样可以保证变化尽可能小</li>
 * </ul>
 * <p>
 * <h4>第三步：反转 i+1 及之后的部分</h4>
 * <ul>
 *     <li>将 i+1 到末尾的部分反转，使其变为升序</li>
 *     <li>这样可以保证得到最小的下一个排列</li>
 * </ul>
 * <p>
 * <h3>3. 算法示意图</h3>
 * <pre>
 * 示例：nums = [1, 2, 3]
 *
 * 第一步：找下降点
 *        [1, 2, 3]
 *             ↑
 *         i=1 (2<3)
 *
 * 第二步：找交换点
 *        [1, 2, 3]
 *                ↑
 *            j=2 (3>2)
 *        交换后：[1, 3, 2]
 *
 * 第三步：反转 i+1 之后
 *        i+1=2 之后只有一个元素，无需反转
 *        结果：[1, 3, 2]
 *
 * 示例：nums = [1, 1, 5]
 * 结果：[1, 5, 1]
 *
 * 示例：nums = [3, 2, 1] （已经是最大）
 * 结果：[1, 2, 3] （反转为最小）
 * </pre>
 * <p>
 * <h3>4. 复杂度分析</h3>
 * <ul>
 *     <li><strong>时间复杂度</strong>：O(n)，最多扫描三次数组</li>
 *     <li><strong>空间复杂度</strong>：O(1)，只使用常数额外空间</li>
 * </ul>
 *
 * @author lxd
 **/
public class Day20260325 {
    public void nextPermutation(int[] nums) {
        int i;
        // 🔍 第一步：从右向左扫描，找到第一个下降点（nums[i] < nums[i+1]）
        for (i = nums.length - 1; i >= 0; i--) {
            if (i + 1 < nums.length && nums[i] < nums[i + 1]) {
                // 🔄 第二步：从右向左找到第一个大于 nums[i] 的位置 j
                for (int j = nums.length - 1; j > i; j--) {
                    if (nums[j] > nums[i]) {
                        // 💱 交换 nums[i] 和 nums[j]
                        swap(nums, i, j);
                        break;
                    }
                }
                // ✅ 找到并交换后，跳出循环
                break;
            }
        }
        // 📍 i 的位置：
        // - 如果找到下降点，i 指向需要调整的较小值
        // - 如果没找到，i = -1，说明整个数组是降序，需要整体反转

        // 🔄 第三步：反转 i+1 到末尾的部分，使其变为升序
        int l = i + 1, r = nums.length - 1;
        while (l < r) {
            swap(nums, l, r);
            l++;
            r--;
        }

    }

    /**
     * 交换数组中两个位置的元素
     *
     * @param nums 数组
     * @param i    位置 i
     * @param j    位置 j
     */
    private void swap(int[] nums, int i, int j) {
        int temp = nums[i];
        nums[i] = nums[j];
        nums[j] = temp;
    }
}

package CodeTop;

/**
 * <a href="https://leetcode.cn/problems/best-time-to-buy-and-sell-stock/description/">121. 买卖股票的最佳时机</a>
 * <p>
 * 给定一个数组 prices ，它的第 i 个元素 prices[i] 表示一支给定股票第 i 天的价格。
 * <p>
 * 你只能选择 某一天 买入这只股票，并选择在 未来的某一个不同的日子 卖出该股票。设计一个算法来计算你所能获取的最大利润。
 * <p>
 * 返回你可以从这笔交易中获取的最大利润。如果你不能获取任何利润，返回 0 。
 * <p>
 * <h3>解题思路</h3>
 * <p>
 * 本题使用动态规划方法解决股票买卖问题，核心思想是维护两个状态：<br>
 * <ol>
 * <li>持有股票状态：当前手中持有股票的最大利润</li>
 * <li>不持有股票状态：当前手中不持有股票的最大利润</li>
 * </ol>
 * <p>
 * <h3>状态定义</h3>
 * <p>
 * dp[i][0] 表示第 i 天不持有股票时的最大利润<br>
 * dp[i][1] 表示第 i 天持有股票时的最大利润<br>
 * <p>
 * <h3>状态转移方程</h3>
 * <p>
 * dp[i][0] = max(dp[i-1][0], dp[i-1][1] + prices[i])<br>
 * &nbsp;&nbsp;含义：第 i 天不持有股票 = max(前一天也不持有, 前一天持有今天卖出)<br>
 * dp[i][1] = max(dp[i-1][1], -prices[i])<br>
 * &nbsp;&nbsp;含义：第 i 天持有股票 = max(前一天就持有, 今天买入)<br>
 * <p>
 * <h3>初始状态</h3>
 * <p>
 * dp[0][0] = 0 (第 0 天不持有股票，利润为 0)<br>
 * dp[0][1] = -prices[0] (第 0 天买入股票，花费 prices[0]，利润为负)<br>
 * <p>
 * <h3>算法步骤</h3>
 * <p>
 * 1. 初始化 dp 数组的第一行<br>
 * 2. 从第 1 天开始遍历，根据状态转移方程更新每个状态<br>
 * 3. 最终答案是最后一天不持有股票的最大利润 dp[n-1][0]<br>
 * <p>
 * <h3>示例演示</h3>
 * <p>
 * 以 prices = [7,1,5,3,6,4] 为例：<br>
 * &nbsp;&nbsp;- 第 0 天：dp[0][0]=0, dp[0][1]=-7<br>
 * &nbsp;&nbsp;- 第 1 天：dp[1][0]=max(0, -7+1)=0, dp[1][1]=max(-7, -1)=-1<br>
 * &nbsp;&nbsp;- 第 2 天：dp[2][0]=max(0, -1+5)=4, dp[2][1]=max(-1, -5)=-1<br>
 * &nbsp;&nbsp;- 第 3 天：dp[3][0]=max(4, -1+3)=4, dp[3][1]=max(-1, -3)=-1<br>
 * &nbsp;&nbsp;- 第 4 天：dp[4][0]=max(4, -1+6)=5, dp[4][1]=max(-1, -6)=-1<br>
 * &nbsp;&nbsp;- 第 5 天：dp[5][0]=max(5, -1+4)=5, dp[5][1]=max(-1, -4)=-1<br>
 * &nbsp;&nbsp;最终答案：dp[5][0] = 5<br>
 * <p>
 * <h3>时间复杂度</h3>
 * <p>
 * O(n)，其中 n 是数组 prices 的长度<br>
 * <p>
 * <h3>空间复杂度</h3>
 * <p>
 * O(n)，需要创建 n×2 的二维数组<br>
 *
 * @author lxd
 **/
public class Day20260204 {
    /**
     * 计算股票买卖的最大利润
     *
     * @param prices 股票每天的价格数组
     * @return 最大利润，如果无法获利则返回 0
     */
    public int maxProfit(int[] prices) {
        int n = prices.length;

        // 创建二维 dp 数组
        // dp[i][0] 表示第 i 天不持有股票的最大利润
        // dp[i][1] 表示第 i 天持有股票的最大利润
        int[][] dp = new int[n][2];

        // 初始化第 0 天的状态
        // 第 0 天不持有股票，利润为 0
        dp[0][0] = 0;
        // 第 0 天持有股票，花费 prices[0]，利润为负
        dp[0][1] = -prices[0];

        // 从第 1 天开始进行状态转移
        for (int i = 1; i < n; i++) {
            // 第 i 天不持有股票的最大利润 = max(前一天就不持有, 前一天持有今天卖出)
            dp[i][0] = Math.max(dp[i - 1][0], dp[i - 1][1] + prices[i]);

            // 第 i 天持有股票的最大利润 = max(前一天就持有, 今天买入)
            // 注意：因为只能买卖一次，所以买入时利润为 -prices[i]
            dp[i][1] = Math.max(dp[i - 1][1], -prices[i]);
        }

        // 最后一天不持有股票的利润一定大于等于持有股票的利润
        return dp[n - 1][0];
    }
}

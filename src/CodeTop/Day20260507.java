package CodeTop;

import java.util.ArrayList;
import java.util.List;

/**
 * <a href="https://leetcode.cn/problems/binary-tree-preorder-traversal/description/">144. 二叉树的前序遍历</a>
 *
 * <h3>🎯 题目描述</h3>
 * <p>给你二叉树的根节点 root ，返回它节点值的 <b>前序遍历</b>。</p>
 * <p><b>前序遍历</b>：按照「根节点 → 左子树 → 右子树」的顺序访问所有节点。</p>
 *
 * <h3>💡 核心思路：递归法</h3>
 * <ul>
 *   <li><b>遍历顺序</b>：根节点 → 左子树 → 右子树（中左右）</li>
 *   <li><b>递归三要素</b>：
 *     <ol>
 *       <li><b>确定参数和返回值</b>：传入当前节点和结果列表，无返回值</li>
 *       <li><b>确定终止条件</b>：当前节点为空时返回</li>
 *       <li><b>确定单层逻辑</b>：先处理根节点，再递归左子树，最后递归右子树</li>
 *     </ol>
 *   </li>
 *   <li><b>时间复杂度</b>：O(n)，每个节点访问一次</li>
 *   <li><b>空间复杂度</b>：O(h)，h 为树的高度，递归调用栈的深度</li>
 * </ul>
 *
 * <h3>🔄 算法步骤</h3>
 * <ol>
 *   <li><b>初始化结果列表</b>：创建空的 ArrayList 存储遍历结果</li>
 *   <li><b>调用递归函数</b>：从根节点开始进行前序遍历</li>
 *   <li><b>递归处理</b>：
 *     <ul>
 *       <li>如果当前节点为空，直接返回（终止条件）</li>
 *       <li>将当前节点值加入结果列表（处理根节点）</li>
 *       <li>递归遍历左子树</li>
 *       <li>递归遍历右子树</li>
 *     </ul>
 *   </li>
 *   <li><b>返回结果</b>：返回包含所有节点值的列表</li>
 * </ol>
 *
 * <h3>📊 ASCII 演示过程</h3>
 * <pre>
 * 示例1：简单二叉树
 *
 *       1
 *      / \
 *     2   3
 *
 * 【前序遍历过程】
 *
 * Step 1: 访问根节点 1
 *   result = [1]
 *   ↓
 * Step 2: 递归左子树（节点 2）
 *   - 访问节点 2
 *   result = [1, 2]
 *   - 节点 2 的左子树为空，返回
 *   - 节点 2 的右子树为空，返回
 *   ↓
 * Step 3: 递归右子树（节点 3）
 *   - 访问节点 3
 *   result = [1, 2, 3]
 *   - 节点 3 的左子树为空，返回
 *   - 节点 3 的右子树为空，返回
 *
 * 最终结果：[1, 2, 3] ✅
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 示例2：复杂二叉树
 *
 *       1
 *      / \
 *     2   3
 *    / \   \
 *   4   5   6
 *
 * 【前序遍历过程】
 *
 * 访问顺序：1 → 2 → 4 → 5 → 3 → 6
 *
 * Step 1: 访问根节点 1
 *   result = [1]
 *   ↓
 * Step 2: 递归左子树（以 2 为根）
 *   - 访问节点 2
 *   result = [1, 2]
 *   - 递归 2 的左子树（节点 4）
 *     * 访问节点 4
 *     result = [1, 2, 4]
 *     * 4 的左右子树都为空，返回
 *   - 递归 2 的右子树（节点 5）
 *     * 访问节点 5
 *     result = [1, 2, 4, 5]
 *     * 5 的左右子树都为空，返回
 *   ↓
 * Step 3: 递归右子树（以 3 为根）
 *   - 访问节点 3
 *   result = [1, 2, 4, 5, 3]
 *   - 3 的左子树为空，返回
 *   - 递归 3 的右子树（节点 6）
 *     * 访问节点 6
 *     result = [1, 2, 4, 5, 3, 6]
 *     * 6 的左右子树都为空，返回
 *
 * 最终结果：[1, 2, 4, 5, 3, 6] ✅
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 示例3：单支树（退化情况）
 *
 *   1
 *    \
 *     2
 *      \
 *       3
 *
 * 【前序遍历过程】
 *
 * Step 1: 访问节点 1
 *   result = [1]
 *   ↓
 * Step 2: 1 的左子树为空，跳过
 *   ↓
 * Step 3: 递归右子树（节点 2）
 *   - 访问节点 2
 *   result = [1, 2]
 *   - 2 的左子树为空，跳过
 *   - 递归 2 的右子树（节点 3）
 *     * 访问节点 3
 *     result = [1, 2, 3]
 *
 * 最终结果：[1, 2, 3] ✅
 *
 * 注意：退化为链表时，空间复杂度为 O(n)
 * </pre>
 *
 * <h3>🔑 关键技巧解析</h3>
 * <pre>
 * 1️⃣ 为什么用递归法？
 *    - 代码简洁，易于理解
 *    - 天然符合树的递归结构
 *    - 前序遍历的逻辑清晰：先处理根，再处理子树
 *
 * 2️⃣ 前序遍历的应用场景？
 *    - 复制二叉树（先创建根节点，再复制子树）
 *    - 序列化二叉树（保存树的结构）
 *    - 表达式树求值（前缀表达式）
 *    - 打印目录结构（先显示文件夹，再显示内容）
 *
 * 3️⃣ 三种遍历方式对比？
 *    - 前序：根左右 → 适合复制、序列化
 *    - 中序：左根右 → BST 中得到有序序列
 *    - 后序：左右根 → 适合删除、计算树高
 *
 * 4️⃣ 为什么需要传递 result 列表？
 *    - Java 中基本类型和引用传递的特性
 *    - 通过传递同一个列表对象，所有递归层共享结果
 *    - 避免每次递归都创建新列表，提高效率
 *
 * 5️⃣ 递归的终止条件？
 *    - 当节点为 null 时返回
 *    - 这保证了不会访问空节点的子节点
 *    - 叶子节点的左右子节点都是 null，会触发终止
 *
 * 6️⃣ 时间复杂度分析？
 *    - 每个节点恰好访问一次
 *    - 每次访问的操作是 O(1)（添加到列表）
 *    - 总体时间复杂度：O(n)
 *
 * 7️⃣ 空间复杂度分析？
 *    - 递归调用栈的深度 = 树的高度 h
 *    - 平衡树：h = log(n)，空间复杂度 O(log n)
 *    - 退化树（链表）：h = n，空间复杂度 O(n)
 *    - 结果列表不计入额外空间（这是输出必需的）
 *
 * 8️⃣ 迭代法如何实现？
 *    - 使用显式栈模拟递归过程
 *    - 先将右子节点入栈，再将左子节点入栈
 *    - 保证左子节点先被处理（栈的 LIFO 特性）
 *    - 迭代法可以避免递归栈溢出问题
 * </pre>
 *
 * <h3>⚠️ 注意事项</h3>
 * <ul>
 *   <li><b>边界条件</b>：空树返回空列表 []</li>
 *   <li><b>单节点</b>：只有一个节点的树，返回 [root.val]</li>
 *   <li><b>列表传递</b>：result 列表在递归过程中被修改，不需要返回值</li>
 *   <li><b>遍历顺序</b>：严格按照「根→左→右」的顺序，不能颠倒</li>
 *   <li><b>递归深度</b>：对于非常深的树，可能栈溢出，可改用迭代法</li>
 *   <li><b>结果顺序</b>：前序遍历的第一个元素一定是根节点</li>
 * </ul>
 *
 * @author lxd
 **/
public class Day20260507 {
    /**
     * 🔍 二叉树的前序遍历（主方法）
     * <p>返回节点值的前序遍历结果</p>
     *
     * @param root 二叉树根节点
     * @return 前序遍历结果列表
     */
    public List<Integer> preorderTraversal(TreeNode root) {
        // 📋 初始化结果列表
        List<Integer> result = new ArrayList<Integer>();
        // 🔄 调用递归方法进行前序遍历
        preorder(root, result);
        // ✅ 返回遍历结果
        return result;
    }

    /**
     * 🛠️ 辅助方法：递归实现前序遍历
     * <p>遍历顺序：根节点 → 左子树 → 右子树</p>
     *
     * @param root   当前处理的节点
     * @param result 存储遍历结果的列表
     */
    public void preorder(TreeNode root, List<Integer> result) {
        // 🛑 终止条件：节点为空时返回
        if (root == null) {
            return;
        }

        // 📝 Step 1: 处理根节点（将值加入结果列表）
        result.add(root.val);

        // 🌿 Step 2: 递归遍历左子树
        preorder(root.left, result);

        // 🌿 Step 3: 递归遍历右子树
        preorder(root.right, result);
    }
}

package Greedy;

/**
 * <a href="https://leetcode.cn/problems/monotone-increasing-digits/description/">738.单调递增的数字</a>
 * <p>
 * 当且仅当每个相邻位数上的数字 x 和 y 满足 x <= y 时，我们称这个整数是单调递增的。
 * <p>
 * 给定一个整数 n ，返回 小于或等于 n 的最大数字，且数字呈 单调递增 。
 * <p>
 * 解题思路:
 * 使用贪心算法解决此问题。从右向左遍历数字，当发现左边位数大于右边时，
 * 将左边位数减1，并记录需要将后面所有位数变为9的位置。
 * 这样可以保证结果是小于等于n的最大单调递增数字。
 * <p>
 * 算法步骤:
 * 1. 将数字转换为字符数组，便于操作每一位数字
 * 2. 从右向左遍历，比较相邻两位数字
 * 3. 如果发现前一位大于后一位，说明不满足单调递增
 *    - 将前一位减1
 *    - 记录当前位置，表示从此位置往后都需要设为9
 * 4. 将标记位置之后的所有数字都设置为9
 * 5. 转换为整数并返回结果
 * <p>
 * 示例:
 * 输入: 332 -> 处理过程: 332 -> 329 -> 299 -> 输出: 299
 * 输入: 1234 -> 已经是单调递增 -> 输出: 1234
 * 输入: 10 -> 处理过程: 10 -> 09 -> 输出: 9
 */
public class Day20251103 {
    public int monotoneIncreasingDigits(int n) {
        // 将数字转换为字符串，再转换为字符数组，便于操作每一位
        String s = String.valueOf(n);
        char[] chars = s.toCharArray();
        int len = s.length();
        
        // 标记需要将之后所有位数设为9的位置，初始值为字符串长度表示不需要设置
        int flag = len;
        
        // 从右向左遍历，检查是否满足单调递增
        // 如果发现前一位数字大于后一位数字，则不满足单调递增
        for (int i = len - 1; i > 0; i--) {
            // 比较相邻的两位数字，如果前一位大于后一位
            if (chars[i - 1] > chars[i]) {
                // 将前一位数字减1，使其变小
                chars[i - 1]--;
                // 记录当前位置，表示从这个位置开始后面所有位数都需要设为9
                flag = i;
            }
        }
        
        // 将标记位置之后的所有数字都设置为9
        // 这样可以保证得到小于等于n的最大单调递增数字
        for (int i = flag; i < len; i++) {
            chars[i] = '9';
        }
        
        // 将处理后的字符数组转回整数并返回
        return Integer.parseInt(String.valueOf(chars));
    }
}
package CodeMusings.DynamicProgramming;

/**
 * <a href="https://leetcode.cn/problems/best-time-to-buy-and-sell-stock-iii/description/">123.买卖股票的最佳时机III</a>
 * 给定一个数组,它的第 i 个元素是一支给定的股票在第 i 天的价格。
 * <p>
 * 设计一个算法来计算你所能获取的最大利润。你最多可以完成 两笔 交易。
 * <p>
 * 注意:你不能同时参与多笔交易(你必须在再次购买前出售掉之前的股票)。
 * <p>
 * 解题思路:
 * 本题是股票买卖问题的进阶版本,限制最多完成两笔交易。
 * 核心在于用动态规划跟踪每一天在不同交易状态下的最大收益。
 * <p>
 * 1. 状态定义:
 * dp[i][j] 表示第i天处于状态j时的最大收益
 * j的取值范围:
 * - 0: 未进行任何操作
 * - 1: 第一次买入股票后的最大收益
 * - 2: 第一次卖出股票后的最大收益
 * - 3: 第二次买入股票后的最大收益
 * - 4: 第二次卖出股票后的最大收益
 * <p>
 * 2. 状态转移方程:
 * dp[i][1] = max(dp[i-1][1], -prices[i])
 * 要么保持前一天第一次买入的状态,要么今天第一次买入
 * dp[i][2] = max(dp[i-1][2], dp[i-1][1] + prices[i])
 * 要么保持前一天第一次卖出的状态,要么今天第一次卖出
 * dp[i][3] = max(dp[i-1][3], dp[i-1][2] - prices[i])
 * 要么保持前一天第二次买入的状态,要么今天第二次买入
 * dp[i][4] = max(dp[i-1][4], dp[i-1][3] + prices[i])
 * 要么保持前一天第二次卖出的状态,要么今天第二次卖出
 * <p>
 * 3. 初始状态:
 * dp[0][0] = 0 (第0天未操作,收益为0)
 * dp[0][1] = -prices[0] (第0天买入,收益为负的股价)
 * dp[0][2] = 0 (第0天买入又卖出,收益为0)
 * dp[0][3] = -prices[0] (第0天进行第二次买入,收益为负的股价)
 * dp[0][4] = 0 (第0天两次买卖,收益为0)
 * <p>
 * 4. 最终答案:
 * dp[len-1][4] 表示最后一天完成两笔交易后的最大收益
 * <p>
 * 时间复杂度: O(n) 其中n为数组长度
 * 空间复杂度: O(n) 可优化为O(1)
 *
 * @author lxd
 **/
public class Day20251208 {
    /**
     * 方法一: 二维动态规划
     * 使用二维数组记录每一天在不同交易状态下的最大收益
     *
     * @param prices 股票价格数组
     * @return 最多完成两笔交易的最大收益
     */
    public int maxProfit(int[] prices) {
        int len = prices.length;

        /*
         * dp数组定义:
         * dp[i][j] 表示第i天处于状态j时的最大收益
         * j=0: 未操作, j=1: 第一次买入, j=2: 第一次卖出
         * j=3: 第二次买入, j=4: 第二次卖出
         */
        int[][] dp = new int[len][5];

        /*
         * 初始化第0天的各种状态
         */

        /* 第0天第一次买入,花费prices[0],收益为负 */
        dp[0][1] = -prices[0];

        /* 第0天第一次卖出,当天买入又卖出,收益为0 */
        dp[0][2] = 0;

        /* 第0天第二次买入,相当于买入又卖出再买入,收益为-prices[0] */
        dp[0][3] = -prices[0];

        /* 第0天第二次卖出,两次交易在同一天完成,收益为0 */
        dp[0][4] = 0;

        /*
         * 从第1天开始遍历,根据状态转移方程更新每个状态
         */
        for (int i = 1; i < len; i++) {
            /* 第一次买入: 保持前一天的状态 或 今天第一次买入 */
            dp[i][1] = Math.max(dp[i - 1][1], -prices[i]);

            /* 第一次卖出: 保持前一天的状态 或 今天卖出第一次买入的股票 */
            dp[i][2] = Math.max(dp[i - 1][2], dp[i - 1][1] + prices[i]);

            /* 第二次买入: 保持前一天的状态 或 今天进行第二次买入 */
            dp[i][3] = Math.max(dp[i - 1][3], dp[i - 1][2] - prices[i]);

            /* 第二次卖出: 保持前一天的状态 或 今天卖出第二次买入的股票 */
            dp[i][4] = Math.max(dp[i - 1][4], dp[i - 1][3] + prices[i]);
        }

        /* 返回最后一天完成两笔交易的最大收益 */
        return dp[len - 1][4];
    }

    /**
     * 方法二: 空间优化版本
     * 使用四个变量代替二维数组,将空间复杂度优化到O(1)
     * 本质上是滚动数组的思想,因为每天的状态只依赖前一天
     *
     * @param prices 股票价格数组
     * @return 最多完成两笔交易的最大收益
     */
    public int maxProfit2(int[] prices) {
        int n = prices.length;

        /*
         * 四个变量分别表示四种状态的最大收益:
         * buy1: 第一次买入后的最大收益
         * sell1: 第一次卖出后的最大收益
         * buy2: 第二次买入后的最大收益
         * sell2: 第二次卖出后的最大收益
         */
        int buy1 = -prices[0], sell1 = 0;
        int buy2 = -prices[0], sell2 = 0;

        /*
         * 从第1天开始遍历,不断更新四种状态
         */
        for (int i = 1; i < n; ++i) {
            /* 更新第一次买入状态: 保持原状态 或 今天买入 */
            buy1 = Math.max(buy1, -prices[i]);

            /* 更新第一次卖出状态: 保持原状态 或 基于buy1今天卖出 */
            sell1 = Math.max(sell1, buy1 + prices[i]);

            /* 更新第二次买入状态: 保持原状态 或 基于sell1今天买入 */
            buy2 = Math.max(buy2, sell1 - prices[i]);

            /* 更新第二次卖出状态: 保持原状态 或 基于buy2今天卖出 */
            sell2 = Math.max(sell2, buy2 + prices[i]);
        }

        /* 返回第二次卖出后的最大收益 */
        return sell2;
    }


}

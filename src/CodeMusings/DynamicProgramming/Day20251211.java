package CodeMusings.DynamicProgramming;

/**
 * <a href="https://leetcode.cn/problems/best-time-to-buy-and-sell-stock-with-transaction-fee/description/">714.买卖股票的最佳时机含手续费</a>
 * 给定一个整数数组 prices，其中 prices[i]表示第 i 天的股票价格 ；整数 fee 代表了交易股票的手续费用。
 * <p>
 * 你可以无限次地完成交易，但是你每笔交易都需要付手续费。如果你已经购买了一个股票，在卖出它之前你就不能再继续购买股票了。
 * <p>
 * 返回获得利润的最大值。
 * <p>
 * 注意：这里的一笔交易指买入持有并卖出股票的整个过程，每笔交易你只需要为支付一次手续费。
 *
 * @author lxd
 **/
public class Day20251211 {
    /**
     * 解题思路：动态规划
     * 这是一道典型的股票买卖问题，可以无限次交易但每次需要支付手续费
     * 使用动态规划来求解最大利润
     * <p>
     * 状态定义：
     * dp[i][0] 表示第i天不持有股票时的最大利润
     * dp[i][1] 表示第i天持有股票时的最大利润
     * <p>
     * 状态转移方程：
     * dp[i][0] = max(dp[i-1][0], dp[i-1][1] + prices[i] - fee)
     * - dp[i-1][0]：昨天就不持有股票，今天保持不变
     * - dp[i-1][1] + prices[i] - fee：昨天持有股票，今天卖出（需要扣除手续费）
     * <p>
     * dp[i][1] = max(dp[i-1][1], dp[i-1][0] - prices[i])
     * - dp[i-1][1]：昨天就持有股票，今天保持不变
     * - dp[i-1][0] - prices[i]：昨天不持有股票，今天买入
     * <p>
     * 初始状态：
     * dp[0][0] = 0：第0天不持有股票，利润为0
     * dp[0][1] = -prices[0]：第0天持有股票，需要花费prices[0]
     * <p>
     * 最终答案：
     * dp[n-1][0]：最后一天不持有股票时的最大利润（必须卖出才能获得最大利润）
     */
    public int maxProfit(int[] prices, int fee) {
        int n = prices.length;

        /*
         * 创建dp数组
         * dp[i][0]表示第i天不持有股票的最大利润
         * dp[i][1]表示第i天持有股票的最大利润
         */
        int[][] dp = new int[n][2];

        /*
         * 初始化第0天的状态
         * 第0天不持有股票，利润为0
         */
        dp[0][0] = 0;

        /*
         * 第0天持有股票，需要花费prices[0]
         */
        dp[0][1] = -prices[0];

        /*
         * 从第1天开始遍历，计算每天的最大利润
         */
        for (int i = 1; i < n; i++) {
            /*
             * 计算第i天不持有股票的最大利润
             * 要么昨天就不持有，要么今天卖出（卖出时扣除手续费）
             */
            dp[i][0] = Math.max(dp[i - 1][0], dp[i - 1][1] + prices[i] - fee);

            /*
             * 计算第i天持有股票的最大利润
             * 要么昨天就持有，要么今天买入
             */
            dp[i][1] = Math.max(dp[i - 1][1], dp[i - 1][0] - prices[i]);
        }

        /*
         * 返回最后一天不持有股票的最大利润
         * 因为不持有股票时的利润一定大于等于持有股票时的利润
         */
        return dp[n - 1][0];
    }
}

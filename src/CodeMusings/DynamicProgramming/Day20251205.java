package CodeMusings.DynamicProgramming;

/**
 * <a href="https://leetcode.cn/problems/best-time-to-buy-and-sell-stock-ii/description/">122.买卖股票的最佳时机II</a>
 * 给你一个整数数组 prices ，其中 prices[i] 表示某支股票第 i 天的价格。
 * <p>
 * 在每一天，你可以决定是否购买和/或出售股票。你在任何时候 最多 只能持有 一股 股票。然而，你可以在 同一天 多次买卖该股票，但要确保你持有的股票不超过一股。
 * <p>
 * 返回 你能获得的 最大 利润 。
 * <p>
 * 解题思路：
 * 1. 这是一道经典的动态规划问题，关键在于理解股票的两种状态：持有股票和不持有股票
 * 2. 与只能交易一次的问题不同，本题可以多次买卖，因此买入股票时需要考虑之前的收益
 * 3. 使用二维数组 dp[i][j] 记录第 i 天的状态：
 * - dp[i][0]：第 i 天不持有股票的最大收益
 * - dp[i][1]：第 i 天持有股票的最大收益
 * 4. 状态转移方程：
 * - dp[i][0] = max(dp[i-1][0], dp[i-1][1] + prices[i])
 * 不持有股票可能来自：昨天就不持有，或昨天持有今天卖出
 * - dp[i][1] = max(dp[i-1][1], dp[i-1][0] - prices[i])
 * 持有股票可能来自：昨天就持有，或昨天不持有今天买入（注意这里用 dp[i-1][0]，说明可以多次交易）
 * 5. 初始状态：第0天不持有股票收益为0，持有股票收益为-prices[0]
 * 6. 最终答案：第n-1天不持有股票的最大收益（最后一定是不持有股票收益最大）
 *
 * @author lxd
 **/
public class Day20251205 {
    /**
     * 计算买卖股票的最大利润（可多次交易）
     *
     * @param prices 股票每天的价格数组
     * @return 能获得的最大利润
     */
    public int maxProfit(int[] prices) {
        int n = prices.length;

        /*
         * dp[i][0]: 第 i 天不持有股票的最大收益
         * dp[i][1]: 第 i 天持有股票的最大收益
         */
        int[][] dp = new int[n][2];

        /*
         * 初始化第0天的状态
         * 第0天不持有股票，收益为0
         */
        dp[0][0] = 0;

        /*
         * 第0天持有股票，说明买入了股票，收益为负的股票价格
         */
        dp[0][1] = -prices[0];

        /*
         * 从第1天开始遍历，计算每天的最大收益
         */
        for (int i = 1; i < n; i++) {
            /*
             * 第 i 天不持有股票的最大收益来自两种情况：
             * 1. 前一天就不持有股票，今天继续不持有：dp[i-1][0]
             * 2. 前一天持有股票，今天卖出：dp[i-1][1] + prices[i]
             * 取两者的最大值
             */
            dp[i][0] = Math.max(dp[i - 1][0], dp[i - 1][1] + prices[i]);

            /*
             * 第 i 天持有股票的最大收益来自两种情况：
             * 1. 前一天就持有股票，今天继续持有：dp[i-1][1]
             * 2. 前一天不持有股票，今天买入：dp[i-1][0] - prices[i]
             *    注意：这里用 dp[i-1][0] 而不是 0，说明可以多次买卖
             *    买入时要在之前的收益基础上减去今天的股价
             * 取两者的最大值
             */
            dp[i][1] = Math.max(dp[i - 1][1], dp[i - 1][0] - prices[i]);
        }

        /*
         * 返回最后一天不持有股票的最大收益
         * 因为如果最后一天还持有股票，说明没有卖出，收益一定不是最大的
         */
        return dp[n - 1][0];
    }
}

package DynamicProgramming;

/**
 * <a href="https://leetcode.cn/problems/house-robber/description/">198.打家劫舍</a>
 * 你是一个专业的小偷，计划偷窃沿街的房屋。每间房内都藏有一定的现金，影响你偷窃的唯一制约因素就是相邻的房屋装有相互连通的防盗系统，
 * 如果两间相邻的房屋在同一晚上被小偷闯入，系统会自动报警。
 * <p>
 * 给定一个代表每个房屋存放金额的非负整数数组，计算你 不触动警报装置的情况下 ，一夜之内能够偷窃到的最高金额。
 * <p>
 * 解题思路:
 * 这是一个经典的动态规划问题。对于每一间房子，小偷都有两种选择：偷或不偷。
 * <p>
 * 状态定义:
 * dp[i] 表示在前 i+1 间房子中能偷到的最大金额（即考虑 houses[0..i]）。
 * <p>
 * 状态转移方程:
 * 对于第 i 间房子，有两种选择：
 * 1. 偷第 i 间房子：那么就不能偷第 i-1 间房子，最大金额为 dp[i-2] + nums[i]
 * 2. 不偷第 i 间房子：那么最大金额为 dp[i-1]
 * 取两种选择中的较大值：dp[i] = max(dp[i-2] + nums[i], dp[i-1])
 * <p>
 * 边界条件:
 * - 当只有一间房子时，只能偷这间房子：dp[0] = nums[0]
 * - 当只有两间房子时，选择金额较高的一间：dp[1] = max(nums[0], nums[1])
 * <p>
 * 时间复杂度: O(n)，其中 n 是房屋数量
 * 空间复杂度: O(n)，用于存储 dp 数组
 *
 * @author lxd
 **/
public class Day20251201 {
    public int rob(int[] nums) {
        // 获取房屋数量
        int n = nums.length;

        // 边界条件：如果没有房屋，偷不到任何金额
        if (n == 0) {
            return 0;
        }

        // 边界条件：如果只有一间房屋，只能偷这间
        if (n == 1) {
            return nums[0];
        }

        // 创建 dp 数组，dp[i] 表示前 i+1 间房屋能偷到的最大金额
        int[] dp = new int[n];

        // 初始化基础情况
        // 只考虑第一间房屋时，最大金额就是这间房屋的钱
        dp[0] = nums[0];
        // 考虑前两间房屋时，只能选择其中一间，选金额较大的那间
        dp[1] = Math.max(nums[0], nums[1]);

        // 从第三间房屋开始进行动态规划
        for (int i = 2; i < n; i++) {
            // 对于第 i 间房屋，有两种选择：
            // 1. 偷这间房屋：最大金额为 dp[i-2] + nums[i]（不能偷相邻的第 i-1 间）
            // 2. 不偷这间房屋：最大金额为 dp[i-1]（保持之前的最大金额）
            // 取两种选择中的较大值
            dp[i] = Math.max(dp[i - 2] + nums[i], dp[i - 1]);
        }

        // 返回考虑所有房屋时能偷到的最大金额
        return dp[n - 1];
    }
}